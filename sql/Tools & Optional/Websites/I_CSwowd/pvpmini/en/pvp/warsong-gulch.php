<h3 class = "title">Welcome to Warsong Gulch</h3>
<blockquote>
The development team is hard at work putting the finishing touches on this, the second battleground.  <a href = "battlegrounds.php">Alterac Valley</a> is designed to be the epic battle, with teams of 40 fighting each other for domination, while Warsong Gulch is meant to be the 10-man-team mini-campaign.  A maximum of 20 people, 10 per side, may enter into a game of Warsong Gulch.
<p>
By releasing Warsong Gulch with Alterac Valley, we want to demonstrate that a battleground's core focus is fun and engaging team gameplay.  Everything else is flexible.
</blockquote>
<h3 class = "title">Concept</h3>
<blockquote>
The goal is to take the other team's flag and bring it back to your own base while your flag remains intact.  A capture wins the round, and three captures wins the battle.
<p>
<table border="0" cellpadding="0" cellspacing="0" align = "right"><tr><td><a href = "../../shared/wow-com/images/pvp/gulch/ss06.jpg"><img src = "../../shared/wow-com/images/pvp/gulch/ss06-thumb.jpg" border = "0"></a></td></tr><tr><td align = "center"><span><a href = "../../shared/wow-com/images/pvp/gulch/ss06.jpg">Warsong Gulch Map</a></span></td></tr></table>Warsong Gulch is designed for small, balanced teams, and a game of Warsong will not start without the requisite 10 members per side.  We want to make Warsong Gulch a fast-paced affair, and team balancing is a very important part of making the experience fun for all participants.  In addition, players will be grouped by level sets of (10-19, 20-29, 30-39, 40-49, 50-59, and 60).   Every consideration was made to create an environment you can jump into and get involved in right away.
<p>
There are two bases on the field - Silverwing Hold to the north and Warsong Lumber Mill to the south.  In between lies a dense field of brush and tree stumps - perfect for covering your tracks in your comings and goings.
</blockquote>
<h3 class = "title">Silverwing Hold vs. Warsong Lumber Mill</h3>
<blockquote>
Each base has three entrances.  The main entrance is a long ramp located in the front of the base.  Within the ramp are Boots of Speed (a special battleground item) that will grant you greatly increased run speed for several seconds.  The other two entrances either take you straight into the flag room or onto a balcony that overlooks it.  These entrances are closer to the flag, but they also take longer to reach and have no special items along the way.  Attached to the ramp is a detour that leads to the top of the base.  This path, while a great deal longer, is a good strategic point from which to view the entire flag room while avoiding detection.
<p>
Located on the field, close to each base, are several huts that also house  special items which can give advantages to the players who obtain them.
</blockquote>
<h3 class = "title">Flag carriers</h3>
<blockquote>
<table border="0" cellpadding="0" cellspacing="0" align = "right"><tr><td><a href = "../../shared/wow-com/images/pvp/gulch/ss02.jpg"><img src = "../../shared/wow-com/images/pvp/gulch/ss02-thumb.jpg" border = "0"></a></td></tr><tr><td align = "center"><span><a href = "../../shared/wow-com/images/pvp/gulch/ss02.jpg">Retrieving the Flag</a></span></td></tr></table>True to its massively multiplayer roots, Warsong Gulch is designed for team play, and players will quickly discover the value of teamwork in determining the outcome of a battle.
<p>
Should a player take the enemy's flag, that player will not be able to mount, forcing him or her to run the length of the field back to the home base.   Without support players to escort the flag carrier, the enemy could quickly reclaim the flag.  Likewise, a single defender would be hard pressed to stop an attack party.  Thus, everything must be coordinated in a team effort of balancing offense vs. defense.
</blockquote>
