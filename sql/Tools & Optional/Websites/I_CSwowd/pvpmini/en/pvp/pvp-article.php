<h3 class="title">The Three Cornerstones of World of Warcraft</h3> <small>August 2005 </small> <br />
<p>
<img src="../../shared/wow-com/images/smallcaps/plain/w.gif" width="40" height="38" align="left">hen we first started making World of Warcraft, there were 3 major components to the game. One of course was the adventuring aspect of the game, where you play against AI-controlled monsters and the environment with a group of friends. The second component is what we internally call the "World is Toy" aspect: making sure there are plenty of things to do in the world that are fun but that have nothing to do with character advancement. The third component is player versus player gameplay. All the games that Blizzard has released have always been competitive and our fans look to us to provide that type of play. So we felt that had to be a core component of World of Warcraft.
<p>
<center>
<a href="javascript: popUpOmega('../../shared/wow-com/images/pvp/article/battleground-big.jpg',1000,315,'Battleground Concept')">
