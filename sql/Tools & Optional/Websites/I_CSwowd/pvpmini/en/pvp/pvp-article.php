<p>
<h3 class="title">The World Supports PvP</h3>
<p>
<img src="../../shared/wow-com/images/smallcaps/plain/o.gif" width="40" height="38" align="left">ne very important motivation in designing PvP into the World of Warcraft was to make it feel like Warcraft. Warcraft - starting back in the day with Orcs & Humans - was about the war between Horde and Alliance. We have expanded the Alliance and Horde to encompass more races within each side, but it's still at its core a game about the conflict between the two factions.
<p>
<table cellspacing="0" cellpadding="0" border="0" align="right"><tr><td align="center"><span><a href="javascript: popUpOmega('../../shared/wow-com/images/pvp/article/ss02.jpg',700,576,'Orcish Demolisher')"><img src = "../../shared/wow-com/images/pvp/article/demolisher.jpg" width = "209" height = "179" border = "0"><br>Orc Demolisher PvP Vehicle</a></span></td></tr></table>
We designed our world to reflect this tension and to create situations that bring players into conflict with the other side. Our quests encourage you to attack the NPCs of the opposing faction. We also set up our zones with conflicts already ongoing between the two sides, and then ask you to participate in those conflicts. For example, in the Hillsbrad zone, there is both a human town and an undead town, and the undead town has a series of quests that ask you to go to an Alliance village and defeat human peasant NPCs. We feel that is a great way to remind the players that a war is still raging between the Horde and Alliance. It gives both sides reasons to dislike the other players. An undead player in Hillsbrad is made to feel like the humans are his enemies, and vice versa.
<p>
The way we handle languages also supports this division between Horde and Alliance. The two sides can't communicate with each other. That's actually not a hard and fast rule; they just speak different languages. There is the ability for any particular race to learn a language of the opposing side, but again, since you can't really communicate with people from the other side, there is already a barrier there between you and them. You already have friends that you speak with versus enemies with whom you cannot.
<p>
<h3 class="title">The Goal of PvP in WoW</h3>
<p>
<table cellspacing = "0" cellpadding = "0" border = "0" align = "right"><tr><td align = "center"><small><a href = "javascript: popUpOmega('../../shared/wow-com/images/pvp/article/ss15.jpg',700,525,'Storming the bridge')"><img src = "../../shared/wow-com/images/pvp/article/ss15-thumb.jpg" width = "202" height = "143" border = "0"></a><br><br></small></td></tr></table>
<img src = "../../shared/wow-com/images/smallcaps/plain/w.gif" width = "40" height = "38" align = "left">e really wanted to make our PvP system unique, and we wanted to create a way for everyone to participate in PvP combat if they wanted to. Many MMORPGs out there now segregate their adventuring groups from their PvP groups. Some games are "PvP-centric" while other games are focused only on adventuring. Even in those particular games you'll see that the designers have created special rules Realms where people can enjoy PvP even though that wasn't what the game was designed for. We really wanted to bridge the gap. We wanted to design a game where people could have a good time adventuring and at the same time engage in PvP whenever they want, and be rewarded for doing so. That was the overarching goal.
<p>
<h3 class = "title">Team-Based PvP</h3>
<p>
<table cellspacing = "0" cellpadding = "0" border = "0" align = "right"><tr><td align = "center"><small><a href = "javascript: popUpOmega('../../shared/wow-com/images/pvp/article/ss04.jpg',700,507,'Reign of Chaos?')"><img src = "../../shared/wow-com/images/pvp/article/ss04-thumb.jpg" width = "202" height = "143" border = "0"></a><br><br></small></td></tr></table>
<img src = "../../shared/wow-com/images/smallcaps/plain/o.gif" width = "40" height = "38" align = "left">ur goal has always been to make our PvP system team-based. If you look at a lot of competitive sports, even competitive games, it is more fun to be on a team than to be by yourself.
<p>
One of the main reasons why - other than for story reasons - that we wanted to go with a team-based PvP system was to give people a sense of community. When you first start the game, whether you join the Horde or Alliance, you will know right away who your friends are and who your enemies are. You immediately have teammates who will jump to your defense if you get attacked in a PvP area, and you know innately who to attack and who not to attack.
<p>
Another benefit of team-based PvP is that it splits the sides evenly and makes our communities smaller, which in turn makes it a lot easier for players to develop friendships with their groups, create guilds, and to get to know everyone in the community.
<p>
