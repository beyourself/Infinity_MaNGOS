<span>
<p>
<img src = "../../shared/wow-com/images/smallcaps/plain/t.gif" align = "left">his is our final in-depth look at the upcoming player-versus-player systems planned for World of Warcraft, and it deals with the new Honor System and Rewards. When these systems are deployed, there will be many more incentives for players to fight each other than just the thrill of the kill. And honorable PvP players will receive just rewards for fighting players that are of appropriate levels. We're in the midst of testing these systems, but there is already a lot that we can share with you.
<p>
As we have mentioned before, when you kill other players in your level range you will receive an honorable kill. All your honorable kills for a week are then calculated to give you an honor score for that week, which then translates into an honor ranking. This honor ranking carries with it titles and material rewards, and eventually, officer status and other perks.
<p>
<h3 class = "title">Honorable Kills</h3>
<p>
<table cellspacing = "0" cellpadding = "0" border = "0" align = "right"><tr><td><a href = "../../shared/wow-com/images/pvp-2/ss06.jpg"><img src = "../../shared/wow-com/images/pvp-2/ss06-thumb.jpg" border = "0"></a></td></tr><tr><td align = "center"><span><small><a href = "../../shared/wow-com/images/pvp-2/ss06.jpg">Honorable Kill!</a></small></span></td></tr></table><img src = "../../shared/wow-com/images/smallcaps/plain/t.gif" align = "left">he way we calculate honor is fairly detailed, and a lot goes on under the hood while we crunch numbers to arrive at your final ranking for the week. However, what players will see when they kill an appropriate-level target is a simple message. We can't actually display your honor score from a kill when it happens because we don't determine the final value of a kill until the end of the day. Also, your honor points for the week are a percentage of the total honor points available based on your contribution to your team's overall effort for that week.
<p>
If you are grouped with others, then all players in your party also gain an Honorable Kill. In fact, it works just like experience points. And just like experience points, you'll get a group bonus for your Honorable Kill as well. At this point, the way we determine whether your opponent counts as an Honorable Kill works similarly to how we determine whether you will get experience from a monster. If you would get experience for killing a monster of the same level as the opposing player (i.e. their level number is not colored gray to you), then you'll get an Honorable Kill.
<p>
In some cases, you might end up joining with other groups to slay one or more enemy players. In those instances, we will not be using tapping rules to prevent other groups from earning an Honorable Kill as well. If multiple groups team up to kill a player, all groups will earn a percentage of honor based on how much damage they dealt to the defeated player. For example, imagine that an enemy player is killed by two other players. Player A was solo and did 35% of the damage, while player B was in a group and did 65% of the damage. Player A would get 35% of the honor from the kill, and Player B's party would get 65% of the honor. That honor would then have a group honor bonus applied to it before being divided amongst the group. Rest assured that even players that did not engage the enemy player (perhaps because they were healing instead of fighting) will get honor points; as long as you are in a group that helped kill an opposing player, you'll get honor.
<p>
The amount of honor you gain is based mainly on three factors: 1) your target's level, 2) your PvP rank, and 3) your level. Then, at the end of the week, we gather all your Honorable Kills and Dishonorable Kills, and then apply certain modifications based on relevant data. For instance, we track repeat kills, and the system will reward you less and less honor with each successive kill of the same opponent on the same day. You'll gain full points for your first kill, but you get far less points with each kill after that, to the point were you will eventually get zero if you continue to prey on that player. We also add a bonus to your score for participation in strategic objectives, such as conquering a Battleground or killing an enemy NPC leader, such as Thrall or King Bronzebeard. The exact number of strategic leaders has yet to be determined, but at the least, the four racial leaders for the Horde and Alliance will count as strategic objectives. For those out to kill the human leader, keep in mind that neither King Anduin nor Jaina actually qualifies as a strategic objective; it's the king's protector, Highlord Bolvar Fordragon, who is the true leader you must kill in order to gain bonus honor points for your faction.
<p>
<h3 class = "title">Dishonorable Kills</h3>
<p>
<img src = "../../shared/wow-com/images/smallcaps/plain/p.gif" align = "left">layer versus player battles are always best when played out between opposing forces of equal level. Thus, our system creates incentives to attack foes around your level. And it also does not reward those who kill players far below their level. If the player or PvP-enabled NPC you kill is trivial (i.e. gray to you), then you won't get an Honorable Kill. In fact, if you kill an NPC who is not PvP-enabled or who is PvP-enabled and trivial, you will gain a Dishonorable Kill. This exacts a penalty on those who, for whatever reason, kill trivial NPC bystanders. To make it easier to identify a potential Dishonorable Kill, such NPCs will be labeled as "civilians" in their tooltip. Note that dishonor does not apply to killing players, regardless of their level.
<p>
<h3 class = "title">Tracking PvP Rankings</h3>
<p>
<img src = "../../shared/wow-com/images/smallcaps/plain/a.gif" align = "left">t the end of the week, we will total everyone's honor points including modifications, and then compare everyone's scores against each other. Based on this, we arrive at 14 rankings for all PvP participants. Your final honor score for the week is thus a percentage of the total honor pool that is established for that week. The honor pool is set at a minimum value, and can be increased through the successful achievement of the strategic objectives outlined before. Because we will recalculate all ranks weekly, players can move up or down in rankings based on activity or inactivity. We will not reset players' points each week, so players don't start at zero and on equal footing each time we recalculate honor scores, but rankings will start to shift if a player begins to engage in more quality PvP action or begins to withdraw from PvP play. We anticipate that it will be quite some time before players eventually climb up to the higher ranks, as the system naturally requires players to amass large quantity of quality kills in order to break through to the highest levels. This could take several weeks, if not months, to happen.
<p>
<table cellspacing = "0" cellpadding = "0" border = "0" align = "right"><tr><td>&nbsp;&nbsp;<a href = "../../shared/wow-com/images/pvp-2/ss07.jpg"><img src = "../../shared/wow-com/images/pvp-2/ss07-thumb.jpg" border = "0"></a></td></tr><tr><td align = "center"><span><small><a href = "../../shared/wow-com/images/pvp-2/ss07.jpg">Honor Tab</a></small></span></td></tr></table>To display a player's PvP rank, we will be adding a new element to your character info sheet that shows your current rank. Your character sheet will have an "Honor" tab that tells you how many Honorable and Dishonorable Kills you're accumulated for your current play session, the previous day, and last week. The tab will also show players a few other stats, such as the number of points awarded them during the previous week and their overall ranking for that period as well.
<p>
<h3 class = "title">PvP Rankings and Rewards</h3>
<p>
<table cellspacing = "0" cellpadding = "0" border = "0" align = "right"><tr><td><a href = "../../shared/wow-com/images/pvp-2/ss10.jpg"><img src = "../../shared/wow-com/images/pvp-2/ss10-thumb.jpg" border = "0"></a></td></tr><tr><td align = "center"><span><small><a href = "../../shared/wow-com/images/pvp-2/ss10.jpg">Officer's Barracks</a></small></span></td></tr></table><img src = "../../shared/wow-com/images/smallcaps/plain/t.gif" align = "left">here are a total of 14 ranks in our PvP system. The lowest five ranks are considered the rank and file of the PvP army, while the ranks from six and higher are considered officers. This distinction is important because once players achieve an officer rank they gain access to the officer's barracks, where some of the best PvP rewards are contained. We are also aware that it breaks the sense of immersion in the game to have a top heavy system with more higher-ranked players than lower-ranked ones. The system is thus designed so that most players will be settled into the "enlisted" ranks, while each successively higher rank will be populated by fewer and fewer players. The highest rank of 14, for example, will only be occupied by the top 0.1 percent of players (one in every one-thousand characters). You'll need to fight furiously and honorably to climb up the ranks of our PvP system and keep it up to stay there.
<p>
<table cellspacing = "0" cellpadding = "0" border = "0" align = "right"><tr><td><a href = "../../shared/wow-com/images/pvp-2/ss09.jpg"><img src = "../../shared/wow-com/images/pvp-2/ss09-thumb.jpg" border = "0"></a></td></tr><tr><td align = "center"><span><small><a href = "../../shared/wow-com/images/pvp-2/ss09.jpg">PvP Reward Mount</a></small></span></td></tr></table>Although we've been calling the material benefits of a PvP rank rewards, it would be more accurate to say that achieving a rank gives you the right to buy certain exclusive items. You don't necessarily gain a free item when you hit a certain rank, but your rank does entitle you to buy and use a "reward" item. And in all cases, these reward items will be priced substantially lower because they are rewards. The elite commander's PvP mount you can buy when you reach rank 11, for example, costs one-tenth the price of a normal elite mount. You can use that as a general guideline for how much (or how little) a PvP reward item will cost you when you attain the right rank to buy it.
