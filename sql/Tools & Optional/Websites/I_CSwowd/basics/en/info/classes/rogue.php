<font color=red>This page does not have a complete listing of all abilities that are available. Look for more once our database section is active. </font>
<P>
<B>Energy</B><BR>
The Rogue requires energy to perform most abilities. Energy is built up over time.
You can only increase your energy pool or the rate at which it regenerates through the use of high-end talents.
Abilities cost less energy if they miss or are blocked.
<P>
<B>Stealth</B><BR>
Stealth is the ability to remain undetected by the enemy. It is not the same as invisibility. When something is in
Stealth mode it can still be seen. However, players in Stealth mode become clear or transparent and blend in with the background
making them more difficult to spot.
<UL>
<LI>Being behind a target is much better than in front of a target.
<LI>The better you are at stealth, the closer you can approach a target before he spots you.
<LI>Full movement is allowed while stealthed.  In other words, you don't get a bonus for standing still.
<LI>Abilities will not cancel Stealth/Vanish when they fail to go off.
<LI>Your success at remaining undetected depends on your Stealth skill level, the direction the monster is facing, and how close you get to the monster.
</Ul>
<P>
<B>Using Stealth</B><BR>
By using Stealth you can sneak up behind monsters to perform various abilities such as Sap, Backstab, and Ambush. Without
Stealth the monster would simply turn around and attack. Try to predict the movement patterns of the monster and then sneak up
behind it at the right moment. This becomes much easier once you reach Stealth level 2 which allows you to move faster. A common
way for a Rogue to fight is to first sneak up on a monster from behind using Stealth. The Rogue then uses a Backstab,
Sap, Ambush, or Cheap Shot. The Rogue then continues with combat now that the monster is in a weakened state.
<P>
<B>Stealth Pulling</B><BR>
Stealth is also great for pulling. The Rogue can activate Stealth, and then sneak up on the enemy without aggroing
nearby monsters. Then the Rogue can Sap an extra monster to start the pull.
As Rogues' skill in Stealth becomes higher they can become even more effective at pulling and scouting for pullers.
<P>
<B>Combo Points and Finishing Moves</B><BR>
There are many Rogue abilities that build up Combo Points which can later be used to perform deadly "finishing" moves.
<P>
<B>Combo Points Indicator</B><BR>
After you have earned your first Combo Point, look at the monster's name bar located at the top-middle of the screen.
To the right of the portrait you will see a little light-up dot next to other dots that are not lit. This is the combo
point indicator. It tells you that you have one Combo Point. If you receive a second Combo Point two dots will become lit.
By watching this indicator you can tell how many Combo Points you have stored up and you will know when to release them
with a finishing move. Look at the description of the finishing moves in your character's ability screen to see how many Combo Points are required for a particular effect.
Combo points will not be removed if the target is deselected. They will only be removed if a different target is selected.
<P>
Combo points are removed when your target is dead.
<P>
<B>Subtlety</B>
<blockquote>
<B>Sap (Requires Stealth)</B> - Knocks the target out for X seconds. Must be Stealthed. Only works on humanoids. Any damage
caused will revive the target. Only one target may be Sapped at a time. Sap will not work on a target that is in combat. Targets have
a chance of waking up before the duration ends.
Players have an increasing chance to break free of the effect, such that it is unlikely the effect will last more than 15 seconds.
<P>
<B>Distract</B> - Throws a distraction, attracting the attention of all nearby monsters for X seconds. Does not break Stealth.
Distract is effective against creatures that are already distracted.
<p>
<B>Blind</B> - Blinds the target, causing it to wander at X% of move speed confused for up to Y seconds.
This requires Blinding Powder. Any damage will cancel the Blind effect. Blind cancels combat when used.
<P>
<B>Vanish</B><BR>
Allows the Rogue to vanish from sight, entering an improved Stealth mode, but reducing the speed to X% of
normal for Y seconds. Spellcasting against a Rogue that successfully uses Vanish will now be interrupted.
Vanish breaks root and snare effects. Vanish cancels spells in progress and missiles in flight if they are being cast
at the vanished player.
If a DoT is active when you use Vanish, unless it is a snare as well, it will remain and break Vanish the next time it does damage.
<P>
<B>Pick Pocket (Requires Stealth)</B><BR>
Pick the target's pocket. You can use this ability to get some free loot off the monster. It does not take away loot that would
be normally received when killing the monster.  Pick Pocket is a special chance to loot the monster. Typically minor amounts of
cash or items are found. You can find valuable gems at higher levels.
<P>
</blockquote>
<B>Combat</B>
<blockquote>
<B>Backstab (Combo Move)</B> - Backstab the target, causing +X% weapon damage plus Y to the target.
Must be behind the target. This ability uses a lot of energy. You can backstab fleeing monsters.
Backstab will automatically target the nearest enemy if no target is selected.
<P>
<B>Feint</B> -  Perform a Feint, causing no damage but lowering your threat a small amount.
The point of this ability is to reduce the amount of monster hate so a target will attack another party member that is better equipped to handle the attack such as a Paladin or Warrior.
<p>
<B>Sinister Strike (Combo Move)</B> - A wicked strike that adds X damage to your normal weapon damage. This is typically the main skill used for generating Combo Points.
<P>
<B>Gouge (Combo Move)</B> - Causes X damage and dazes the opponent for Y seconds. Target must be facing you.
Any damage caused to the target will break Gouge. Cancels combat if successful. This ability can give you
time to run, or can allow you to get behind the monster for a Backstab.
<P>
<B>Kick</B> - A quick kick that injures a single foe for X damage and interrupts the spell being cast Y% of the time for Z seconds.
This ability should be used against spell casters to interrupt them when they are in the process of casting. Watch for the
casting animation, and then wait until you have enough energy to Kick. Good Rogue players are always on top of using Kick.
<P>
</blockquote>
<B>Assassination</B>
<blockquote>
<B>Eviscerate (Finishing Move)</B> - Finishing move that causes additional damage per Combo Point. This is your instant damage SLAM ability that you should use to finish monsters off or take a chunk out of them.
<P>
<B>Garrote (DOT)(Requires Stealth)</B> - Garrote the enemy, causing X damage over Y seconds. Must be
in Stealth mode behind the target. If you are going to fight the target for a long time it's a wise idea to sneak up and use this skill. The monster will be damaged over time as you fight them.
<P>
<B>Rupture (Finishing Move)(DOT)</B> - Finishing move that causes damage over time. Lasts longer per
Combo Point. This is another damage-over-time ability that is more useful when you fight the monster long enough so that it reaches the full effect; otherwise, you should use another finishing move.
<P>
<B>Ambush (Combo move)(Requires Stealth)</B> - Ambush the target, causing X% weapon damage plus Y to the target.
Must be Stealthed and behind the target.
<P>
<B>Slice and Dice (Finishing Move)</B> - Finishing move that increases melee attack speed by X%. Lasts longer per Combo Point. You only need to power up this ability enough such that you have the increased speed for the duration of the fight. If you are fighting multiple monsters you might find it useful to power-up on one monster, release, then use the speed on the second monster.
<P>
<B>Cheap Shot (Combo Move)(Requires Stealth)</B> - Stuns the target for X seconds. This ability allows you to start off
the fight with some cheap hits on the enemy that they can't respond to. A Rogue could also Stealth during a fight using Vanish (while
in a party) and use a Cheap Shot at the end of the fight to prevent the monster from escaping.
<P>
<B>Kidney Shot (Finishing Move)</B> - Finishing move that stuns the target. Lasts longer per Combo Point. This ability can
also be used to stun a monster to prevent them from escaping or to prevent any further damage. This is subject to diminishing returns.
</blockquote>
<B>Sprint</B><BR>
Sprint is useful in two situations.
Its main purpose is to act as a special "oh no" ability which gets you out of trouble. If too many monsters come at you at once, if a special monster aggros on you, etc., you can press this button and run away from danger. The second use of Sprint is for a short burst of speed as you're traveling around the world. You
can also use it to beat other players in foot races.
There are certain timed quests which Sprint can also help with.
Place Sprint on your first action bar page so you can always see when it is ready to be used again.
Sprint can be used while stealthed.
<P>
<B>Evasion</B><BR>
The Rogue's dodge chance will increase by X% for Y seconds. This is another "oh no" ability that you should use in emergency situations.
<P>
<B>Safe Fall</B><BR>
Reduces damage from falling. You can use this to jump off high locations without dying. =]
<P>
<B>Sap (Sleep) Pull</B><BR>
The Rogue can use Sap to put Humanoids to sleep. If you are alone you can use Sap to break up a group of two enemies. Sneak up on the targets from behind using Stealth. Then use Sap to put a victim to sleep. Then pull the still awake enemy and kill it, and then finish off the sapped target.
<P>
<B>Typical Battle</B><BR>
Sneak up behind the monster using Stealth. Perform a Backstab, Ambush, Cheap Shot, or DOT on the monster. Then begin combat with the monster. Start using Sinister Strike to build up Combo Points. Then release with a DOT, or direct damage finishing move such as Eviscerate, or use Slice and Dice. Now build up Combo Points again using Sinister Strike. You should always perform a move the second you have enough energy unless you're building up more energy for a more powerful ability.
A common mistake among novice Rogues is to not use their abilities enough.
<P>
<B>Rogue Combos</B><BR>
Use Gouge to stun a monster during a battle. Then use a Backstab from behind. You will need enough energy for this to work;
otherwise the monster will wake up before you can do your Backstab.
<P>
Use Sinister Strike to build up Combo Points. Then use Slice-N-Dice after earning a couple of Combo Points. Now gain more Combo Points using Sinister Strike again. Now release with Eviscerate.
<P>
Use Cheap Shot to stun a monster at the start of combat. Then power up additional Combo Points with abilities like Sinister Strike while
the monster cannot move.
<P>
<B>Away from Keyboard</B><BR>
When you have to leave your computer, enter Stealth mode and move to a spot where the monsters are unlikely to patrol. Using Stealth, the Rogue can go AFK in places that other classes generally can't.
<P>
<B>Skill Points</B><BR>
Rogues should focus on getting more Agility which adds to their Defense, Critical Hit chances, Attack Power, and Dodge chance.
Additionally, you could also focus on Stamina for added health.
<P>
<B>Weapons</B><BR>
Rogues use daggers by default. You can, however, train in swords if you wish to be different. You can even
build up both skills and use the best depending on what weapons you have. At level 10 you can dual wield.
<P>
Rogues can also use throwing weapons or train in bows, crossbows, or guns which will typically do more damage.
You can use throwing weapons to get some additional damage done to the monster before it reaches you. They can also be
used to finish off monsters that are running away. Additionally, these weapons are useful for pulling monsters rather
than sneaking up on them in Stealth mode.
<P>
<B>Poisoning</B><BR>
The Rogue has the unique ability to create and apply special poisons to weapons. Poisoning can be trained at level 20. To create poisons you will need to buy empty vials and purchase various reagents as dictated by each recipe. At higher character levels the Rogue can train in more powerful and
unique poison recipes. There are even additional extremely powerful recipes to be found which require rare ingredients
available out in the world. Poisons can be resisted.
<P>
Crippling Poison, which can slow an enemy's movement speed, is very effective against monsters that run when they are about to die.
This makes the Rogue very useful in a party. Party members become accustomed to having a Rogue around for this helpful ability.
<P>
<B>Lock Picking</b><BR>
You can improve this skill on locked chests. You should also
create practice locks with engineering, or purchase them from engineers, to develop this skill more quickly.
<P>
You can also offer to unlock locked boxes from other players for free to build up skill in Lock Picking.
<P>
Only rogues may train in Lock Picking.
<P>
<B>Traps</B><BR>
The Rogue can detect and disarm traps. Hunters use traps.
Disarming an enemy faction hunter's trap will flag the rogue for PvP.
<P>
<B>Footlockers</B><BR>
A number of areas in the world have footlockers in them.
  These areas are in place for rogues to visit to increase their
  lockpicking skills. The contents of these footlockers are only token
  amounts of treasure, but they should allow for a more interesting
  experience while increasing lockpicking skill. The difficulty of the
  locks in the area correspond to the level of the zone, so if your
  lockpicking skill is around 100, you would go to Durnholde Keep in
  Hillsbrad. But if your skill is 250, you might head to Tyr's Hand in
  Eastern Plaguelands. The Rogue trainers will point the player to an appropriate area with boxes to unlock
depending on the lockpicking skill of the player.
<UL>
<LI> Hillsbrad Foothills - Durnholde Keep
<LI> Redridge Mountains - Lake Everstill
<LI> Ashenvale Forest - Zoram Strand
<LI> Stonetalon Mountains - Windshear Crag and Associated Mines
<LI> Wetlands - Murloc Camps
<LI> Desolace - Sar'theris Strand
<LI> Badlands - Angor Fortress
<LI> Swamp of Sorrows - Pool of Tears
<LI> Searing Gorge - The Slag Pit
<LI> Tanaris Desert - Lost Rigger Cove
<LI> Azshara - Bay of Storms
<LI> Eastern Plaguelands - Tyr's Hand (elite)
</UL>
<B>Profession Options</B><Br>
The Rogue can choose whatever trade skill he or she wants, however these skills might be useful:
<blockquote>
<B>Enchanting</B> - Rogues can enchant their equipment to make themselves more powerful.<BR>
<B>Herbalism and Alchemy</B> - The Rogue can create buff potions or healing potions.<BR>
<B>Fishing and Cooking</B> - The Rogue has no healing so the class is dependent on food when no healing
party member is available. Fishermen fish up locked boxes which can improve the Rogue's Lock Picking skill. <BR>
<B>Skinning and Leatherworking</B> - Rogues can create their own equipment and sell it to others as well.
</blockquote>
