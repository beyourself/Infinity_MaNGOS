<font color=red>This page does not have a complete listing of all abilities that are available. Look for more once our database section is active. </font>
<P>
<B>Totems</B><BR>
The Shaman has up to four types of totems: Air, Earth, Fire, and Water. The Shaman must earn each totem by questing.
The Earth totem is the first totem that's available. Other totems are available at higher levels. You should stop
by the trainer every so often as you level up to see if they have a quest for a new totem.
<P>
Totems appear in the game in two different ways: as inventory objects and as spell effects. In order to cast totem spells,
the Shaman must have earned and possess in his inventory the totem for that element. When he casts a totem spell, he
generates a temporary magical totem on the ground nearby that can be seen and attacked by players and monsters.
<P>
Totems are immune to area effect spells.
<P>
<blockquote>
<LI>Totems remain stationary and do not move with you. You and your group must remain close to the totem to benefit from it.
<P>
Totems are Soulbound, meaning that you cannot give one to another player. Totems are required in your inventory to cast a totem of
that totem type. For example, if you want to cast a Searing Totem of the Fire Totem type you must have a Fire Totem in your
inventory. The good news is that the totems have unlimited usage
once you get them. You don't have to continue to buy reagents to use your totems.
<P>
<LI>You can have one totem of each totem type active at a time. For example, you can cast up to four totems at once: an Air Totem, Earth Totem, Fire Totem, and Water Totem.
<P>
<LI>The last totem overrides the previous totem of the same type. If you cast a fire totem, then cast another fire totem, the last one will cancel the first one. This is important to know because you don't want to waste your mana by casting two totems of the same totem type.
<P>
<LI> Once placed on the ground, totems can be attacked by both monsters and players.  This is important to realize as enemy Shaman can cast totems as well.
When you see an enemy Shaman cast a totem, attack and destroy it.
<P>
<B>Air Totems</B>
<P>
<blockquote>
<B>Windwall Totem</B> - Summons a Windwall Totem with X health at the feet of the caster. The totem protects party members
within Y yards, reducing ranged damage taken by Z. Lasts A min.
<P>
<B>Nature Resistance Totem</B> - Summons a Nature Resistance Totem with X health at the feet of the caster for Y min that
increases the nature resistance of party members within Z yards by A.
<P>
<B>Grace of Air Totem</B> - Summons a Grace of Air Totem with X health at the feet of the caster. The totem increases
the agility of party members within Y yards by Z. Lasts A min.
<P>
<B>Windfury Totem</B> - Summons a Windfury Totem with X health at the casters feet. The totem enchants all party
members' weapons with wind, if they are within Y yards. Each hit has a Z% chance of granting the attacker A extra
attack with B extra attack power. Lasts C min.
</blockquote>
<B>Earth Totems</B>
<P>
<blockquote>
<B>Stoneskin Totem</B> - Reduces melee physical damage taken.
<P>
<B>Earthbind Totem</B> - Slows down enemy movement speed. Cast this totem shortly before
an enemy is about to run away. This will slow him down so you can kill him before he can get help from his friends.
<P>
<B>Strength of the Earth Totem</B> - Gives nearby party members a bonus to strength. This has the potential to increase their damage
and other bonuses depending on their class. If you have a lot of melee strength based tanks, you might find this useful.
<P>
<B>Stoneclaw Totem</B> - Taunts nearby creatures to attack it. This is one of your most useful
Totems. When the monsters are attacking this totem, they are not attacking you. This means less damage during a fight. This is also a somewhat useful escape totem. Cast it and start running after the monsters start attacking the totem. This can sometimes be enough to allow you to escape without dying.
Stoneclaw Totem is usable against targets that are immune to nature attacks.
<P>
<B>Tremor Totem</B> - Shakes the ground around it, waking party members from harmful status. This totem is
useful for when monsters try to put you to sleep or use other "harmful status effects."
</blockquote>
<P>
<B>Fire Totems</B>
<P>
<blockquote>
<B>Searing Totem</B> - Summons a Searing Totem at your feet that attacks nearby enemies.
</blockquote>
<P>
<B>Water Totems</B>
<P>
<blockquote>
<B>Healing Stream Totem</B> - Heals nearby group members. Use this during or especially
after battle.
<P>
<B>Frost Resistance Totem</B> - Increases party members' frost resistance. This is useful against monsters that have frost attacks.
</blockquote>
</blockquote>
<P>
<B>Especially Cool Abilities!</B>
<P>
<blockquote>
<B>Ghost Wolf</B><BR>
Research this ability as soon as it is available. This allows you to transform into a wolf and run faster! A special button will
appear on your interface once you have learned Ghost Wolf. You cannot cast spells or do many other things as a Ghost Wolf.
You cannot ride air mounts such as Wyverns while in Ghost Wolf form. When in Ghost Wolf form, the shaman is considered a Beast.
Ghost Wolf is only useable outdoors.
<P>
<B>Reincarnation</B><BR>
Reincarnation is a passive ability that will give you to option of letting you resurrect
 yourself once every hour upon death.<P>
<B>Astral Recall</B><BR>
The Shaman can use this spell to return to the Home Point. This is especially useful for many applications such as selling,
turning in quests, meeting up with people, and more!
<P>
<B>Ancestral Spirit</B><BR>
Resurrects players! This is an invaluable ability that is required often and is greatly appreciated
in a party. This ability cannot be used in combat.
</blockquote>
<P>
<B>Earth Magic</B>
<P>
<blockquote>
<B>Earth Shock</B> - A direct damage spell that also disrupts spells from being cast.
You can cast this while running. This ability causes additional threat.
<P>
<B>Rockbiter Weapon</B> - Imbue the Shaman's weapon, increasing attack power by X
when using that weapon. Lasts for Y minutes. This ability causes additional threat.
</blockquote>
<P>
<B>Spirit Magic</B>
<P>
<blockquote>
<B>Purge (Dispel)</B>  - Removes one magic effect. Use this if the enemy has some kind of buff on. Once it's removed, the enemy will be weaker and hopefully easier to kill.
<P>
<B>Far Sight</B> - Changes the caster's viewpoint to the targeted location. This spell is especially useful for scouting, for looking for certain NPCs, and for FUN! Far Sight rules.
</blockquote>
<P>
<B>Fire Magic</B>
<P>
<blockquote>
<B>Flame Shock</B> - Sears the target with fire and causes damage over time. Use this ability if you will be fighting
the monster for a longer period of time. If you use it on a monster that will be killed pretty quickly, then the spell is
somewhat wasted.
<P>
<B>Flametongue Weapon</B> - Imbue the Shaman's weapon with fire. Each hit causes X to Y additional fire damage, based on
the speed of the weapon. Slower weapons cause more fire damage per swing. Lasts for Z minutes.
</blockquote>
<P>
<B>Water Magic</B>
<P>
<blockquote>
<B>Healing Wave</B> - Heals a friendly target. This is your healing spell. It's one of your most important spells since you will need it to heal yourself and your party members.
<P>
<B>Chain Heal</B> - Heals a friendly target for X to Y, then jumps to heal additional nearby targets. Each jump reduces
the effectiveness of the heal by 50%. Heals Z total targets.
<P>
<B>Frostbrand Weapon</B> -  Chance of causing additional frost damage and slowing the target's movement speed.
This ability is great to use in addition to the Earthbind Totem against monsters that are going to run.
</blockquote>
<P>
<B>Air Magic</B>
<P>
<blockquote>
<B>Lightning Bolt</B> - This is a direct damage spell useful for finishing monsters off, doing damage to them or even pulling monsters. You may want to specialize in Lightning.
<P>
<B>Lightning Shield</B> - A chance the attacker will be struck for damage. This expends X charges. This ability is useful for protection
and additional damage.
</blockquote>
<P>
<B>Other Spells</B>
<P>
<blockquote>
<B>Water Breathing</B> - This is useful if you're fighting for extended periods of time under water or just for fun and exploration. You can cast this on other players too! This ability
requires a reagent.
<P>
<B>Water Walking</B> - Allows the friendly target to walk across the water for X min. Any damage will cancel the effect.
<P>
<I>There are other cool abilities at higher levels. </I>
</blockquote>
<P>
<B>Tips</B>
<P>
<blockquote>
You should cast Searing Totem at the start of the battle, and then cast a direct damage spell (Lightning Bolt)
to pull the monster. If the monsters are spell casters, you might want to start off with the spell Earth Shock
to prevent them from casting spells (or healing). If you are soloing, you should cast the spell Lightning Shield,
make sure your weapon damage buffs are active, and then engage in combat. At some point, you should cast
the spell Stoneclaw Totem, which will taunt the monsters off of you, to allow you to attempt to heal yourself,
or run away if you're in trouble. You should cast Earthbind Totem to prevent the monster from fleeing or you can
try to finish off a running monster with one of the shock spells or your direct damage spell.
<P>
-You have several planning options from going heavy armor, defense, and defensive talents to focusing more heavily on caster like stats such as mana, spell specialization, and so on.
</blockquote>
