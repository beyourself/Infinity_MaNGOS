<font color=red>This page does not have a complete listing of all abilities that are available. Look for more once our database section is active. </font>
<P>
<B>Resurrection</B><BR>
Resurrect spells use the same timer as when reclaiming your corpse.  When a player is resurrected, they will have
to wait for a set amount before they can click the accept button. Resurrect teleports you to the location at which the
spell was cast, not to the caster's present location. Resurrection is not useable in combat.
<P>
<B>Race Specific Spells</B><BR>
There are some spells available only to certain races by completing quests:
<UL>
<LI>Desperate Prayer (Level 10).  (Dwarf/Human only.)
<LI>Starshards (Level 10).  (Night Elf only.)
<LI>Touch of Weakness (Level 10).  (Undead only.)
<LI>Hex of Weakness (Level 10).  (Troll only.)
<LI>Fear Ward (Level 20).  (Dwarf only.)
<LI>Feedback (Level 20).  (Human only.)
<LI>Elune's Grace (Level 20).  (Night Elf only.)
<LI>Devouring Plague (Level 20).  (Undead only.)
<LI>Shadowguard (Level 20).  (Troll only.)
</UL>
<B>Healing Spells</B> (Holy Magic)<BR>
<blockquote>
<B>Lesser Heal</B> - Heals your target for X.<BR>
<B>Heal</B> - Heals your target for X.<BR>
<B>Greater Heal</B> - Heals a single target for X. This is a slow-casting spell.<BR>
<B>Flash Heal</B> - Heals a friendly target for X to Y. This is a faster casting heal which requires more mana. If someone is about
to die you may wish to use this to save their life because another heal wouldn't save them in time.<BR>
<B>Prayer of Healing</B> (Group Heal) - Heals nearby party members for X. This is a powerful spell.<BR>
<B>Renew</B> - Heals X damage over Y seconds. Since Renew heals over time, it's best to use
after a battle or when a tank comes back from pulling.
</blockquote>
<B>Holy Magic Spells</B>
<blockquote>
<B>Prayer of Fortitude</B> (drop only) - Power infuses the party, increasing their Stamina by X for 1 hour.
Requires reagent.
<P>
<B>Smite</B> (Direct Damage) - Smite an enemy for X holy damage. This ability is useful against monsters that are weak to holy damage.
<P>
<B>Dispel Magic</B> - Dispels magic on the target, removing X harmful spells from a friend or X beneficial spells from an
enemy. This can be used to debuff monsters or to remove negative spells cast by monsters on you or other party members.
<P>
</blockquote>
<B>Shadow Magic Spells</B>
<blockquote>
<B>Mind Blast</B> (Direct Damage) - Blasts the target for X shadow damage, but causes an amount of threat. Threat means the
monster is going to be very angry and will likely come after you in a party. That makes this
a good spell to finish off a nearly dead monster or a good spell to taunt monsters away from other party members.
<P>
<B>Shadow Word: Pain</B> (Damage over Time) - A word of darkness that causes X damage over Y seconds. Use DOTs such as this when
you are going to be fighting a monster for a while. If the monster will be killed quickly, then this spell is wasted because
it doesn't have time to complete before the monster's death. This will stack with the Warlock spell Corruption.
<P>
<B>Mana Burn</B> - Drains X mana from a target. For each point of mana drained in this way, the target takes Y damage.
This is a useful ability against enemies with mana.  Select the monster and look for a mana bar.
<P>
<B>Mind Vision</B> - Allows the caster to see through the target's eyes for X minutes. This
ability is useful for scouting, pulling, fun or curiosity.
<P>
<B>Fade</B> - Fade out, reducing the caster's threat level on all enemies by a small amount for X sec. If the monsters are angry
at you because you've been healing, use this ability to reduce the amount of threat/hate from the monsters. Then, if you're lucky, the
monsters will attack someone more capable of fighting them in melee combat such as a Warrior.
<P>
<B>Mind Control</B> - Controls a humanoid mind up to level X, but slows its attack speed Y%.
Lasts up to Z min. Cannot be cast on tapped targets.
</blockquote>
<B>Disciplines</B>
<blockquote>
<B>Levitate</B> - Allows the caster to levitate, floating a few feet above the ground. While levitating, you will fall at a
