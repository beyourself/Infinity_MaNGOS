<font color=red>This page does not have a complete listing of all abilities that are available. Look for more once our database section is active. </font>
<P>
<B>Auras</B><BR>
You can only have one aura active at a time. You'll have to decide what is best for the situation. Other players can also let you know what is best to use. If there are multiple Paladins in the same group, they can each have a different aura active,
but multiple Paladins cannot stack the effects of the same aura. If you find yourself in a party with another Paladin, ask him or her what aura he or she is going to use, then use a different one that will
complement it.
<P>
<blockquote>
<B>Devotion Aura</B> - Gives additional armor to nearby party members. Use this when multiple party
members are taking damage during battle.<BR>
<B>Retribution Aura</B> - Causes holy damage to any creature that strikes a party member.<BR>
<B>Fire Resistance Aura</B> - Increases nearby group member's fire resistance X. Players may only have one Aura on them per Paladin at any one
time.<BR>
<B>Shadow Resistance Aura</B> - Increases nearby group member's shadow resistance X. Players may only have one Aura on
them per Paladin at any one time.<BR>
<B>Frost Resistance Aura</B> - Increases nearby group member's  frost resistance X. Players may only have one Aura
on them per Paladin at any one time.<BR>
</blockquote>
<B>Blessings</B><BR>
A Blessing is a short buff. The Paladin can only have one Blessing active on a player at a time. The intention
is for players to decide what Blessing is best for the situation and change Blessings depending on what is happening. If there are multiple Paladins in a group, the limit is one Blessing per person per Paladin. Thus, with two Paladins each party member can have two Blessings.
<P>
<blockquote>
<B>Blessing of Might</B> - Increases damage. Use this on party members who are focused on damaging the enemy with physical attacks.<BR>
<B>Blessing of Wisdom</B> - Increases mana regeneration. Use this on spellcasters like Priests, Mages, etc. You can also use this on yourself after battle to regenerate mana.<BR>
<B>Blessing of Protection</B> - Protects the target from all physical attacks. This Blessing is only usable on party members.
While this is active the targeted player is unable to attack or use physical abilities.
<BR>
<B>Blessing of Salvation</B> - Reduces the amount of threat generated. This is useful to cast on
casters such as Priests or Mages to make their lives easier against the monsters. Monsters will often stop targeting them
and this Blessing will allow the casters to do more without being a monster's primary focus.<BR>
<B>Blessing of Light</B> - Places a Blessing on the friendly target, increasing the effects of Holy Light spells used
on the target by X and the effect Flash of Light spells used on the target by Y. Lasts Z min. Players may only have
one Blessing on them per Paladin at any one time.<BR>
<BR>
<B>Blessing of Freedom</B> - Places a Blessing on the party member, granting immunity to movement impairing effects for X
sec.
Will counter the movement impairing effects of Frostbolt, Cone of Cold, Frost Shock.
It will not prevent the damage taken by Frost Nova, Hamstring, Wing Clip, Mind Flay, Counterattack, or Blast Wave or
the melee and ranged attack speed slowing effects of Cripple.
Players may only have one Blessing on them per Paladin at any one time.<BR>
<B>Blessing of Sacrifice</B> -
Places a Blessing on the party member, transferring X damage taken per hit to the caster.
