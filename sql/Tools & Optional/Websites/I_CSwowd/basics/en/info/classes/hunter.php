<B>Grouping</B><BR>
The Hunter's goal is to stay away from the monster and shoot at it from range. When you team up with other players
this becomes more difficult. When the monster decides to attack other party members it will get within your
minimum range if you're standing next to the rest of the party. For that reason, it's best to stay away from
other party members during combat. That way if the monster attacks them or rushes toward them, you can still
continue to fire at range. Also let party members know they should never run toward you because that will also
bring the monster within your minimum range.Make it clear to other group members that you're going to be
slightly away from them during combat. Another thing to watch out for is other party members pulling the monster.
Often it's best to use the Hunter or the Hunter's pet to pull.
<P>
<B>Multiple Hunters</B><BR>
If you happen to party with multiple Hunters make sure each of you knows which Sting to use. Each Hunter
can have one Sting active at a time. If you have two Hunters, that's two Stings that can be on the monster.
Ask your fellow Hunter to cast one Sting while you cast the other. Both Hunters should fight away from each
other so if the monster attacks one of them, the other Hunter can still fire from range.
<P>
<B>Runners</B><BR>
Hunters are great at dealing with monsters that are running away. Monster flight is a major concern because
fleeing monsters may call others and bring them into battle. You can simply shoot a fleeing monster or hit it
with Concussive Shot to slow it down. You can also Wing Clip it before it retreats to prevent it from running away.
<P>
<B>Shooting on the Run</B><BR>
The Hunter can shoot Arcane Shot while running. It can also use Hunter's Mark while on the run. If you have to
chase a monster or enemy player as they are running away, keep this in mind.
<P>
