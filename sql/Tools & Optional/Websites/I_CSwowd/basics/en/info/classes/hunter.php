<B>Tracking</B><BR>
Tracking is only available to Hunters; they may train in it at Hunter class trainers.
You can train in Track Beasts, Humanoids, Undead, Hidden, Elementals, Demons, Giants, and Dragonkin at different
levels.
<P>
<B>Dual Wield</B> (Passive Ability) - Allows one-hand and off-hand weapons to be equipped in the off-hand.
Train in this once it's available.<P>
<B>Aspects</B><BR>
Aspects are special animal related buffs that allow the Hunter to receive
special bonuses from its animal friends.
You can only have one aspect active at a time similar to Paladin seals.
<P>
Aspects need to be changed depending on the situation. You can only have one active at a time and you need to have
the "best" one active. For example, you can start off with Aspect of the Hawk while you're attacking from range.
If the monster rushes up to you, switch to Aspect of the Monkey and engage in melee combat. You can also do a
Wing Clip then run after switching to Aspect of the Cheetah to get away.
Aspects of Cheetah/Pack are canceled when mounted.
<P>
<ul>
<B>Aspect of the Monkey</b> - The Hunter takes on the aspects of a monkey,
increasing the chance to dodge by X%. This is recommended when you're pulling or taking damage in
certain situations.<BR>
<B>Aspect of the Cheetah</B> - The Hunter takes on the aspects of a cheetah, increasing
movement speed by X%. If the Hunter takes damage, they will
be dazed for Y sec. Only one Aspect can be active at a time.
This can be used to
travel or to back away from monsters so you can use your ranged weapon.<BR>
<B>Aspect of the Pack</B> - The Hunter and its group take on the aspects of
a pack of cheetahs, increasing movement speed by X%. If a pack member takes damage, they will
be dazed for Y sec. Only one Aspect can be active at a time.
<BR>
<B>Aspect of the Beast</B> - The Hunter takes on the aspects of a beast becoming untrackable.<BR>
<B>Aspect of the Wild</B> - The hunter and his group take on the aspect of the wild, increasing
Nature resistance by X. Only one aspect can be active at a time.
</ul>
<B>Beast Spells</B><BR>
<ul>
<B>Scare Beast</B> - Scares a beast, causing it to run in fear
for up to X sec. Damage caused may interrupt the effect. Only one beast
can be scared at a time.
Beasts have an increasing chance to break free of the effect, such that it is unlikely the effect will last more than 15 seconds.
<BR>
<B>Eagle Eye</B> - Zooms in the Hunter's vision. Use this to scout, which is
