<td>
<span>
Hunters track, tame, and slay all manner of animals and beasts found in the wild. Whether they rely on bows or firearms, Hunters
consider their weapons and pets to be their only true friends.
<P>
Hunters are great for pulling monsters as they are able to set traps that hinder monsters that the Hunters pull to attack.Hunters
are also able to use various beast-related special abilities, take on bestial traits that allow them to do things that they could not do
normally, use a wide variety of ranged attacks complimented by a few melee abilities, and the ability to control and tame beasts.
<P>
Hunters are closely related to their pets and must remember to always keep their pet's happiness in mind.When using a pet,
Hunters will notice an icon in the top left corner of their screen that depicts their pet's level of happiness.The more
Hunters use their pets, the lower their pet's level of happiness will become.Hunters should always remember to feed their pets
to raise their pet's level of happiness or worry about their pet abandoning them or even attacking the Hunter!Hunters are also
the only class able to name their pets.
<P>
<B>Ranged Class</B><BR>
The Hunter is primarily a ranged attacking class.It has melee attacks, but its damage at range far outweighs its melee
capabilities.<BR>
<BR>
<B>Great Pulling Class</B><BR>
The Hunter also uses traps, which you lay down ahead of time and draw monsters into.Use of ranged combat, various debuffs, and
traps make it a great pulling class in groups.<BR>
<BR>
<B>Stats</B><BR>
The Hunter cares about Agility (ranged attack power, 1/2 melee attack power), Stamina, Intelligence (for mana), Spirit, and
Strength (when it can't get Agility).
<P>
<B>Scopes</B><BR>
Engineers can create scopes that can be attached to your bows and guns to increase their damage output. Seek out an
engineer to create one for you or buy one at the auction house.
<P>
<B>Ammo</B><BR>
Always use the best ammo available. Engineers can make gun ammo so sometimes it will be good to get ammo from them
instead of buying it from an NPC merchant.
<P>
<B>Quivers & Ammo Bags</B><BR>
These can speed up the firing rate of your bows and guns so it's always best to use them. Search for better bags
that make you fire even faster. Some quests can provide you with faster-firing bags.
<P>
<B>Pet Name</B><BR>
You can name your pet! Only Hunters can name pets. Right-click on their portrait and pick rename. You can only
do this one time.
<P>
<B>Pet Happiness/Feeding</B><BR>
Your pet has a happiness rating. This is shown via an icon in the top left corner of the screen. As you continue to use your pet,
it becomes less and less happy. However, if you feed your pet it increases its happiness.
Happiness determines damage output, so the less happy your pet is, the weaker it gets.
If you neglect your pet too much, it abandons you and may even attack you!
<P>
<B>Pet Food</b><BR>
Always save food for your pet. You can see what types of food your pet eats by trying
various foods, checking in your pet menu (hit C then select the Pet tab), or by checking in the Stable.
Beast Lore can be cast on animals to see what types of food they prefer. You can also ask guild members
and friends to save food for your pet and mail it to you! Awww how cute...
<P>
<B>Pet Size</b><BR>
Pets scale to appropriate sizes. If you tame a pet, it will likely be smaller than the "monster" version.
This was done because certain pets were very messy to play/group with at their default sizes.
<P>
<B>Stables</B><BR>
You can house your pets at the Stables. Stables are located near Inns and Mailboxes. You can buy additional slots
to contain more pets. If you want to switch pets and still keep both, do so at the Stables.
<P>
<B>Do the Hunter Dance</B><BR>
Hunters are most powerful when in ranged combat. You should always try to attack via range whenever possible.
The Hunter is meant to run around during combat. Hunters have a minimum range when using ranged weapons that
prevents them from firing their weapon (minimum range error) when the monster is in this area. When this happens,
Hunters must back away from the monster in order to fire their ranged weapon again. If the monster starts
attacking you, you have several methods available for backing away from it to fire your bow or gun:
<P>
<ul>
<B>Use your Pet's Taunt</B><BR>
Start a battle using your pet and wait a bit before attacking the monster yourself. Once the monster has built
up some hate toward your pet, engage it in battle. If the monster starts to attack you instead, use your pet's
taunt ability.
<P>
<B>Wing Clip & Run</B><BR>
Rush in and attack a monster with a Wing Clip and then switch to Aspect of the Cheetah. Start running away
from the monster for a while. Then stop, turn around, and attack. You can also fire a Concussive Arrow to
slow the monster down.
