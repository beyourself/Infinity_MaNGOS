<P>
<B>Soul Shards</B><BR>
The Warlock requires Soul Shards for certain spells and pets. You receive a Soul Shard by using the Drain Soul spell on a
monster as it is dying. A catch is that you cannot get Soul Shards from monsters that don't give you experience. So you can't go into a newbie region
and kill easy monsters for Soul Shards. One Soul Shard takes up one slot in your bags, which means you don't want
to carry a whole lot of them. However, you do always want to have some in your bags so you can summon your pets.
When using Drain Soul the caster cannot do any other action such as move, attack, or cast another spell, or else the spell will be interrupted.
If you do not stand still, the Drain Soul will not work. You will need to learn how to do this properly, which can
be challenging.
<P>
Soul Shards do not stack. This is for balance reasons and so you won't hoard huge numbers of them.
<P>
If you find yourself without Soul Shards, you will have to get them without the use of most pets, which can
be a challenge. Imps can be summoned without the use of a Soul Shard so you can use them
to get your Soul Shard, and then switch to a more powerful pet.
<P>
Honorable kills generate a Soul Shard when Drain Soul and Shadowburn are used.
<P>
<B>DOTs</B><BR>
DOTs are damage-over-time spells.  Because they do damage over time, you want to cast them at the start of
a battle and have them deal damage throughout the battle. If you're killing a monster very quickly, then DOTs aren't
the best to use.
<P>
<!--------AFFLICTIONS------------------------------->
<B>Afflictions</B><BR>
<b>Curses</B><BR>
<BR>
The Warlock has several types of curses and it is up to the player to decide which is best for the particular situation.
Curses can only be cast on the enemy. You can only have one curse active at a time on a monster. The last curse you
cast will override the previous curse on the monster.
<blockquote>
<B>Corruption</B> - Inflicts a disease on a target that causes X damage over Y seconds. Corruption will stack with Shadow Word: Pain. <BR>
<B>Curse of Weakness</B> - Damage caused by the target is reduced. This is useful in shorter battles. <BR>
<B>Curse of Recklessness</B> - Increases the damage done by the target, but greatly reduces its armor.  This is a good group based spell that is especially good to cast on monsters that are trying to run away.<BR>
<B>Curse of the Elements</B> - Curses the target, reducing Fire and Frost resistances by X for Y min. This ability is good to use when playing with other casters who use those types of spells. <BR>
<B>Curse of Agony</B> - Curses the target with agony. Causes large damage over time.<BR>
<B>Curse of Shadow</B> - Curses target, reducing Shadow and Arcane Resistance by X for Y min. Only one
Curse per Warlock can be active on any one target.<BR>
<B>Curse of Doom</b> - Curses the target with  impending doom, causing X shadow damage after Y min. If the target dies
from this damage, there is a chance that a Doomguard will be summoned. Cannot be cast on players (boo!)
<BR><BR>
<B>Drain Life</B> - Transfers health from target to caster. This ability is used during battle both to damage the monster
and to regain life. Drain Life is good to use against high-level monsters that typically resist your other
attacks. Although Drain Life can be resisted, you should keep trying until your cast succeeds (doesn't resist). Once you land the
spell, it will start draining life; it does damage to the monster even if you don't actually need the life. <BR>
<B>Life Tap</B> - Converts health into mana. If you need mana, what you do is Life Tap, then steal life from the enemy monster.<BR>
<B>Drain Mana</B> - Transfers X mana to the caster (Warlock) every second. This is a "leash" ability similar to the Dark Ranger's abilities in Warcraft III. Once the monster gets out of range, the leash breaks, and the spell is deactivated. Also, if the monster interrupts your casting, the interruption breaks the leash. Use this ability on enemy casters to drain their mana and replenish your own. <BR>
<B>Fear</B> - Send the monster running away. This is a good crowd control ability or escape spell.
Be careful using this ability though. Monsters could wake up other monsters by calling for help as they run away.
You typically don't want to use this in dungeons.
Players have an increasing chance to break free of the effect, such that it is unlikely the effect will last more than 15 seconds.
This ability is very funny.<BR>
<B>Howl of Terror</B>  - Howl, causing all enemies within X yds to flee in terror for
Y sec. This is a good "oh no" ability to use when surrounded or for PvP. This ability has an increased chance of
breaking early.<BR>
<B>Death Coil</B> - Drains X  of the enemy target's health, returning Y% to the caster.
This has a long cooldown so it's especially good for emergency situations.
</blockquote>
<!----------DEMONOLOGY---------------------------------------------------->
<B>Demonology</B>
<blockquote>
<B>Shadow Ward</B> - Absorbs shadow damage. This ability is good to use when you know the monster is using shadow damage.<BR>
<B>Demon Skin</B> - Increases health regeneration and armor. Increases health regeneration at all times. You should never be without the Demon Skin buff.<BR>
<B>Demon Armor</B> - Increases armor, shadow resistance, and health regeneration. Increases health regeneration at all times. This ability replaces Demon Skin.  It
should always be active. <BR>
<B>Detect Lesser Invisibility/Invisibility</B><BR>
<B>Searing Pain</B> - Inflict searing pain on the enemy target, causing X to Y Fire damage. Causes a
Z amount of threat.<BR>
<B>Sense Demons</B> - Use to locate Demons on the minimap.<BR>
<BR>
<B>Stones</B><BR>
Warlocks can create special stones that they can use themselves or give to other players.
<P>
<B>Healthstone</b> (requires Soul Shard) - Creates a Healthstone, which is essentially a health potion. <BR>
<B>Soulstone</B> (requires Soul Shard) - This is the Warlock's version of Resurrection. The Warlock creates a
Soulstone that is automatically used upon death, restoring its owner to life at X%
health and mana. The Soulstone is soulbound when created. When the target dies it may resurrect itself any time
until release. There is a cooldown before a Soulstone can be used again.
<BR>
<B>Spellstone</B> (requires Soul Shard) - Creates a Spellstone for the caster. When equipped and used, the Spellstone removes
all magic effects from the caster and will absorb X mage damage for Y min. In addition, equipping the Spellstone improves your
chance to get a critical strike with spells by Z%. Use a Spellstone before doing
Hellfire to reduce the amount of damage to yourself.<BR>
<B>Firestone</B> - Creates a Firestone which can be equipped in the off hand. When equipped,
enchants the main hand weapon with fire, granting each attack a chance to deal X to Y additional
fire damage. In addition, equipping a Firestone increases the damage done by fire spells by Z.<BR>
<B>Banish</B> - Banishes the enemy target, preventing all action but making it
invulnerable for up to X sec. Only one target can be banished at a time. Only works
against Demons and Elementals. <BR>
</blockquote>
<!----DESTRUCTION------------------>
<B>Destruction</B>
</blockquote>
<B>Immolate (DoT)</B> - Burns the enemy for initial damage and additional damage over time. <BR>
<B>Shadow Bolt</B> - Sends a shadowy bolt at the enemy. This is your direct damage (DD) spell, and you'll want to use it when you're not casting something else. It is useful for killing fleeing monsters, finishing monsters off, and so on.<BR>
<B>Hellfire</B> - Ignites the area surrounding the caster, causing fire damage to the
caster and all nearby enemies. This is a good spell to use when surrounded, but
its effects will stay. You can kill yourself if you're not careful. You may
have to use a potion, or cast Drain Life, to keep yourself alive.<BR>
<B>Soul Fire</B> - Burns the enemy's soul, causing X to Y damage. It takes a long time to cast
so it's best to start a fight with this attack.<BR>
</blockquote>
<B>Awesome abilities</B><BR>
Check out these fun abilities.
<P>
<blockquote>
<B>Eye of Kilrogg</B> - Summon the Eye of Kilrogg - This summons an eye that you can control. This eye
can move away from your character and can be used for scouting purposes as it was used in Warcraft II:
Tides of Darkness. The eye is stealthed and has one hit point. The Eye cannot fly up in the air.
It can travel up stairs and other players can see the Eye. The Eye doesn't make monsters angry at you so
you can use it to scout out an area before you go there.
This is a channeling spell so it will be
canceled if someone interrupts the Warlock's casting.<BR>
<B>Summon Felsteed</B> - Create your own Felsteed horse! Horses allow you to travel much faster than walking.<BR>
<B>Unending Breath</B> - Breathe under water! You can cast this on other players too!<BR>
<B>Enslave Demon</B> - Enslave a target demon for a limited amount of time. You can use this ability to take
control of a Demon and use it to attack other monsters. Requires a soul shard. Increased chance of breaking early if
cast repeatedly on the same target. This is very cool and useful. If you are trying to enslave a demon, cast Curse of
Shadow before you Enslave the Demon to reduce its resistances.
Cannot be cast on tapped targets. Cannot be dispelled.
<BR>
<B>Ritual of Summoning</B> - Begins a ritual that summons a targeted group member. Requires three people to perform.
To use this ability, first target a person (usually the party icon) you want to summon. Next cast the spell.
You will then need two people (party members only) to right-click on the portal one time and then do nothing once their
character begins the summoning animation. Once all three players are summoning (all have summoning animation) it
will take several seconds to complete the ritual. Then the portal will disappear and the player summoned will appear.
If it doesn't work, try it again. A failed Ritual of Summoning will not cost a soul shard. Only party members can participate
in rituals. Target of Ritual of Summoning must already be in the same instance if caster is in an instance. Summoning
gives a confirmation dialog to the person being summoned. Ghosts cannot be summoned.
</blockquote>
<P>
<B>Pets</B><BR>
<blockquote>
Pets require knowledge and skill to control. Learn which pet to use based on the situation. Read up on pet management.
<P>
<B>Pet Spells</B><BR>
Demon spells are available as Grimoires from the Demon Trainers in the world.  Purchase the Grimoire and read it
while the appropriate demon is summoned to teach it the spell.
<P>
<B>Health Funnel</B> - Transfers health to the caster's pet every second as long as the caster channels. Use this ability by having your pet attack and then channeling health into the pet.
Then, if you need health, cast Drain Life, use a potion, or have someone heal you. This is a leash spell so it
cancels if the pet runs out of range or if the spell is interrupted.
<P>
<B>Imp</B> - The Imp is a good pet to use if you're in a party. He blasts the enemy with a ranged attack and then runs away so that he is a safe distance from the target. Because he's fragile and tends to run away from melee combat, you have to
be careful with him so that he doesn't aggro monsters. Also, he's not the best to use in solo.
On the other hand, if you don't have a Soul Shard, you will need him to get another one.
<P>
<B>Voidwalker</B> - This is an ideal soloing pet. Use it to tank for you. The Voidwalker has
massive hit points and can use the Torment skill to attract monsters' anger, but its damage
output is very low.
<P>
<B>Succubus</B> - This pet inflicts a high amount of damage on your foes, but is very fragile. The Succubus
has an amazing damage output, especially when backstabbing monsters with Lash of Pain. It is also
good at crowd control with Soothing Kiss and Seduce to make a monster sleep for a short while.
<P>
<B>Felhunter</B> - This pet is best used against enemy casters.
<P>
<B>Inferno</B> - Summons a meteor from the twisting nether, causing 200 Shadow damage and stunning all enemy targets in
area for 2 sec. An infernal rises from the crater, under the command of the caster. Once
control is lost, the Infernal must be Enslaved to maintain control. Requires a reagent. Usable outdoors only.
The quest for the Infernal can be found in Felwood.
The summoned Infernal will now be enslaved for 5 minutes before turning on its summoner.
<p>
<B>Ritual of Doom</B> - Begins a ritual that sacrifices a random participant to summon a doomguard. The doomguard must be
immediately enslaved or it will attack the ritual participants. Requires the caster and 4 additional  people to complete
the ritual. In order to participate, all players must right-click the portal and not move until the ritual is complete.
Requires a Demonic Figurine as a reagent. The Ritual of Doom quest can be found in the Tainted Scar.
</blockquote>
<B>Equipment</B><BR>
Choose equipment that adds to Intelligence, Spirit, and Stamina.
Remember that with Drain Life, added mana means added health because you can steal Life more often.
<P>
<B>Hate List Management</B><BR>
You will have to learn to balance the monster hate between you and your pet. You need to
learn how to back off when the monster is attacking you so that your pet can Taunt the monsters away from you. Stop casting
spells, and use whatever Taunt abilities your pet might have.  Learn which spells really make monsters angry
and factor that in before you use them.
<P>
<B>Playing the Warlock</B><BR>
Now that you know the basics of the Warlock, you have to learn how to play the class. At the start of combat,
you will likely want to cast your DOTs and tell your pet to attack the enemy. Next you will cast other spells
and possibly Drain Life to bring your health back up. Then, if you want a Soul Shard, you will need to cast
Drain Soul right before the monster dies.
<blockquote>
<LI>Cast several DOTs on the monster, then cast Fear on it. As the monster runs away, hit it with direct damage spells.<BR>
<LI>Use Fear if you pull too many monsters at once. Be warned that fleeing monsters may bring friends back with them,
so use Fear carefully.<BR>
<LI>Demon Trainers sell grimoires which you can buy even without the pet. You can right-click on it at any convenient time with the proper pet summoned to teach the pet the new spell.
</blockquote>
<B>Mount</B><BR>
The Warlock has a quest at 40 and again at 60 for special mounts.
Warlock Epic Mount quest: To begin, speak with a demon trainer in any of the capital cities: Kurgal, Martha Strain,
Spackle Thornberry, or Jubahl Corpseeker.
<P>
<B>Profession Options</B><BR>
You can pick whatever you want. You might find these more tailored to the Warlock:
<blockquote>
<B>Tailoring</B> - Make cloth armor for the Warlock or other players to wear. <BR>
<B>Fishing/Cooking/First Aid</B> - Heal the Warlock. <BR>
<B>Herbalism/Alchemy</B> - Make cool potions for the Warlock to use. <BR>
<B>Enchanting</B> - Enchant your equipment and sell enchantments.
</blockquote>
