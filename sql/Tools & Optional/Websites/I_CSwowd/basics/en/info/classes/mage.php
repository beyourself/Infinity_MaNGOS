<B>Arcane Magic</B>
<blockquote>
<B>Arcane Intellect (Buff)</B> - Increases the target's Intellect by X for Y min. Always have this ability active on yourself and cast it on other players that need it as well.
<P>
<B>Arcane Brilliance (Party Buff)</B> - Infuses the target's party with brilliance, increasing their Intellect by X for 1 hour.
Requires Reagent: Arcane Powder. This is only available for level 56+ players via a looted book.<BR>
<B>Arcane Missiles</B> - Launches Arcane Missiles at the enemy, causing X damage each second for Y seconds. This spell has a spectacular animation.
Arcane Missiles cannot be cast if silenced.
<P>
<B>Polymorph</B> - Polymorph transforms enemy Beasts, Humanoids, and Critters into a sheep, forcing it to wander around for the duration of the spell. While wandering, the sheep cannot attack or cast spells and will regenerate health very quickly. Only one target can be Polymorphed at a time and any damage will transform the target back into its normal form, dismounted if applicable. There is also an increased chance of the spell breaking early especially if cast on players. Players targets get decreased duration of the effect and a better chance to break free, such that it is unlikely the effect will last more than 15 seconds. Multiple castings on a single target are subject to diminishing returns for the spells' durations.
<P>
<B>Dampen Magic</B> - Dampen magic reduces the effects of magic on a targeted party member. This will decrease the damage
taken by harmful spells by X and healing spells by Y. Lasts Z min.
Use this to protect party members from harmful spells, but be aware that it will also reduce the effect of healing
spells that are cast on them while the effect of Dampen Magic persists.
<P>
<B>Amplify Magic</B> - Amplifies magic used against the targeted party members, increasing damage taken from
spells by X and healing spells by Y. Lasts Z min. This would be helpful if you want to increase healing and are
not worried about increased spell damage.
<P>
<B>Arcane Explosion (AOE)</B> - Causes an explosion of arcane magic around the caster, causing X to Y damage to all targets within Z yards. This ability would be useful when the caster is surrounded by targets.
<P>
<B>Detect Magic</B> - Detects beneficial magic effects on the target for X min. This is an information gathering spell. It's helpful to know what buffs the monster is using so you know how to counter them or know what the monster is capable of. If you can remove those buffs it is even more helpful.
<P>
<B>Remove Lesser Curse</B> - Removes 1 lesser curse from a friendly target.
Curses can be damaging, last a long time, and are annoying.
Use this to remove curses from yourself and friends.
<P>
<B>Blink</B> - Teleports the caster to the targeted location, breaking free of any bonds that hold the Mage. Blink
is a spell Mages enjoy.
It has multiple uses, from escaping a monster's spell to aiding exploration and traveling. Blink counters stun effects.
<P>
<B>Mana Shield</B> - Absorbs X physical damage, draining mana instead. Lasts Y min.
This is a good spell to use if you're in trouble and about to die. Since it drains your mana, your hope is the
monster will die (usually in a party situation) before you do.
<P>
<B>Counterspell</B> - Counters the enemy's spell X% of the time, preventing him from casting that spell or any other spell from that school of magic for 10 sec. This ability is to be used against other spell casters. Remember that the monsters will be really angry at you when you use it.
<P>
<B>Mage Armor</B> - Increases your resistance to all magic by X and allows Y% of your Mana regeneration to continue while
casting. Lasts Z min.
