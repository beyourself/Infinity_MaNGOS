<B>Class Role</B><BR>
The Druid has three potential roles in a group. When in their normal, biped form, they act well as a
healer and can give support in the form of buffs and debuffs; they also have limited crowd control
against dragons. Their Bear form gives them offensive and defensive power enough to step up to a
monster and take the hits (we're looking into aggro management for the form). In Cat form, the Druid
becomes a damage dealer. This trio of roles in one class gives the Druid a diverse feel and allows
them to perform nearly any role perceived as "needed" in a party.
