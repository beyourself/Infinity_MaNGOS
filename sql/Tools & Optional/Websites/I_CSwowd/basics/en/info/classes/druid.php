<IMG src="../../../shared/wow-com/images/smallcaps/plain/d.gif" width="37" height="38" border="0" align="left">ruids are the keepers of the world. Locked in slumber for generations, they awoke to meet the threat of the
Burning Legion during its recent invasion. After Archimonde's defeat, the Druids chose to remain in the waking
world and help to rebuild their shattered lands. The Legion's attack left a terrible scar on the natural order,
and the Druids seek to heal it.
<P>
The Druid gives players several play style options. The Druid in normal form is a caster that can fight with
spells or via melee combat. In Bear Form the Druid becomes a Warrior with Rage. While in Cat form the Druid becomes a
Rogue with Energy and stealth. The Druid can also transform into two other special animal forms.
The Druid can also take on the role of a Priest, casting spells to heal himself and fellow characters.
A Druid is not as versatile in his abilities as a Priest is, lacking the spells Holy Word: Shield and Resurrection,
but he is otherwise a very capable healer.
