<B>Restoration</B>
<blockquote>
<B>Healing Touch</B> - Heals a friendly target for X to Y. This is the Druid's main healing spell. You can only use this
ability while you are in Druid form. There are times where you should have two levels of this spell available on your action bar to use based on how damaged the target is. You don't want to waste mana by using a more powerful heal than is necessary.
<P>
<B>Rejuvenation</B> - Heals the target for X over Y seconds. Rejuvenation is good to have active during combat.
That way when the enemy is causing damage, you are healing
during that time and mitigating some of that damage. If you or a party
member is slightly damaged after battle, you can cast Rejuvenation to help
heal the damage. Rejuvenation is also useful to cast on tanks that are pulling monsters.
<P>
<B>Regrowth</B> - Heals a friendly target for X to Y and another X over Y seconds. This spell is a mix of a heal and a
heal over time spell. Regrowth takes a while to cast so you don't typically want to use it on yourself when you're being attacked. This spell is useful after battle or for casting on party members who are damaged but are not taking significant damage. Otherwise you will want to switch to Healing Touch instead.
<P>
<B>Tranquility</B> - Regenerates all nearby group members for X every X seconds for X seconds. Druid must channel to maintain the spell. If a monster interrupts this spell it will cancel. This ability would be best used when multiple party members need healing at once.
<P>
<B>Rebirth</B> - Returns the spirit to the body, restoring a dead target to life with X health and Y mana. This spell requires a reagent.
<P>
<B>Mark of the Wild</B> (Powerful Buff!) - Increases all friendly targets' armor by X. At higher levels this spell adds an increase to all attributes. At even higher levels this ability also adds an increase to all resistances. This is one of the most powerful buffs in the game, making a Druid a welcome addition to a party.
<P>
<B>Gift of the Wild</B> (loot drop) - Increases party member's armor by X, all attributes by Y and all
resistances by Z for 1 hour.
<P>
<B>Cure Poison</B> - Cures one poison effect on the target. Another welcomed ability when monsters are
poisoning you.
<P>
<B>Abolish Poison</B> - Attempts to cure one poison effect on the target and another poison effect every X seconds.
<P>
<B>Remove Curse</B> - Dispels X curse(s) from the target. This is a welcomed ability as no one likes to be cursed!
</blockquote>
<!-------BALANCE-------------->
<B>Balance</B>
<blockquote>
<B>Moonfire</B> (DoT) - Burns the enemy for X to Y damage and then an additional X damage over X seconds. This ability gives a starting blast and then damages over time. You should use this ability whenever you're fighting the monster long enough for the spell to run its full duration. Otherwise, you're just wasting mana. Moonfire is also good for stopping runners before they get help.
<P>
<B>Starfire</B> - Causes X to Y damage to the target. The first question from a player when this spell
becomes available is typically, "How is this different from Wrath?" Starfire takes longer to cast but
does more damage. It's a good initial strike spell. Additionally, Starfire does full damage while Wrath does
nature damage. If the enemy has resistances to nature damage, Starfire is more effective to use.
<P>
<B>Wrath</B> (DD) - Causes X to Y nature damage to the target. This is your first available direct damage blast spell. This is useful for causing initial damage to the enemy. It's also useful for finishing off an enemy that is trying to run away.
<P>
<B>Thorns</B> (Additional damage buff) - Thorns sprout from the friendly target doing X nature damage to anyone who hits them. This is a mid-range timed buff that adds additional damage to the enemy in melee combat. Cast this on any party member that is tanking. Always have this up when you're in melee combat with the enemy.
<P>
<B>Faerie Fire</B> (Curse) - Decrease the armor of the target by X for X seconds. While affected, the target cannot stealth or turn invisible. This is a staple Druid spell that should be worked into combat the second it is available. Be sure to only use this ability when you're in melee combat with the enemy. If you're just casting spells on a monster, Faerie Fire is not needed.
Faerie Fire will stack with other armor reducing debuffs.
<P>
<B>Entangling Roots</B> - Roots the target in place and causes X nature damage over X seconds. Only usable outdoors. This spell has many uses. You can use Entangling Roots to break up spawns (groups of monsters).
Cast Entangling Roots on a monster you wish to keep out of combat, and then engage the remaining monster(s).
You can use Entangling Roots in solo combat to allow you to blast the enemy with Wrath or Starfire while
the monster cannot respond. Another use of Entangling Roots is to stop runners from escaping to warn
their friends. Subject to diminishing returns in PvP. This is considered an immobilizing effect.
<P>
<B>Hibernate</B> - Forces the enemy target to sleep for about X sec. Any damage will awaken the
target. Only one target can be forced to hibernate at a time. Only works on Beasts and Dragonkin.
There is an increased chance of this spell breaking early.
<P>
<B>Barkskin</B> - The druid's skin becomes as tough as bark. Physical damage taken is reduced by X%. While protected, damaging
attacks will not cause spellcasting delays but non-instant spells take Y sec longer to cast and melee combat is slowed by
Z%. Lasts A sec.
<P>
<B>Soothe Animal</B> - Soothes the target beast, reducing the range at which it will attack you by X yards. The highest rank can soothe targets level 70 or lower. This ability will help you avoid combat with a beast. If there are several beasts
next to each other, you can cast Soothe Animal on one, and then engage the other. This ability can also be useful for avoiding combat while traveling.
<P>
</blockquote>
<B>Shapeshifting</B><BR>
The Druid can turn into a Bear or Cat, and transform into Aquatic Form or Traveling Form. The main penalty for this
transformation is that the Druid is unable to cast spells, use items, or even talk to NPCs.
You will need to learn proper management of these forms.
You will likely have to switch to Druid form after combat to cast spells, heal, and use buffs, and then return
to Bear or Cat form when you're ready to fight again. You will also need to learn which is the best form to
fight in for each situation. Druids regenerate mana while in shape shift form.
Shapeshifting now breaks roots, snares, and freeze effects. All Shapeshift forms now include an immunity to polymorph effects.
Shapeshifting into an animal form will remove Polymorph effects.
Shapeshifting will counteract the slowing effects of frost-based spells (Frostbolt, Frost Shock, Cone of Cold, etc.).
<P>
<UL>
<img src="../../../shared/wow-com/images/parchment/plain/light/bulletblack-big.gif" width="16" height="17" border="0" align="center">Druids in shapeshifted form can gather herbs and skin creatures.  <BR>
<img src="../../../shared/wow-com/images/parchment/plain/light/bulletblack-big.gif" width="16" height="17" border="0" align="center">When in Bear form, Dire Bear form, Cat form, Travel form or Aquatic form, the druid is considered a Beast.
</UL>
<B>Druid Form</B><BR>
This form is useful when soloing until your Cat abilities are built up. While in parties if you are acting as a healer,
you will need to remain in this form so you're ready to heal.
<P>
<B>Bear Form</B><BR>
While in Bear form you are similar to a Warrior. You might wish to read over the <a href="warrior.php">Warrior page</a>
for insights on how to play a Warrior. Bear form increases armor and hit points, and allows the use of various bear abilities.
Bear form is useful in parties when you need someone to tank. The Bear has several useful abilities including taunt and a
stun to interrupt spells. These abilities are only usable in Bear form.
<blockquote>
<B>Swipe</B> - Swipe X nearby enemies.
<P>
<B>Growl</B> - Adds a small amount of threat to the target. This is your taunt ability that you use to get the monster to attack you rather than a weak party member such as a spellcaster. You will need to use this ability a lot to train it up to useful levels.
Rage cost reduced if Growl fails. Growl is useable against targets
that are immune to physical attacks.
<P>
<B>Demoralizing Roar</B> - The Druid roars, decreasing nearby enemies' attack power by X.
<P>
<B>Maul</B> - Increases the Druid's next attack by X damage. Rage cost reduced if Maul fails.
<P>
<B>Enrage</B> - Generates X rage over X sec. but decreases armor by X%. The Druid is considered in combat
for the duration.
<P>
<B>Bash</B> - Bashes the target, stunning for X seconds. This of course is especially useful against spellcasters. Rage cost reduced if Bash fails.
<P>
<B>Challenging Roar</B> - Taunts all nearby enemies for X sec. Challenging Roar is useable against targets
that are immune to physical attacks.<BR><BR>
<B>Frenzied Regeneration</B> - Converts up to X rage per second into health for Y sec. Each point of rage is converted into Z health.
</blockquote>
<B>Aquatic Form</B><BR>
Shapeshift into aquatic form to increase swim speed and gain underwater breathing.
Aquatic form looks like an angry seal. This form is useful for underwater exploration. It's useful for traveling by water faster than by land until
travel form is available. Trick: if you're fighting under water and are about to run out of air (breath), convert to
Aquatic form to replenish air. Then switch back. You can fight in Aquatic form for laughs but you can't use any
special abilities or cast spells.
<P>
Aquatic form mana cost scales with level.
<P>
<B>Cat Form</B><BR>
Shapeshift into Cat form to increase attack speed and gain the use of various Cat abilities.
The cat is much like the Rogue. You should read the <a href="rogue.php">Rogue Basics page</a> for more details on how to play a Rogue.
Many Cat abilities are similar to Rogue abilities. Cat form is best for soloing and for party use. If you're a Rogue player,
you can appreciate how useful this form can be.
<blockquote>
<B>Prowl</B> - Allows the Druid to sneak around, but reduces movement speed to X% of normal.
Abilities will not cancel Prowl when they fail to go off.
<P>
<B>Claw</B>  - Claw the enemy, causing X additional damage. Awards one combo point. Energy cost reduced if Claw fails.
<P>
<B>Rip</B>  - Finishing move that causes damage over time. Damage increases per combo point.
<P>
<B>Shred</B> -  Shred the target, causing 225% damage plus X damage to the target. Must be behind the target. Awards one combo point. You do not need to be in stealth mode to use this ability. Energy cost reduced if Shred fails.
<P>
<B>Tiger's Fury</B>  - Increase damage done by X for X sec.
<P>
<B>Rake</B>  - Rake the target for X damage and X damage over X seconds. Awards one combo point.
Because this is a damage over time spell, you should only use it when you intend to fight a monster long
enough for the spell to reach its full duration. Energy cost reduced if Rake fails.
<P>
<B>Dash</B> - Increases movement speed for X sec. Dash can be used while prowling.
<P>
<B>Cower</B> - Cower, causing no damage but lowering your threat a small amount.
Energy cost reduced if Cower fails. Cower is useable against targets
that are immune to physical attacks.
<P>
<B>Track Humanoid</B> - Shows the location of all nearby humanoids
on the minimap for X minutes.
<P>
<B>Pounce</B> - Pounce on the enemy, stunning the target for X sec and causing X damage over X sec. Must be stealthed and behind the target. Awards one combo point.
Energy cost reduced if Pounce fails.
<P>
<B>Feline Grace</B> - Reduces damage from falling. You can use this ability to jump off high cliffs and land safely.
<P>
<B>Ferocious Bite</B> - Finishing move that causes damage per combo point and converts each point of energy into additional
damage.
</blockquote>
<B>Travel Form</B><BR>
Transforms the druid into a travel form, increasing movement speed. You become a cool-looking cheetah.
Travel form mana cost scales with level. Travel form is not useable underwater or indoors.
<P>
<B>Druid Form: Typical Combat</B>
<OL>
<LI>        Blast the enemy with Wrath or Starfire
<LI>        Cast Moonfire on the enemy
<LI>        Cast Faerie Fire
<LI>        Begin melee combat
</OL>
<B>Druid Form: Alternative Method</B>
<OL>
<LI>        Cast Entangling Roots on the enemy
<LI>        Blast the enemy with Wrath or Starfire
<LI>        Cast Moonfire on the enemy
<LI>        Blast some more or cast Faerie Fire once the target breaks the Entangling Roots
</OL>
Look at the Warrior and Rogue pages for tips on playing in Bear (Warrior) and Cat (Rogue) form.
