<P>
<B>Action Bar</B><BR>
Each stance has its own action bar. You place abilities for that stance on that bar. When you switch stances, your action bar also switches.
<P>
<B>Shouts</B><BR>
The Warrior has several Shouts which are useful in combat situations. All Shouts require Rage to use, are instant, and have a moderate re-use timer.
<blockquote>
<B>Battle Shout</B> - Increases the melee attack power of all party members within X yards by Y. Lasts Z sec. <BR>
<B>Intimidating Shout</B>  - The warrior shouts, causing the targeted enemy to stay and fight. All other nearby
enemies will flee in fear. Lasts X sec..<BR>
<BR>
<B>Demoralizing Shout</B> - Reduces the melee attack power of all enemies within X yards by Y. Lasts Z sec.<BR>
<B>Challenging Shout</B> - A Shout that causes all nearby enemies to attack the Warrior.
This shout has a long cooldown time so it is best used in emergency situations to save group members.
</blockquote>
<B>General Abilities</b><BR>
<blockquote>
<B>Heroic Strike</B> - Strong attack that increases the melee damage on your next attack.<BR>
<B>Sunder Armor</B> - Your next attack reduces target's armor. Sunder Armor can be stacked up to 5 times. Continue to reduce the armor of the target to make them take more damage.
Sunder Armor will not stack with Expose Armor, but will stack with Faerie Fire.
<BR>
<B>Shield Bash</B> - Instant attack that bashes the target for X damage, interrupting spell casting, and preventing any spell in that school from being cast for Y sec. Not useable in Berserker Stance.<BR>
<B>Hamstring</B> - Your attack will cause X damage and slow the enemy's movement to Y% of normal for Z sec. Best when used to prevent an enemy from quickly fleeing. Can also be used as an escape ability. Available in both Battle and Berserker stance. <BR>
<B>Rend</B> - Your attack will cause the target to bleed.   In addition to your regular weapon damage your target takes
bleed damage for a brief period of time. This is a DOT that should be used at the start of combat.  Available in
Battle and Defensive Stance.<BR>
<B>Blood Rage</B><BR>
An instant ability that sacrifices some of your life to increase your Rage bar.  Best used in a group
that has a healer. Generates X rage immediately and Y rage over its duration. Can be used in all stances.<BR>
<B>Slam</B> - Slams the opponent, causing X additional damage. Can be used in all stances.<BR>
<B>Intercept</b> - Charge an enemy, causing X damage and stunning it for Y sec.<BR>
<B>Retaliation</B> - Instantly counter-attack any enemy that strikes you in melee for X sec. Melee attacks made
from behind cannot be counter-attacked.<BR>
<B>Execute</B> - Attempt to finish off a wounded foe, causing X damage and converting each extra point of rage
into X additional damage. Only works on enemies that have X% or less health.<BR>
</blockquote>
<B>Battle Stance Abilities</b><BR>
<blockquote>
<B>Charge</B> - This should be used at the start of combat to generate some Rage. The Warrior will quickly dash to the monster's
location. Charge also allows for a chance to stun the enemy for a moment. To use this ability, the warrior
can not be in active combat mode and must be close enough to the enemy without being inside the minimum
range for the charge. It may take some time to learn how to properly use Charge.<BR>
<B>Thunderclap</B> - An instant close range area attack.
All enemies affected by Thunderclap take slight damage but their attacks are also greatly slowed for a short time.<BR>
<B>Overpower</B> - Instant attack that causes weapon damage plus X. Only usable after the target dodges. The overpower can not be blocked, dodged, or parried.<BR>
<B>Mocking Blow</B> - Your attack causes additional damage and forces the target to attack you!
It is great for saving a group member who is in trouble.
Mocking blow has a long cooldown time so use sparingly.<BR>
</blockquote>
<B>Defensive Stance abilities</B><BR>
Defensive Stance reduces the damage taken and damage caused.
<blockquote>
<B>Shield Block</B> - Greatly increases chance to block. Only blocks one frontal attack. In addition to reducing the damage taken it also sets up the revenge counter attack combo.<BR>
<B>Disarm</B> - If the enemy is using a weapon, a successful use of Disarm will greatly reduce the damage dealt by the target for a short period of time.  Long cooldown time.<BR>
<B>Taunt</B> - Instant ability that taunts the target. A successful use will draw the attention of the target, causing
them to attack you. This should be used regularly to keep the monsters attacking you.
This of course requires healers to keep you alive during the battle.<BR>
<B>Revenge</B> - Instant attack that causes extra damage.  Counter attack the enemy for additional damage.
This must follow a defensive action. Best used in conjunction with Shield Block.<BR>
</blockquote>
<B>Berserker Stance abilities</B>
<blockquote>
<B>Cleave</B> - A sweeping attack that does your weapon damage plus X to the target and his nearby
ally. This ability is useful when fighting multiple monsters at once. Make sure the other monster
isn't slept; otherwise Cleave will wake them up.<BR>
<B>Berserker Rage</B> - The warrior enters a berserker rage, becoming immune to fear and knockout effects and
generating extra rage when taking damage. Lasts X sec.
<BR>
<B>Recklessness</B> - The warrior will cause critical damage with most attacks and be immune to fear effects for the next X sec., but damage taken is increased by Y% <BR>
<B>Whirlwind</B> - In a whirlwind of steel you attack up to X enemies within Y yards, causing weapon damage to each enemy.
<BR>
<B>Pummel</B> - Instant attack that causes damage and interrupts spellcasting for a short duration.
</blockquote>
<B>Combos</B><BR>
Use a Shield Bash/Block, then attack with Revenge.
<P>
<B>Training</B><BR>
It's very important to build up your skills in multiple weapons so you can easily switch to a different weapon if a better
weapon of a different type comes along. Keep weapons of as many types as you can in your packs and continue to swap them to build up your skills.
<P>
<B>Tips</B><BR>
<LI>Team up with healers (Priest, Shaman, Druid) to keep you alive.
<P>
<LI>Pick one or two of the two-handed weapon types to use.
<P>
<LI>Manage your Rage! If you are fighting a caster save some  for a timely shield bash/Pummel to stop them from blasting you or
healing themselves. That one extra use of Heroic Strike may have caused some extra damage but if the monster heals it's like starting
all over.
<P>
<LI>Try and plan your Talents ahead of time.
<P>
<LI>Protect your group mates. A Priest or a Mage isn't meant to take the brunt of an enemy's attack.  Use the Taunt ability to
focus the attacks on yourself.
