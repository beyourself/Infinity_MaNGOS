<font color=red>This page does not have a complete listing of all abilities that are available. Look for more once our database section is active. </font>
<P>
<B>Rage</B><BR>
The Warrior has a red Rage bar. This is similar to a Mana bar for casters. During combat the Warrior generates
Rage based both on damage done and damage taken/prevented. You may also generate Rage using specific abilities.
The Rage bar continues to fill as the Warrior fights. Once the Warrior has enough Rage points, the Warrior can
use different skills that require Rage. When not in active combat, the Warrior's Rage slowly fades to 0.
The Warrior also loses all accumulated Rage if the Warrior switches stances, so plan ahead and avoid changing
stances mid fight!
<P>
<B>Stances</B><BR>
Warriors have different stances available to them which determine what abilities can be used during combat.
There are special stance buttons located above your action bar to switch stances. If you switch stances during battle you
lose all your Rage so keep that in mind. Typically you don't switch stances during battle.
Stances are for different situations.
<UL>
<LI><B>Battle Stance</B> is used mainly for soloing, adding different state
changing attacks and high damage moves.<BR>
<LI><B>Defensive Stance</B> offers fewer types of offensive special attacks but gives the Warrior more defensive specials plus a
bonus to your defense rating. This is primarily meant for acting as a group's main tank.
<LI><B>Berserker Stance</B> allows for more critical strikes but the Warrior takes more damage.
This is best used when the Warrior is a secondary tank in a party.
</UL>
<B>Other Information</B><BR>
Warrior abilities that target one or more enemies generate additional threat.
<P>
<!----------
<B>Berserker stance</B>
is used when you need added damage/sec. or if in a group where you are not acting as the group's main tank.
