Here are some tips for managing your items.
<p>
<B>Bags </B><BR>
You can open up all your current bags at once by hitting "Shift + B."
A good suggestion is to remap the B hot key so it simply opens all your bags at once. That way you
don't have to figure out which bag contains the item you're looking for. Whenever you need something, hit
B to open up all your bags.
<P>
<blockquote>
<B>Bag Organization</B><BR>
Come up with an organization plan for your bags. The very left bag (or top when you open them all) can be
devoted to permanent items that are important. The next bag can be dedicated to holding quest items. The third
bag could hold all your trade skill items, and so on. Your default backpack could contain food, water, and things
you use for healing. It helps to pick different colored bags so you can easily identify which bag your
item belongs in.
</blockquote>
<B>Equipment Plans</B><BR>
There are many different possible equipment plans. Decide what is best for you based on your playing style.
<P>
<blockquote>
<B>Looks/Style</B> - Some players select their equipment based on how it looks on their character. This is
great for style. However, from a combat point of view it doesn't really matter what your character looks like.
Stats are the most important aspects for combat. From a player's point of view, however,  you may notice that your
character's appearance will influence the actions of other players. If you look especially cool you may find
other players offering compliments.
Therefore, you might decide that your look is most important and thus focus mainly on your appearance,
not worrying about character stats. If you can get the appearance you're looking for, while also having superior
stats on the items you like, that's even better.
<P>
Suggestion: keep an alternative outfit in your backpack or bags to wear when you're not fighting. This is
great approach to use for a role-playing/realistic point of view. The only downside is that it takes up
room in your pack(s) that could be used for other items.
<P>
<B>Stats First</B> (Power Play) - Power players are primarily attracted to an item's stats and how it will
improve their character's performance.  They are willing to wear any item that improves their stats even if
it includes, for example, wearing a dress on a male character!
<P>
If you are focusing on stats, find out which will benefit your character class the most. There are often
multiple paths you can select. You should ask other players of the same class, especially more experienced
players, for their advice on what stats will best benefit your character. For example, Warriors
should look for high damage weapons and might want to focus on Strength. Or, perhaps, Warriors might
want to focus more on Stamina increasing items.
<P>
<B>Which item is better?</B><BR>
This is a common question. The most general solution to this problem is to ask other players which item is
the better item. Link both items in a party or guild chat channel and then ask other players what they think
is the better item to use for your character.
Feel free to ask why one item is better than the other.
You will then learn and gain experience that you can use later, or if someone asks you for help. There are usually plenty of stat
focused players out there that know exactly what the "best" item is.
<P>
<B>Looks and Stats</B><BR>
This option is the best of both strategies. Find an item with stats that help your character that also
looks great!
</blockquote>
<B>Item Selling</B><BR>
It's important to learn which items should be sold to other players and which should be sold to the vendors.
<P>
<blockquote>
<B>Other Players</B> - What will other players buy? This is determined by demand and luck. Players will
most often buy your item if it's one they consider good and is sold at the right price. Players will go
for items they want or need. If you're trying to sell an item that is easily obtainable,
it's unlikely to be purchased by other players. A good way to learn which items are valuable is to
pay attention and watch auctions. You can see what types of items people sell and for what price. That way
if you ever get one of those items and look to sell it, you will know what it will sell for. You
should also ask other players for their advice. Also, item colors are important to learn.
Blue items are often more valuable than green items, and so on.
<P>
<B>"Vendor Trash"</B> - This term has come to refer to items that should be sold to the merchants.
This is because they won't, or are extremely unlikely to be bought by other players.
Perhaps they are some body part that is dropped by a monster. Since players most likely won't
purchase the item, it leaves only one group of buyers, the NPCs! If someone tells you an item is
"Vendor Trash" this means you should sell it to the merchants. Of course they could just be insulting your item!
Make sure you know the difference.
</blockquote>
<B>Old Equipment</B><BR>
Once you're done using an item, likely because you have found a better one, it's time to get rid of it.
You have several options:
<P>
<blockquote>
<B>Hand-me-downs to a friend or guild mate</B><BR>
Lower level or poorly equipped players appreciate gifts. This can improve your standing with them,
which might benefit you later on. If the item is bound to your character, your only choice is
to sell the item to a vendor.
<P>
<B>Auction</B><BR>
Sell the item in an auction. "One person's trash is another person's treasure." Your old items might fetch
a hefty sum. Be sure that the item isn't soulbound to your character before trying to sell it.
<P>
<B>Merchants</B><BR>
Anything you cannot give away, or sell to other players, should be sold to the merchants.
</blockquote>
<B>The Bank</B><BR>
Use the bank to store items. This will free up space in your bags. Store anything that you are not using
or that you're unlikely to use.
<blockquote>
<B>Future Upgrades</B> - Some items have a level requirement above your current level. So you must wait until you level up before you can use them. <BR>
<B>Items For Other Classes</B> - Save items for friends or mail them to your other characters.<BR>
</blockquote>
Don't forget that you can get additional storage by buying bag slots and filling them with the
largest bags you can find.
<P>
<B>Out of Room?</B><BR>
If you're out of room, it's time to make some decisions as to what to keep. Try to sell anything you can
to other players. If you have some items that you've tried to sell but cannot, it might be time to
sell them to the merchants.
<P>
