<B>Level Requirements / (min 10)</B><BR>
Many items have level requirements. You cannot use them until your character has reached that level. Place your cursor over
the item to check the level requirement. "(min 10)" is the minimum level that you have to be to use the item.
So you would have to be character level 10 or higher to use this item.
<P>
Items are listed in <font color=red>red</font> when your character cannot use them.
<P>
<B>Item Quality Colors</B> (from least powerful to most powerful)
<blockquote>
<font color=gray><B>Poor Quality*</B></font><BR>
<font color=white><B>Common Quality</B></font><BR>
<font color=00c400><B>Uncommon Quality</B></font><BR>
<font color=blue><B>Rare Quality</B></font><BR>
<font color=purple><B>Epic</B></font><BR>
<B>Legendary</B> (Color Undecided)<BR>
<B>Artifact</B> (Color Undecided)<BR>
</blockquote>
*Poor Quality Items should be sold to the vendor. Players do not want these items.
<P>
<B>Item Type Rarity</B><BR>
This section explains when you will come across items for certain slots on your character.
<P>
At level 1: Chest, Shirt, Hands, Legs, Feet, Waist, Wrist<BR>
At higher levels (not revealed), listed in the order at which you will find them: Shoulder/Finger, Head/Neck, Trinket.
<P>
<B>Item Bound Types</B>
<blockquote>
<B>Not Bound</B> - Items can be equipped, removed, or traded at any time and in any order.<BR>
<B>Bind on Equip</B> - You can trade this item if you haven't equipped it. Once you equip it, you can no longer trade
it with other players.<BR>
<B>Bind on Use</B> - Once you have used this item, you can no longer trade it.<BR>
<B>Bind on Acquire</B> - Once you pick up the item, you will no longer be able to trade it with other players. Be very careful
not to loot this item if you're not supposed to have it permanently.<BR>
<B>Quest Items</B> - Quest Items are bound to your character. You cannot trade them.
</blockquote>
<B>Durability</B><BR>
Item durability is an important aspect of World of Warcraft that works to stabilize the economy,
decrease inflation specifically in the high end game, and in general
keep prices of player sold items lower than normally would occur without
such a feature.  Durability should also add additional depth to the game
since players will have more variety in their items and more abilities
will be added over time to interact with durability. Item durability is
different from item decay in that players will never lose items through
item durability. On death, all equipped items will lose 10% durability.
<P>
<UL>
<B>Durability key points</B><BR>
<LI>All armor and weapons have durability, but not trinkets, cloaks, shirts,
  guild tabards, necklaces and rings.
<LI>Items remain at full effectiveness until they have no durability. Their
  power does not degrade.
<LI>Items cannot permanently break.
<LI>All armor/weapon vendors can repair durability in exchange for cash.
<LI> When an item reaches zero durability, it no longer gives the player any
  benefit; it acts as though it does not exist until repaired.
<LI>Professions cannot repair durability, but we will discuss ways for the
  professions to interact with them in the future.
</UL>
<B>Set Items</B><BR>
There are Set Items where wearing more pieces of an item set will convey more bonuses to your character.
<P>
<B>Armor Swapping</B><BR>
Armor can't be swapped out (changed) while in combat.  Other equipment such as weapons and accessories can be swapped out.
<P>
<B>Linking Item Stats to Chat Text</B><BR>
You can create a link to your item in chat text, which is useful for showing it to people for trading purposes or just to share information
about your item. Hold down "Shift" and left-click on the item while having your chat text typing box open. This will
insert a link to that item. Whenever you see [itemname], you can click on that text to see the stats of the item.
Trade channels are the only general chat channels that allow item links. You can also link to items in the auction house to
show items for sale to your friends =].
<P>
<B>Stacking</B><BR>
Some items are stackable. They will stack/combine automatically when you drag them over to your backpack. You can
break up a stack of items by holding "Shift" and then left-clicking on the item. This will bring up a number box, which you can
use to indicate how many items you want to take out of the stack.
<P>
<B>"Made By &lt;Playername&gt;"</B><BR>
This is an item that was created using a trade skill. The name on the item is the name of the person that created the item.
<P>
<B>+Damage vs. +Mana</B><BR>
Using Fireball as an example, you can cast more fireballs with the INT item, but the damage you do is greater with the DMG item.
Overall you deal slightly less damage on a full mana bar but at an increased DPS using the DMG item.
<P>
<B>Cloaks & Helms</B><BR>
There are options that let you not display your helm and/or cloak on your character. These options will
allow a player to wear a cloak or helm, but not have it show up in the game world. The check boxes to
control this functionality are in the Options menu under "Interface Options." If you select to disable
the display of these items other players will not see them as well.
<P>
<B>Additional Information</B><BR>
<UL>
<LI>You can turn in Thorium Shells (bullets) in exchange for Thorium Headed Arrows. Both Orgrimmar and Ironforge have a turn-in NPC.
<P>
<LI>Elixirs will not stack with player spells that improve the same attribute.
<P>
<LI>Shields have blocking values. Blocked attacks avoid an amount of damage based on your shield and your character's Strength.
<P>
<LI>Wand use can be interrupted when the user takes damage.
Wands can be used by all classes that possess the Wand Proficiency; fire, frost and shadow skills are
not necessary.
Wand damage is reduced by resistance in the appropriate school.
Wand use is not prevented when silenced.  It is prevented in all the same situations
melee is prevented.
<P>
<LI>Quest Items: Quest rewards are labeled as Bind on Acquire.  Quest rewards can still be equipped or sold to a merchant, but you
can't sell or give them to other players.  This has been implemented to deter these items from flooding the game economy which was
an issue prior to this rule.
<P>
<LI>Quivers and ammo pouches will increase attack speed when using a gun or bow.
<P>
<LI>Items with procs are using time-based procs. Their proc frequencies are entered as the number of desired
procs per minute, and their actual per-swing proc chances are calculated from that using the weapon's speed.
<P>
<LI>All cloaks can be worn by all classes.
<P>
<LI>Items that increase percentages to block, dodge, crit, parry, and hit chance now stack.
</UL>
<P>
<B>Common Questions</B>
<blockquote>
<B>Where do I find rings, necklaces, trinkets, shoulders, and hats?</B><BR>
These items are more commonly available at higher levels, for more powerful characters.
<P>
<B>To what items can I apply armor patches?</B><BR>
Chest (body armor), feet (boots), hands (gloves), legs (pants).
<P>
<B>How do item drops work?</B><BR>
Monsters randomly drop items from a loot table (grab bag of possible items). Some monsters always drop an item, such
as powerful, rare, and uniquely named monsters.
<P>
<B>Where can I get armor patches?</B><BR>
Leatherworkers can make them. You can get them elsewhere as well, such as a quest reward.
<P>
<B>How can I see what items I can use?</B><BR>
Hit "C" for your character screen. Then, click on the "Skills" tab. Scroll down to "Proficiencies" which will list
what types of items you can use.
<P>
<B>What items don't display on my character?</B><BR>
Trinkets and rings do not display on your character.
<P>
<B>Do items have weight? Can I be encumbered?</B><BR>
Items do not have weight. You can carry as many items as you have room for without any negative effect on your character.
</blockquote>
