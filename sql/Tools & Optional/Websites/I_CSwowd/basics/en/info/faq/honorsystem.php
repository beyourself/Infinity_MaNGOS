<b class="blood">Quick honor system explanation</b>
<blockquote>
<b>Honorable kill (HK):</b> killing a "green" or higher level player of the opposing team, or killing one of the NPCs considered a racial leader.<br>
<b>Dishonorable kill (DHK):</b> killing a "gray" NPC flagged as a civilian.<br>
<b>Contribution points (CP):</b> points awarded daily based on your honorable kills.<br>
<b>Honor points (HP): </b> points awarded weekly based on your contribution points compared to the total contribution points awarded to your entire team throughout the week. HK has no direct effect on your honor points.
</blockquote>
<b class="blood">Contribution points</b>
<blockquote>
<b>High levels yield more:</b> killing a player higher level than you yields a lot more CP than killing a lower level one. Also, a level 10 killing another level 10 will get less CP than a level 50 killing another level 50.<br>
<b>Groups and raids:</b> all members of groups and raids alive and nearby at the time of the kill get one HK and a share of CP.<br>
<b>Repeated killing:</b> you only get full CP for killing the same player once per day. The second kill on the same player on the same day gives 75% of the CP, the third 50% and the fourth 25%. Subsequent kills give no CP at all.<br>
<b>Sharing with monsters:</b> monsters take their share of the CP. Attacking a player fighting a monsters might net you an easier kill, but it gives less reward.<br>
<b>No-points timer:</b> when players exit an instance or arrive from a flight they are worth zero CP for some time.<br>
<b>Dishonorable kills:</b> dishonorable kills currently have no effect on your CP, but this will change in the next content patch.
</blockquote>
