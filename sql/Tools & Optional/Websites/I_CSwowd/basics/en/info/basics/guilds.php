<h3 class="guidetitle">Guild Names:</h3>
Be sure to create an appropriate (non offensive) guild name</a>.
Offensive guild names might be renamed or the guild might be disbanded. Sometimes a name is already taken or disallowed. Keep
trying different names until you get one that is accepted.
<P>
<h3 class="guidetitle">Guild Commands</h3>
Note: most of these commands can only be used by officers and leaders.
<ul>
<B>/ginfo</B> - Gives basic information about your guild<p>
<B>/g &lt;message&gt;</B> - Sends a chat message to all members of your guild<p>
<B>/o &lt;message&gt;</B> - Sends a chat message to all officers of your guild<p>
<B>/ginvite &lt;player&gt;</B> - Invites another player to join your guild<p>
<B>/gremove &lt;player&gt;</B> - Removes a player from your guild<p>
<B>/gpromote &lt;player&gt;</B> - Promotes a player one rank within your guild<p>
<B>/gdemote &lt;player&gt;</B> - Demotes a player one rank within your guild<p>
<B>/gmotd &lt;message&gt;</B> - Sets the guild's message of the day<p>
<B>/gquit</B> - Removes you from your guild<p>
<B>/groster</B> - Gives an entire guild roster (officers and leaders only)<p>
<B>/gleader &lt;player&gt;</B> - Sets another player as the guild leader (guild leader only)<p>
<B>/gdisband</B> - Disbands your guild (guild leader only)<p>
</ul>
</span>
</td>
