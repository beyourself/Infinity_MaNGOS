<p>
<img src="../../../../shared/wow-com/images/basics/battlegrounds/warsonggulch/info/quests_thumb.jpg" width="200" height="160" border="0" align="right" style="margin-left:10px;" usemap="#quests"><B>Quests</B><BR>
There is a repeatable quest given at the entrance to the Battleground. This quest gives everyone except for max-level
players experience each time they win a Battleground. The first time you turn in the quest, you will get a
faction increase. Subsequent turn-ins of this quest will not grant faction increases, although you will continue
to earn experience.
<P>
<B>Level Ranges</B><BR>
There are six Warsong Gulch level ranges (10-19, 20-29, 31-39, 41-49, 51-59, and 60). Only your level range options are visible.
<P>
<B>Starting</B><BR>
When you first start the match, you will be in an enclosed area. One minute after the map starts, a 1-minute timer will count down before the barriers around the starting point are removed. This is the cue to go!
<P>
<B>Flags</B>
<UL>
<LI><img src="../../../../shared/wow-com/images/basics/battlegrounds/warsonggulch/info/flags_thumb.jpg" width="200" height="160" border="0" align="right" style="margin-left:10px;" usemap="#flags">Right-clicking on the enemy's flag will cause you to pick it up.
<LI>Right-clicking on the enemy flag icon while you're carrying it will cause it to drop.
<LI>Invulnerability effects, mount spells, and stealth spells prevent a player from picking up a flag. Using these effects while holding a flag causes it to drop.
<LI>If your flag isn't being carried, right-clicking it will port it to your base.
<LI>Returning the enemy's flag to your flag's starting position while your flag is there will do the following:
<UL>
<LI>Earn a capture for your team
<LI>Respawn both flags at their starting position after a short period (one minute or less)
</UL>
<LI>Messages are sent to all players in the Battleground whenever a flag is picked up, dropped, returned, or captured.
<LI>Capturing three flags will win the game, granting a reputation bonus with your allies and a quest item noting your victory.
<LI>Players earn Bonus Honor when their faction captures a flag or wins the Battleground.
</UL>
<b>Bonus Honor</b><br>
You receive bonus honor points for completing certain objectives. These honor points are caclulated in the form of "bonus kills for a maximum leveled character". The honor points from these bonus kills are not split with anyone else. The honor bonus varies so there isn't a set number that can be listed. The following is a list of how many bonus kills you get for each objective:
<ul>
<li>Capturing a flag (3 bonus kills each)
<li>Winning the match (1 bonus kill)
</ul>
<B>Other Information</B>
<UL>
<LI><img src="../../../../shared/wow-com/images/basics/battlegrounds/warsonggulch/info/guides_thumb.jpg" width="200" height="160" border="0" align="right" style="margin-left:10px;" usemap="#guides">There are three respawning power-ups. Check them out.
<LI>Right-clicking an enemy player's corpse prevents that player from being able to resurrect using player spells or corpse reclamation. You can also loot money from the dead player's corpse.
<LI>Spirit Guides at each faction's "graveyard" location periodically resurrect players in an area around the Spirit Guide. Players are revived in waves. If there are too many dead players, some players might have to wait until the next resurrection wave to be revived. You can also talk to a Spirit Guide to start the timer again.
<LI>The Warlock's Ritual of Summoning spell is disabled.
<LI>Resurrected Hunters and Warlocks will have their pets resurrected.
</UL>
