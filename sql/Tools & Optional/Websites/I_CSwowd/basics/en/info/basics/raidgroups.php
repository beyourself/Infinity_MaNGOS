<img src="../../../shared/wow-com/images/basics/raidgroups/ss-right.jpg" width="197" height="483" align="right" border="0">
<img src="../../../shared/wow-com/images/smallcaps/plain/r.gif" width="40" height="38" align="left">aids are super-groups that allow up to 40 players to link their groups together. You can
use raid groups to fight powerful monsters; to organize player vs. player battles; and especially
to work with others to share loot, experience, and quests in raid dungeons.Raids allow you to overcome challenges
that might otherwise be nearly impossible.
<P>
<b class="blood">Quest Credit</B><BR>
Raid members cannot earn credit for most non-raid quests. Specifically, they will not receive credit for
killing non-raid creatures or collecting items for non-raid quests.
This is to prevent people from CHEESING powerful monsters or quests by just attacking them with a lot of
players, thus making the battle trivial.
<P>
<b class="blood">Experience</B><BR>
Raids grant substantially less experience for defeating monsters than normal groups.
You can, however, still get experience which normally isn't possible unless groups join up.
So while you won't be getting as much experience, you'll at least be getting some!
<P>
<b class="blood">Raid Chat</B><BR>
Once you're in a raid group you can chat in the raid channel by typing
/raid, followed by your text.
<P>
<b class="blood">Raid Group vs. Party Group</B><BR>
A raid group shares a chat channel, experience, loot, and raid quests.
Besides this sharing, other groups in the raid are not connected to your group. You will only see players in your current group.
If you use a group or AOE beneficial spell, it will only affect players in your group, not the entire raid.
<P>
<b class="blood">Finding Raid Group Members</B><BR>
You cannot see other raid members on the map if they are not in your group. If you want to find out where
a member of the raid is, the easiest way to find out is to swap them into your group, then back to their original group.
<P>
<b class="blood">Raid Leaders and Assistants</B><BR>
<a href="javascript:popUpOmega('../../../shared/wow-com/images/basics/raidgroups/fr/ss01.jpg',750,525,'Raid Window')"><img src="../../../shared/wow-com/images/basics/raidgroups/fr/ss01-thumb.jpg" align="right" border="0"></a>A raid leader can add and remove people to the raid group. They can move players between groups as well. Simply open up the raid group window and click-drag a player to the new location.
You can promote other players in the raid so that they can also invite people to the raid group and move people between groups. This can
save you time if you're not able to efficiently manage up to 40 players at once yourself.
<P>
The raid leader has "(L)" by their name. Assistants have "(A)" by their names.
<P>
<b class="blood">Raid UI</b><br>
<a href="javascript:popUpOmega('../../../shared/wow-com/images/basics/raidgroups/fr/ss02.jpg',750,525,'Raid Inteface')"><img src="../../../shared/wow-com/images/basics/raidgroups/fr/ss02-thumb.jpg" align="right" border="0"></a>
You can open the raid UI and drag the name of a class to your gamefield to have a listing of all players of that class be displayed on the game field. Likewise you can drag a group name to your playfield to have the players in that group displayed on your gamefield. In either case the display will show the health, mana and limited buffs or debuffs on each player in the group or class and you can click on a player listed this way to target that player. You can then right-click on the class or group UI element to make some modifications to how the UI displays or to remove the UI element.<br />
<br />
<b class="blood">Class Colors</B><BR>
Classes are color-coded. There's no significance to the colors beyond allowing that class to stand out against
other class colors.
<P>
<b class="blood">Group Balancing</B><BR>
It is very important to correctly balance groups. Typically, you want a healer in each group. If a Priest is not available, a Shaman, Druid, or even a Paladin can help out.
<P>
<b class="blood">Leaving a Raid Group</B><BR>
To quit a raid group, you must leave your current group. If you forget, you'll continue to be in the raid group.
Requiring you to manually leave a raid allows people to log in and out without falling out of the raid group.
<P>
<b class="blood">/raidinfo</b><BR>
The /raidinfo command shows remaining time on saved raid instances. Also shows an ID to compare with other players (if you have the same saved ID as them, you will go into the same instance)
<P>
<b class="blood">Raid Calendar System</B><BR>
In 1.9, the way raid timers are handled underwent a significant change.The new system has instances reset at a
certain server time, regardless of when you were actually saved to the instance.The reset times are as follows:
<ul>
<li>Molten Core: Every 7 Days, resetting during weekly maintenance.
<li>Blackwing Lair: Every 7 Days, resetting during weekly maintenance
<li>Temple of Ahn'Qiraj (40-man): Every 7 Days, resetting during weekly maintenance
<li>Onyxia: Every 5 Days
<li>Zul'Gurub: Every 3 Days
<li>Ruins of Ahn'Qiraj (20-man): Every 3 Days
</ul>

The resets, if not during weekly maintenance, are planned to occur during off-hours when the least amount of
raids are active. There are many reasons why this particular approach was chosen; we've gone over a
multitude of options and felt that this change would be best in the long run.
<p>
As the reset time approaches, if you are in the instance, you will be informed of the impending reset every hour on
the hour.15 minutes before the reset, the reminders become more frequent; once the reset time is reached, all
players in the instance will be ported to their bind point.You can access your saved raid status at any time
via /raidinfo, and you will be informed of your status regarding an instance when you zone into the dungeon.
<p>
We know that this change is a major one for raiding guilds; we've done our utmost to make the reset times as
convenient as possible for the majority of our players.
