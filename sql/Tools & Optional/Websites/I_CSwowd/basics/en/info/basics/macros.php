<div class = "postBody">
Macros allow you to create personal and cool special commands that you can do at the touch of a button. There are many
possible uses for macros. We'll give you some examples and leave it up to you to figure out more uses for macros.
You can even ask your friends for suggestions!
<P>
<B>Creating a Macro</B>
<UL>
<LI>Type <b>/macro</B> or click on the talk button and select macro. A box will pop up with your current macros. You should have no macros to start.
<LI>Click on "<B>New</B>" at the bottom of that box. Another box will pop up, with a place to enter the
macro name and a choice of an icon for the macro.
<LI>Name your macro. Select a name that will make sense to you when you see it again. For example, "Assist".
<LI>Select an icon. Let's pick a sword.
<LI>Select "<B>Okay</B>"- You should now see the icon you selected (sword) along with your icon name in your current macros box (assist).
<LI>Now <B>Left-Click</B> on the assist icon to highlight it. There is a button there to change the name or icon for your macro if you later decide to rename it or select a different icon.
<LI>Once your assist icon is highlighted, move your mouse pointer into the "Enter Macro Commands" box. This is where
you can type what you want the button to do when you press it. You will need to add a "/" in front of the
command, if it is a slash command, just as you would enter in your text entry box.
<LI>Type "<B>/Assist Nebu</B>".
<LI>Move your mouse pointer back over the sword icon (assist) and <B>left-click</B> and hold down the button over the icon.
<LI>Now, drag the icon over to an empty slot on your action bar.
<LI>To use your new macro icon, press the number associated with that action button, or right-click on the button itself.
<LI>You now have a macro button that will assist another player (Nebu) in their attack.
When Nebu attacks a player or monster, press your Assist macro button and
you will automatically select the same target Nebu is attacking.
</UL>
<B>Finishing Macros</B><BR>
When you are done editing or creating macros, click the "x" in the top right-hand side of the box to close the macro window.
<P>
<B>Editing Existing Macros</B><BR>
Type <B>/macro</B> then <B>left-click</B> on the macro button you wish to change. You can now make changes in the "Enter Macro Commands" box. Click the "x" to close the macro box when you are done.
<P>
<B>Macro Limit</B><BR>
There is a character limit for each macro you create. If your message is too long, make it shorter!
<P>
<B>Macro Suggestions</B><BR>
Use any slash commands
<P>
Here areother possible uses for macros:
<ul>
<LI>repeat text you don't want to type again<BR>
<LI>repeat text for online auctions<BR>
<LI>make a funny saying<BR>
<LI>perform a list of commands such as:
<P>
/leave 1
/leave 2 (to leave channels 1 and 2)<BR>
/join wowtraders (joins channel wowtraders) <BR>
/g Hello everyone! (says hello to your guild chat channel)
</ul>
<B>Other Information</B><BR>
<UL>
<LI>You can use "%t" in your macro text to insert the targeted monster, creature, player that you
wish to be inserted in your sentence. For example, "/say hi %t" will say "Hi Nebu" if you have the player Nebu selected.
<P>
<LI><B>/cast</B> allows you to cast spells by name. Type /cast (spell subtext) - Example: "<B>/cast Fireball (Rank 1)"</B>. To add spell casting to a macro you can
type it manually, or shift-click a spell in your spell book to add the proper /cast line to the macro.
</UL>
