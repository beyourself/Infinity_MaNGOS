To be a good party member you should learn about the basics of good party play.
<P>
<B>Pick a leader and stay with your group!</B><BR>
Pick someone to lead the party around. Make sure it's clear to the party that they should be following that specific
player. Otherwise, people will wander around and it will become a mess.
Wandering players might also wake up a bunch of monsters and cause the party to die.
Things are much simpler if the party just picks one person to lead the group.
<P>
People who are not leading should do what the leader says and make sure that they are always keeping up with the rest of the group.
You can type "/f name" to follow a character in your group. This makes it easier to keep up.
If you need to leave the keyboard, make sure you inform the group so you're not left behind. While
you are lagging behind, monsters could spawn in between you and your group. When you try to return to the group,
you could "train" the monsters to the group and end up killing them.
Also the group might not notice that you haven't been keeping up and
might expect your help during a difficult monster battle, only to die because you were not there!
Remember that you can also use "/afk" (Away From Keyboard).
<P>
<B>Quests</B><BR>
Make sure that everyone has the same quest.
You can do this by asking them to confirm the name of the quests in their quest log.
Otherwise you might get halfway into a quest before you discover that one of your party members hasn't got the quest.
<P>
Stick around until everyone has completed the quest.
<P>
Select your quests based on party size.
If you can do the quest by yourself, don't waste time doing it in a party.
Instead, spend time doing quests that you can't do without a party.
You also might want to avoid quests that require each person in the party to collect a lot of parts.
For example if the party has 5 members, and you need 20 items for each person, that ends up being 100 items you need to collect!
You can complete that much faster by yourself or with a smaller group.
<P>
Some quest items that are not marked as "Quest Items" can be traded with other members in the party. If you get extra, pass them around.
<P>
If you're doing quests that require you to each collect "X" number of items, report to the party your status as you go.
A good way to do it is to say "4/20". That means you have 4 out of 20 items.
That lets people know how far you are and how far you have to go.
Also, after a party member has completed their collection, it becomes easier for the rest of the party to collect
those items; any items dropped for a person with the complete set will now be available to those in the party that still need them.
<P>
If you are doing a multiple part quest and some players are behind, you could help them catch up by doing the earlier steps of the quest. That will allow everyone to be on the same step.
<P>
<B>Casters</B><BR>
It's the job of the caster to cast any helpful buffs on the tanks.
The caster should also heal (if they have it) the tanks and help keep them alive.
They should also remove any enemy debuffs that are cast on the tanks.
<P>
<B>Buffs</B><BR>
Ask for a caster to "Buff" you again when the original wears off. If you're the person casting the Buffs, ask your party members to let you know when they
need/want them again.
<P>
When Buffing, be sure to Buff yourself first and then the other members of your Party.
When your Buffs begin to drop, you will automatically know that your Party member's Buffs are about to drop soon as well.
<P>
<B>Health Bars</B><BR>
Watch the health bars of players and heal anyone who starts to get low.
If you have some other ability to save the person being attacked, such as a tank taunting the enemy, do that instead.
It is especially important to save the character that is responsible for healing if they are near death.
<P>
If you're nearly dead, stop attacking and let someone else take over attacking.
<P>
<B>Tanks</B><BR>
It is the job of the "Tank" to soak up damage for the party.
If the monsters start attacking weaker classes such as Mages, Shamans, or Warlocks, it's the Tanks job to attack the monster and pull them off the casters.
<P>
<B>Don't Run!</B><BR>
It is important not to run, even if you're about to die unless the whole party is running.
If you run you will likely wake up more monsters or travel to some place where it will be more
difficult to resurrect you. Ask for help and stand still so that the
tanks can come and taunt them off you or kill the monster. Don't run in circles either because the melee
players won't be able to reach the monster.
<P>Stop attacking the monster so that you don't rack up points in their hate memory/list.
You might even want to just die instead of getting the rest of the party killed. You can be resurrected once the battle is
over.
<P>
<B>Communication</B><BR>
<LI>Let your party know if you see a monster patrolling near that might attack unexpectedly.<BR>
<LI>Let your party know if you have to leave your computer.<BR>
<LI>Let your party know if you are low on Mana if you are required to have Mana for a battle.<BR>
<LI>Let your party know if you spot anything they are looking for, such as specific monsters, landmarks, or even trade skill nodes (Mining/Herbalism). <BR>
<LI>Let your party know if you're about to die and you need healing (if you're unsure if they will heal).<BR>
<LI>If you are a caster, let your party know if monsters are attacking you so they can stop attackers if they are not already drawing them off of you.<BR>
<LI>Be reasonable in your communication. You don't need to spam or repeat yourself, or otherwise annoy the players you're asking to help you.
<P>
<B>Pay Attention/Keep Up</B><BR>
Don't spend time watching TV, reading, or diverting your attention away to some other distraction. The game usually requires your full attention.
<P>
<B>Don't Wake Up Slept/Disabled Monsters</B><BR>
Players put monsters out of the battle to make the battle easier. Don't wake them up. Make sure you say sorry if you do
and try to correct this in the future. Casters should also make sure they don't try to sleep monsters that are already
being attacked.
<P>
<B>Pool Resources</B><BR>
If you pool your money together you can often buy something good for a party member that will help the party.
This should mainly be done only if you are playing with good friends or guild mates.
