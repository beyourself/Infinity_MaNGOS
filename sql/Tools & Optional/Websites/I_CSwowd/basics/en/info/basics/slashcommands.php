<div class = "postBody">
You can type "/a" &lt;started by hitting the tab button&gt; and it will scroll through all the "a"
commands.
<P>

<B>/tell&lt;player&gt; &lt;message&gt;</B> (alias '/t') - Send a player a private message. You can scroll through
all the people that whispered you recently by typing on the tab key.
<BR>
<B>/w &lt;player&gt; &lt;message&gt;</B> - Send a player a private message. You can scroll through
all the people that whispered you recently by typing on the tab key.

<BR>
<B>/chathelp</B> (alias '/chat') - List of chat commands.<BR>
<B>/em &lt;message&gt;</B> (alias '/me') - Creates an emote.<BR>
<B>/exit</B> - Quit the game.<BR>
<B>/follow</B> (alias '/f') - Target a player and type this. You will follow that player.<BR>

<B>/ignore &lt;player&gt;</B> - Ignore playe.r<BR>
<B>/unignore &lt;player&gt;</B> - Turn off ignore player.<BR>
<B>/invite &lt;player&gt;</B> (alias '/inv') - Invite a player to your party.<BR>

<B>/logout</B> - Log out of the game.<BR>
<B>/party &lt;message&gt;</B> (alias '/p') - Sends a message to party chat.<BR>
<B>/played</B> - Reports the amount of time you've played the game with that character.<BR>
<B>/pvp</B> - Enable your character to be attacked by other players in Player vs. Player combat.<BR>

<B>/random &lt;X&gt; [Y]</B>: Generates a random number between X and Y, if Y isn't
specified, then it will generate a number between 1 and X. This is very
useful to determine who's going to have the right to loot a chest or harvest
a resource. Using /random will send the text to your party or raid wherever they are instead of the local area around
the player that used /random.<BR>
<B>/r</b> - Reply to the last tell/private message. You can scroll through
all the people that whispered you recently by typing on the tab key.
<BR>
<B>/say &lt;message&gt;</B> (alias '/s') - Send a message to people near you.<BR>
<B>/sit</B> - When standing, your character will sit. Your character regains health more quickly when sitting. <BR>

<B>/stand</B> - When sitting, your character will stand. <BR>
<B>/who</B> - Lists the players online.<BR>
<B>/yell &lt;message&gt;</B> (alias '/y') - Yells a message to the area around. Many players consider this to be "spam" so use it
sparingly.<BR>
<P>

<B>/cast</B> allows you to cast spells by name. Type /cast (spell subtext) - Example: "<B>/cast Fireball (Rank 1)</B>". To add spell casting to a macro you can type it manually or shift-click a spell in your spell book to add the proper /cast line to the macro.

<P>

<B>Chat Commands</B>
<ul>
<B>/#, /c, /csay</B> - Send text to channel # (e.g. /1 Hi!).<BR>
<B>/announcements, /ann</B> - Toggle join/leave announcements on a channel.<BR>
<B>/afk, /dnd</B> - Set your Away From Keyboard or Do Not Disturb flags.<BR>

<b>/ban, /unban</B> - Ban/unban a player from a channel.<BR>
<B>/chatlist, /chatwho, /chatinfo [channel]</B> - List channels, or channel members.<BR>
<B>/cinvite, /chatinvite</B> - Invite a player to a channel.<BR>
<B>/join, /channel, /chan</B> - Join a channel.<BR>
<B>/kick</B> - Kick a player off a channel.<BR>

<B>/leave, /chatleave, /chatexit [channel]</B> - Leave a channel (or all channels).<BR>
<B>/mod, /moderator, /unmod, /unmoderator</B> - Change a player's moderator status.<BR>
<B>/moderate</B> - Toggle moderation on a channel.<BR>
<B>/mute, /squelch, /unvoice, /unmute, /unsquelch, /voice</B> - Change a player's permission.<BR>
<B>/password, /pass &lt;channel&gt; &lt;password&gt;</B> - Change password.<BR>

</ul>

<B>Guild Commands</B><BR>
Check out our <a href="guilds.php">guild page</a> for guild commands.
<P>

<B>Voice (Sounds)</B><BR>
The following will play a sound! There are two to three variations for each one.
The voices will be different depending on your character.
<ul>
<B>/help</B>.<BR>

<B>/inc</B> - Incoming. You type this to warn your party that a monster is coming to attack.<BR>
<B>/charge</B>.<BR>
<B>/flee</B>.<BR>
<B>/attack</B>.<BR>
<B>/oom</B> - Out of mana, low on mana.<BR>

<B>/fol</B> - Follow Me.<BR>
<B>/wait</B>.<BR>
<B>/heal</B>.<BR>
<B>/cheer</B>.<BR>
<B>/fire</B>.<BR>
<B>/rasp</B> - Sticking your tongue out sound.<BR>

</ul>
