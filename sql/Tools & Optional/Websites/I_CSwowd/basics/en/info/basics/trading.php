<img src="../../../shared/wow-com/images/basics/trading/beersign.gif" align="right"><img src="../../../shared/wow-com/images/smallcaps/plain/t.gif" align=left>rading is a great way to find those hard to find items you're looking for. Some items cannot be easily found on your own. You can try to do without them but why not use other players to find these items for you? Many people have the goal of finding the best possible equipment for their character. While playing the game a lot can help you do this, trading is often much easier and faster if you have the money.
<P>
<B>Trading With Another Player</B><BR>
<img src="../../../shared/wow-com/images/basics/trading/talking.jpg" align=right>Select a character, and then right-click on its portrait/name. You can also do this for player
portraits. This will launch the trade screen with another player. Place your items in
the top portion of the screen. Once you are satisfied with the other player's trade, hit "Trade" button.
<P>
To trade money, open up your backpack and hold down shift while clicking on the money amount. You can then select
the amount of money and drag it over to the trade window.
<P>
You can also drag an item or money from your bags and drop it on another player to initate a trade window.
<P>
Make sure the other player gives the correct type of coin in the trade.
<P>
<B>Trading Screen + Trade Skills</B><BR>
Some trade skills can use the trade screen to manipulate another player's items. A locksmith can open locked boxes
placed in the trade screen. Enchanters can also enchant items that are in the trade screen.Place these items
in the box at the bottom, which is for items that will be modified by other players but not traded
<P>
<B>Linking Item Stats</B><BR>
The most important thing to learn about item trading is linking item stats. Open up the chat prompt (hit enter)
and then hold down the shift key while left-clicking on the item. Note: this will not work if the item is
in the bank or if the chat prompt is not open. You can use these links in most messages, including say,
private messages, guild chat, and trade channels.
<P>
Trade links will only work in the trade channels. They will not work in other general channels.
<P>
<B>Trading Channels</B><BR>
There are public trading channels located in the big cities. You can only use these channels while in the cities.
<P>
<B>Find Out Who Uses the Item</B><BR>
Know which classes use which type of equipment:
<ul>
Mail Armor: Warrior, Paladin<BR>
Leather Armor: Druid, Rogue, Shaman, Hunter<BR>
Cloth Armor: Mage, Warlock, Priest<BR>
Weapons: some items can only be used by certain classes
</ul>
<B>Trade Skill Components</B><BR>
Players with trade skills will pay money for stacks of normal and rare components, such as medium hide, 20 light leather, or a lesser moonstone.
Ask around before selling these types of items to the merchants.
<P>
<B>Making Deals</B><BR>
The goal of a seller is to get the best item or the most money for their item. The
goal of a buyer is to try to get deals on items, and to get them as cheaply as
possible. Prices are not fixed, so haggle. If the person won't go for the deal,
chances are someone else will. If the person has a very valuable and rare item,
you might have to give in to the seller's price if you really want to get the item.
If you're having trouble selling your item, lower the price. Find out what an item is worth.
Figure out how rare it is and what people will pay for it. Look around and see what
people are offering and are paying for items. Then come up with a price or offer a
similar price. You can watch the sales of other similar items to find out what your
item is worth. Item prices will change depending on the time of day, which determines
how many players are on. There are also many other factors that can make an item more
or less valuable.
<P>
<B>How bad do you want the item?</B><BR>
That is a good question. Be sure you don't let the other person know how bad you want
the item; otherwise they will have second thoughts about trading it. They will think
"If this person wants it this bad, how much will someone else pay?"
If you really desire the item, you might want to cave in and pay whatever it takes to get it.
But if you really don't need the item, pass up on an overpriced item. "I'll trade you everything
I have" generally is not a good thing to say.
<P>
<B>20 "bad" items don't make a good item</B><BR>
A common trading mistake is to assume people are willing to trade one good item for a lot of
lesser items. This goes against the common selling strategy of getting the most you can for an item.
Many people
are looking to unload their items for valuable items or items they can use. When
space is limited, they are often not looking for more items in return that they will have to unload.
You can, however trade a few good items for a better item if the other person likes
all the items you're offering.
<P>
<B>Trading Reputation</B><BR>
Your trading reputation is very important. If you cheat someone, you can really ruin
your reputation in the future if the cheated person spreads the word about you. This
can be even easier due to guilds and guild chat. Don't risk creating problems for
yourself in the future. Be honest in your dealings and you will be rewarded.
<P>
If a person somehow messes up and is unable to use the item, don't be
afraid to allow them to return the item. This is of course optional, but it
will earn you bonus points in the marketplace.Furthermore, since this is a
rare situation it should not affect your bottom line much.
<P>
<img src="../../../shared/wow-com/images/basics/trading/auctionhouse.gif" align=right><B>Auction Houses</B><BR>
If you want to keep things simple and don't want to have to haggle or bargain with other players, try the
<a href="auctionhouses.php">Auction House</a>. It's simple to use! Just search for the item you want and place a bid.
Learn more <a href="auctionhouses.php">here</a>.
