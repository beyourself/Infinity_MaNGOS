<b>Current Flag Status</b><br>
Players can view the current score of the match by looking at the top middle of their screen. When one side captures three enemy flags, they win and the match ends.
<p>
In addition, a blinking red flag appears next to the Alliance score when the Horde flag has been taken from its base, and a blinking blue flag appears next to the Horde score when the Alliance flag has been taken from its base.
<p>
<B>Rank Icon</B><BR>
This icon shows the player-vs.-player Honor Rank of that character. <a href="../../../pvp/pvp-article-part2.html#ranktable">Click here</a> to see a list of ranks for both Horde and Alliance.
<P>
<B>Name</B><BR>
This lists all the players currently participating in that Battleground instance. Alliance players' names are listed in blue, and Horde players' names are listed in red. Your name is listed as yellow.
<P>
<B>Killing Blows</B><BR>
This lists the number of times a player delivered a killing blow to an enemy inside the Battleground.
<P>
<B>Deaths</B><BR>
This shows the number of times a player has died in the Battleground.
<P>
<B>Honorable Kills</B><BR>
This lists the number of enemies that player, or his/her group, participated in defeating in that Battleground.
<P>
<B>Flag Captures</B><BR>
This lists the number of times a player has successfully captured the opposing faction's flag by safely bringing the flag to his/her base. The match is won when one of the factions have captured the opposing team's flag three times.
<P>
<B>Flag Returns</B><BR>
This lists the number of times a player has returned his/her faction's flag back to their base after it was taken by a member of the opposing faction. Player can return the flag by right-clicking on it after it has been dropped by an enemy.
<P>
<B>Bonus Honor</B><BR>
This lists the amount of bonus Honor awarded to each team. Bonus Honor is awarded to each team every time they capture the opposing team's flag.
