Here are some of the terms which may cause some confusion. This is not a complete list of words that you may hear.
There are many other common terms used on the internet which do not directly relate to the game.
<P>
<B>AC</B> - Armor Class or Defense.<BR>
<B>Add</B> - An extra monster has joined an existing battle.<BR>
<B>Alt</B> - A character on your account other than your main character.<BR>
<B>AOE</B> - Area of Effect Damage. This is a spell that hurts a group of monsters in an area like Blizzard in Warcraft III.<BR>
<B>AE</B> - Area Effect<BR>
<B>AFK</B> - Away from Keyboard. This means the player is away from his computer. <BR>
<B>Aggro</B> - This means the monsters are mad at you and you've "activated" them to attack you.
They are now in the motion of trying to reach and attack you. "The Murloc aggroed on me!" or "The Murlocs will
aggro if you get too close"<BR>
<B>Aggro Radius</B> - The radius around the monsters where they will "wake up" and attack you.<BR>
<B>AGI</B> - Agility<BR>
<B>Avatar</B> - Your character<BR>
<B>Bind</B> - Determines where you will respawn when you have died. "To Bind" means to set your bind point.<BR>
<B>Buff</B> - A beneficial spell cast on a monster or player. An example of a "buff" is the Priest's Inner Fire or Shaman's Bloodlust.<BR>
<B>Carebear</B> - Player that prefers to help other players attack monsters rather than attack other players in player vs. player combat.<BR>
<B>Caster</B> - A character that stays on the back row to heal or cast spells on the enemy, such as a Mage.<BR>
<B>Cheese</B> - To exploit an imbalance in the game.<BR>
<B>Creep</B> - Monster<BR>
<B>Critters</B> - Monsters that don't attack back, like a bunny or deer.<Br>
<B>DD</B> - Direct Damage. This is a spell that does all of its damage in one hit rather than spreading its damage over time. <BR>
<B>DMG</B> - Damage<BR>
<B>DOT</B> - Damage over time<BR>
<B>DPS</B> - Damage per second<BR>
<B>De-Buff</B> - A negative spell cast on a unit that makes it less powerful. An example of a "de-buff" is Slow.<BR>
<B>FH</B> - Full Health<BR>
<B>FM</B> - Full Mana<BR>
<B>GM</B> - Game Master. Someone employed by Blizzard Entertainment to assist and help players.<BR>
<B>Griefer</B> - A person who purposely tries to annoy or anger other players.<BR>
<B>GS</B> - Goldshire<BR>
<B>HP</B> - Hit points or Health<BR>
<B>IF</B> - Ironforge<BR>
<B>Incoming (INC)</B> - This means an attack is coming.<BR>
<B>Instancing</B> - This is a dungeon where you will load into your OWN copy of the dungeon with your group. Only you and your group will be in your copy of the dungeon. Another group that enters the same area will enter their own copy of the dungeon.<BR>
<B>INT</B> - Intelligence<BR>
<B>Kiting</B> - A style of combat in which a player continually stays out of combat range of an enemy usually by running from it, while simultaneously causing damage to it.<BR>
<B>KOS</B> - Killed on Sight. If an Orc approaches a Human Guard, the Guard would try to kill on sight (aggro).<BR>
<B>KS</b> - Kill Steal. Try to steal another person's kill.<BR>
<B>LFG</B> - Looking for a group.<BR>
<B>Log</B> - When you log off; disconnect from the game.<BR>
<B>LOL</B> - Lots of Laugh or Laughing Out Loud. This abbreviation indicates something is really funny. Unfortunately although very common, that term became uncool a long long long time ago. Other alternatives that you can use instead are "hehehehe" or "hahahaha" which aren't the best but are more hip. ROFL should also be avoided to appear cool.<BR>
<B>LOM</B> - Low on Mana.<BR>
<B>LOS</B> - Line of Sight<BR>
<B>LOOT</B> - To take the treasure from a monster that has been killed or from a chest.<BR>
<B>LVL</B> - Level<BR>
<B>Mez</B> - Short for Mesmerize. Refers to spells, such as Sleep, that temporarily incapacitate a target.<BR>
<B>MMO</B> Massively Multiplayer Online.<BR>
<B>MMOG</B> - Massively Multiplayer Online Game<BR>
<B>MMORPG</B> - Massive Multiplayer Online Role Playing Game<BR>
<B>MOB</B> - Abbreviation of the word "Mobile". Mobs are all computer controlled characters in the game. You should likely use some other
term such as monster, creep, bad guy.<BR>
<B>Newbie</B> - A term that sometimes means new player but also is used to mean a player is not very good. <BR>
<B>Newb</B> - Short for Newbie. See above. <BR>
<B>N00b</B> - An incredibly uncool way to say newb. Don't use it. <BR>
<B>NPC</B> - This is a non player-controlled character. The characters are controlled by the server or realm. A "computer" character. <BR>
<B>OOM</B> - Out of Mana. People say this to let their party know they are out of mana and can't cast any spells, especially healing.<BR>
<B>PC</B> - Player controlled character<BR>
<B>Pet</B> - A NPC controlled by a player such as a Wolf, Infernal, and so on. <BR>
<B>PK</B> - Player Kill or Player Killer.<BR>
<B>POP</B> - Often used shorted term for re-spawn of monsters.<BR>
<B>PST</B> - Please Send Tell. Indicates that the person speaking wants to receive communication via a /t(ell) or /w(hisper) command.<BR>
<B>Puller</B> - Person who pulls monsters for the party.<BR>
<B>Pulling</B> - One of the players in a party heads out and leads one or more of the monsters back to the party so that they can attack them. The idea is to prevent too many monsters from attacking at once.<BR>
<B>PvE</B> - Player Vs. Environment. Combat between players and computer controlled opponents.<BR>
<B>PvP</B> - Player vs. Player. This means for one player to attack another player.<BR>
<B>Raid</B> - A raid is a large-scale attack on an area by a group of players.<BR>
<B>Res</B> - Short for resurrect<BR>
<B>Re-Spawn</B> - A monster that has been killed has spawned (been created) again.<BR>
<B>Rest (state)</B> - An indicator of how tired a character is, which affects how much experience is gained from killing monsters.<Br>
<B>RR</B> - Redridge Mountains<BR>
<B>Spawns</B> - The location or process of monsters appearing when they are created in the world.<BR>
<B>SPI</B> - Spirit<BR>
<B>STA</B> - Stamina<BR>
<B>Stack</B> - The amount of identical items which stack into one inventory slot.<BR>
<B>STR</B> - Strength<BR>
<B>SW</B> - Stormwind<BR>
<B>Tank</B> - A melee character that can take a lot of damage like a Warrior.<BR>
<B>Taunt</B> - Related to Aggro. An ability that allows a player to pull the attention of a monster off of another player and onto him- or herself.<BR>
<B>Threat</B> - Related to Aggro. This is what a character "gives off" to generate or draw monster aggro.<BR>
<B>Train</B> - To lead monsters into another player. This is not a desired behavior. <BR>
<B>Twink</B> - A low level character who has been made more powerful by higher level characters,
usually by getting stronger armor and weapons than the character would normally have at such a
low level.<BR>
<B>Uber</B> - German slang for 'super', originally meaning 'above' ; exceptionally powerful<BR>
<B>WF</B> - Westfall<BR>
<B>Vendor Trash</B> - An item that only a vendor/merchant would buy<BR>
<B>WoW</B> - World of Warcraft<BR>
<B>WTB</B> - Wanting to buy<BR>
<B>WTS</B> - Wanting to sell<BR>
<B>XP or Exp</B> - Experience Points
