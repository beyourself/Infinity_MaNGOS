<P>
<img src="../../../shared/wow-com/images/basics/npcs/directions.gif" align="right"><B>Ask for Directions!</B><BR>
Some NPCs can give you directions in the big cities. For example, walk up to a Stormwind guard that has
a speech bubble. Talk to that guard and they will offer to tell you how to get to various places. Once
you select the location they will place a special arrow on your minimap in the top right showing you where
to go!
<P>
<B>Guard Defense System</B><BR>
The guard system enables NPCs to call for guards when they see an enemy as well as when they are attacked. The guards will chase players and then return to the NPC that called for help once they lose track of, or kill the player.
<P>
If a guard is killed, another guard will be called to take its place. There is a limit to how many guards each town can have which will recharge over time.
<P>
Guard posts will send existing guards before creating new ones.
