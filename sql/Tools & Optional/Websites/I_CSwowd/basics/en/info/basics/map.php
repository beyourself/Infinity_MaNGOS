<B>Exploring the Map</B><p>
<img src="../../../shared/wow-com/images/smallcaps/plain/w.gif" align=left>orld of Warcraft features a map exploration system. Players start out with a <a href="../../../shared/wow-com/images/basics/map/ss0002.jpg">blank map</a> with some very basic features.
As they explore the world, they unlock portions of the map and also gain experience. This rewards players for exploring and gives
players access to maps for future reference.
<P>
<B>World Map</B><BR>
<table border=0 cellpadding=0 cellspacing=0 align=right><tr><td align=center><a href="../../../shared/wow-com/images/basics/map/ss0001.jpg"><img src="../../../shared/wow-com/images/basics/map/ss0001-thumb.jpg" border=0 title="Yarr...dere be dragons!"></a></td></tr></table>You can access the World Map by pressing "m" on the keyboard. There are three levels of zoom:
<UL>
<table border=0 cellpadding=0 cellspacing=0>
<tr><td valign=top><img src="../../../shared/wow-com/images/parchment/plain/light/bulletblack-small.gif"></td>
<td valign=top><span><a href="../../../shared/wow-com/images/basics/map/ss0003.jpg">Region/Zone View</a></span></td></tr>
<tr><td>
<tr><td valign=top><img src="../../../shared/wow-com/images/parchment/plain/light/bulletblack-small.gif"></td><td valign=top><span><a href="../../../shared/wow-com/images/basics/map/ss0004.jpg">Continent View</a> (Eastern Kingdoms or Kalimdor)</span></td></tr>
<tr><td valign=top><img src="../../../shared/wow-com/images/parchment/plain/light/bulletblack-small.gif"></td><td valign=top><span><a href="../../../shared/wow-com/images/basics/map/ss0001.jpg">World View</a> (Entire World)</span></td></tr>
</table>
</UL>
You can zoom out via right-clicking or by pressing the zoom button. You can zoom back in by selecting the area of the map, you
wish to visit and left-clicking.
<P>
<B>Unlocking the Map</B><BR>
To unlock an area of the map you need to travel to a specific point of interest in the zone. Once you've entered
the area of the point of interest, you will <a href="../../../shared/wow-com/images/basics/map/ss0005.jpg">receive an experience reward</a> and a message telling you that you've
discovered the area. You receive more
experience for discovering higher level areas. As you level up you will receive reduced experience for exploring an area. For
example, if you're level 20 and discover an area in Elwynn, you will receive a very small amount of experience. On the other hand,
if you discover an area in the Wetlands, you would get much more experience.
<P>
The map is not revealed by traveling to a blank portion of the map, as is done in real-time strategy games like
Warcraft III.
Instead, you must discover a key area in the region/zone to unlock that portion of the map.
<P>
You get no experience for flying over an area on an air transport such as a Gryphon. You also do not reveal areas when you
explore them as a ghost.
