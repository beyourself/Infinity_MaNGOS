<img src="../../../shared/wow-com/images/basics/duels/backtoback.jpg" align="right"><B>"I challenge you to a duel!"</b><BR>
<img src="../../../shared/wow-com/images/smallcaps/plain/d.gif" align="left">uels are a great way to prove who is the better warrior. Perhaps another
player has offended you. Challenge them to a duel! "/slap playername". You can have a
duel (one-on-one fight) with another player of either the Alliance or the Horde.
<P>
To start a duel, select the player you wish to duel. Right-click on the player's portrait and
select "duel". Alternatively, you can also select someone and type "/duel" with that player
selected or type "/duel playername".
<P>
A battle flag will be placed near you indicating that a challenge has been issued and a duel
might begin. The person you challenged must accept the duel or you will not be able to attack them.
Watch the color of their character's name. It will turn red when the duel has begun. A sound will
also play when a duel begins.
<P>
If you try to run too far away from the other player after starting a duel a special box will
pop up letting you know you're about to exit the dueling area: "Exiting duel area, you will
forfeit in X seconds."If you forfeit, a message of your cowardice will be broadcast to the
region. Example: <B>Player 1 has fled from Player 2 in a duel.</B>
<P>
The result of the duel is announced to the current region (zone). Example: <B>Player 1 has defeated Player
2 in a duel.</B>
<P>
<B>Additional Information</B><BR>
<ul>
<li>You can't duel in certain areas.
<li>You can't duel players you can attack normally.
<li>Skills will not increase from use while dueling or engaged in PvP.
<li>Dueling players can cast helpful spells on their allies (but not vice versa).
</ul>
<B>Tips</B><BR>
<UL>
<LI>Some players will move in circles (strafing) as you fight, especially Rogues. You'll have to learn
how to run and rotate to combat this technique.
<LI>Duels are helpful to test new spells and item effects.
<LI>Some players consider it cheating to use potions during a duel. You might want to ask before the challenge
what the "rules" are.
<LI>During a duel you shouldn't get any help from allies such as healing. Buffs are debatable.
</UL>
<B>Common Questions</B><BR>
<ul>
<B>What do I win if I win a duel?</B><BR>
Your reward will be a message broadcast to the entire region (zone) announcing your win. The loser also kneels before you.
<P>
<B>Do I lose anything if I lose a duel?</B><BR>
There is currently no penalty, so you can duel all you want.
<P>
<B>Will I die if I duel?</B><BR>
Not usually, but it happens sometimes.
</ul>
