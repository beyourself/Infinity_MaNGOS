<div class = "postBody">
<B>Health</B><BR>
If you run out of health, your character will die. Health is the green bar in the top-left corner. If you move and hold
your cursor over the health bar you can see exactly how much health you have. You can increase this number by leveling up,
by increasing your stamina, or by getting items that add to health. You can restore your health through healing spells,
health potions, and other methods. After battle you can heal via food or by just sitting down. Your health will also
regenerate while you are in combat.
<P>
Save or buy up healing potions to use in emergency situations to avoid death. You can place a health potion on
your action bar for faster usage.
<P>
<B>Combat Times</B><BR>
Combat times vary depending on your level, the monster's level, class, equipment, spell usage and more. One common
thing you will notice is that low level monsters are quick to kill while monsters that are much higher level will
take much longer. This is because monsters that are higher level will have a greater chance to resist your spells and
dodge your attacks. Elite monsters (level+) will take even longer to kill because they have increased hit points.
While World of Warcraft has a real-time combat system, weapon users must wait their turn to swing
again based on the delay or speed of their weapon. A spell user must wait for the length of the spell's
cooldown until they can cast a spell again.
<P>
<B>Stuns</B><BR>
If you are stunned during battle, you cannot do anything. You are frozen and just have to sit there until the stun has worn off. If you place
your cursor over the stun icon in the top right you can see how long it is going to last.
<P>
<B>Fear</B><BR>
Some monsters will cast fear type spells on you. These abilities cause you to lose total control over your character.
The game (or the monster) will gain control over your character. Your character may run off in a random direction
(your name will turn red). You simply have to wait until this spell effect has worn off. If you place your mouse cursor over the icon for the
fear-like ability, you can see how long it will last. It's also common and expected that you inform your party members that you've been feared.
For example, type: "Feared."
<P>
Players who are feared cannot use items, cast spells, or use abilities until fear wears off.
<P>
<B>Charm</B><BR>
Some monsters cast Charm spells which allow them to take control over your character. You will become hostile and may attack
your own party members. Other party members can also target and attack you. Inform your party members that you are
"Charmed!"
<P>
<B>Possession</B><BR>
Possessed units (e.g. Mind Control, Eyes of the Beast etc...) can cancel combat mode by clearing your current target. In addition, the UI has a gold border when the possessed unit is actively in combat mode with the target.
<P>
<B>Poison</B><BR>
Many monsters can cast poison on your character. Enemy rogue type monsters can also use poisoned blades which cast a poison DoT (Damage over Time)
on you. You will realize this ability is on you by a green animation on your character. You will need to cure yourself with a spell that some classes
have, wait for it to wear off on its own, or use a potion that can cure poison.
<P>
<B>Curses</B><BR>
Some monsters have the ability to curse your character with various curses (spells) that can do all kinds of interesting things. Curses can last a
very long time. Your defense against curses is to have a party member that can cure curses or to find some type of potion or item that could cure
a curse.
<P>
<B>Disease</B><BR>
Disease is another negative spell that monsters or players (in PvP) can cast on your characters. Some of these diseases can actually spread to
other players. Diseases can last a very long time. You can check their length by placing your cursor over the disease icon. To remove the
disease you will need to find a player that can cure disease or find an item or potion to do it yourself.
<P>
<B>Immune</B><BR>
If you cast a spell on a monster and it says immune, this means the monster cannot be harmed by whatever ability you are casting on it.
You should stop casting that ability on the monster immediately because it is not working. It is possible that some immunity is temporary.
For instance, some monsters have the ability to cast a temporary anti-magic shield on themselves. You'll have to figure out if the immunity is
temporary or permanent. Look at what buffs are cast on the monster.
<P>
<B>Disarm</B><BR>
Some monsters can disarm your character. You will begin fighting hand-to-hand which you might notice will increase your
unarmed skill. Disarm will only disarm weapons and will select the weapon in the off-hand if there is no weapon in
the main hand slot.
<P>
<B>Monster "Resists" your Spell</B><BR>
There is nothing you can do against a monster resisting your spell except to try casting your spell again until it works.
Monster resists typically happen when the monster is higher level than you.
Additionally you can reduce monster resists by leveling higher than the monsters.
Monster resists are not the same thing as "resistances."
<P>
<B>Running Away</B><BR>
If you need to get away, use any ability that you have on the monster that will slow them down, allowing you to escape. An example is the
Mage's Frost Nova.
<P>
<B>Monster Flee</B><BR>
Humanoid and certain other types of monsters run when they are about to die. Often they run for help. You need to find some ability to stop
them before they are able to draw other monsters into the fight. You can use a direct damage (dd) spell, an ability that slows or stuns the
enemy, or an ability that holds a monster in place so that you can fight them. Otherwise you might have to fight the fleeing monster's friends!
<P>
Some monsters will call for help when they are about to die.
<P>
<B>Monster Spell casting</B><BR>
If a monster starts casting a spell such as Healing, you need to use an ability on them that interrupts its spell casting.
An example of this is the Paladin's Fist of Justice. There are other abilities that can do the same, but you need to have them
ready to use at the right moment.
<P>
You can sometimes tell what spell the monster is casting based on its casting animation and color and
game playing experience.
<P>
<B>Spell Interruption</B><BR>
As with monsters, player spells can be interrupted. However, monsters can interrupt player spell casting simply by making an attack that
does damage rather than using special spell interruption abilities. If you are attacked while casting a spell, some extra time will be
added to the casting progress bar. Realize that as you're trying to cast a spell you are not damaging the enemy.
They are getting free hits on you. So you need to make the decision as to whether it's worth it to try to
continue to cast the spell or if you'll take so many hits from the monster that it's not worth trying.If many
monsters are attacking at the same time making continuous hits, it's not worth it to try casting a spell.
<P>
<B>Controls</B><BR>
Learn everything you can about controls so that you can improve your play. Don't forget that you can cast spells on yourself easily by
holding down alt and pressing the hot key for the spell.
<P>
<B>Using Spells or Abilities While In Combat</B><BR>
While you're in combat, some abilities or spells will not work. If this is the case, a spell will mention it on its description or
inform you via an error message.
<P>
<B>Melee Range</B><BR>
If you wish to fight in melee combat, make sure you are in range to hit the monster. Sometimes it can be hard to tell if you're out of
range or not but look for red warning messages informing you that you're out of range.
<P>
<B>Spell Range</B><BR>
Try to cast spells on monsters to initiate combat at the maximum spell range. Move far away from a monster, then move closer while
continuing to click the spell icon. Once you get within range you will be able to cast. This space will allow you to cast another
spell right after the first one because the monster will still be running to attack you.
<P>
<B>Weapon Switching</B><BR>
Switching weapons in combat triggers a 1 second global cooldown for all abilities for Rogues and a 1.5 second global cooldown for everyone else.
<P>
<B>Damage Absorption</B><BR>
Damage absorption does not protect against falling, drowning, or fatigue damage.
<P>
<B>Facing the Right Direction</B><BR>
Some spells or abilities require you to be facing the enemy ahead or from behind. If you are not in the proper position you will receive a
warning message. You should reposition your character.
<P>
<B>Combat Log</B><BR>
Your combat log can be very helpful to inform you how you are doing and what is happening during combat,
and to test changes to combat via spells or item changes. Additionally, there are interface mods made by the fan community which can provide you with combat analysis.
<P>
<B>Number of Monsters</B><BR>
It's best to fight one monster at a time whenever possible as this will be the least amount of risk. For more information on how to pull
only one monster read the <a href="partiespulling.php">Parties: Pulling page</a>.
<P>
<B>Situational Awareness - Camera View</B><BR>
As you are fighting and moving around the world, rotate the camera around to check if you're about to run into monsters or if monsters are
coming toward you.
Players that don't pay attention are likely to run into unexpected monsters more often.
If there are two or more players
fighting the same monster it's often good to fight face to face on either side of the monster so that each player can watch out for
additional monsters and warn the other player. If a monster is coming towards you and might join in the fight, lead the current monster
you're fighting away to a safe spot to fight.You can do this by identifying the player
that has currently attracted the attention (hate/aggro) of the monster. That player then needs to back away or move to a
safe spot to fight while the monster follows them. Players with poor situational awareness typically die a lot.
<P>
<B>Optimize Combat</B><BR>
Spend a lot of time practicing to optimize your combat. You should try to learn to do everything as perfectly as possible during combat
so that you (a) win the battle and (b) end the battle in the best shape with health and mana. By perfecting your play (PP) you will
become a better player, have an easier time, and sometimes gain respect from other players due to your abilities.
<P>
Keys to perfecting your play:
<UL>
<LI>Organize your action bar very well. Put your most used abilities on page one. Put abilities you use less often on page two. Learn to switch quickly between bars during combat (using shift mouse wheel or shift 2). Learn to use the hot keys for your action bar to cast spells more quickly.
<LI>Learn all of your abilities. Learn each ability so that you will know what it is used for and when to use it. If you need help, talk to more experienced players of your class or read class strategy guides.
<LI>Get your abilities and spells as soon as you can. A common thing to hear is, "oh I haven't got that ability/spell yet."
There are good reasons for this such as not having enough money for spells or you need help on a quest. However, laziness shouldn't
be the cause of you not having researched an ability. Some abilities such as healing, resurrection, buffs, and cures are very important to a party.
</UL>
<B>Damage over Time Spells (DoTs) </B><BR>
After casting a DoT on an enemy, watch the icons by the enemy's title bar to see when that DoT wears off. Then use the hot key and cast it
immediately again. Using this method, you can have your DoT active for most of the battle.
<P>
<B>Be active during combat</B><BR>
A common mistake is to start a battle and just sit back and watch. You should be using your abilities as soon as they are available. There
should be very little time between when the ability becomes ready and when you use it. You will find that making little changes and being
on top of things will significantly improve your effectiveness in combat.
<P>
<B>Other Information</B><BR>
<UL>
<LI>You cannot eat or drink while in combat. You are now considered "in-combat" if any nearby monster is engaged with you.
</UL>
