<h3 class="guidetitle">Player vs. Player Combat</h3>
You have equipped yourself and are at the maximum level. Now it's time to use your experience to defeat other players. Discover Player vs. Player combat! There are <a href="battlegrounds/index-2.html">three Battlegrounds</a>, the Gurubashi Arena Booty Run, and a special arena in Dire Maul ripe with competition. You can also compete on the <a href="../../pvp/pvp-article-part2.php">Honor System Rankings</a> to be #1 on the realm for your faction.
<P>

<h3 class="guidetitle">Player vs. Player Rewards</h3>
While participating in Player vs. Player combat, you can gain higher <a href="../../pvp/pvp-article-part2.php#ranktable">PvP ranks</a>. As you increase in rank you can purchase better <a href="../../pvp/rewards-weapons.php">weapons</a>, <a href="../../pvp/rewards-armor.php">armor</a>, and even <a href="../../pvp/rewards-mounts.php">mounts</a>. Officers also have access to special Officer barracks that contain vendors with special rewards.
<P>
