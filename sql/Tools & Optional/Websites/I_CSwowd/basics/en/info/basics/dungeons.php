<img src="../../../shared/wow-com/images/smallcaps/plain/p.gif" align="left">layers will come across two types of dungeons while playing World of Warcraft: micro dungeons and world dungeons. There are more than a hundred micro dungeons throughout the world of Azeroth, ranging in relative size from small to quite big. Micro-dungeon locations will include tombs, haunted mines, ice caves, and sunken ships to name a few. The transition into and out of these dungeons will be seamless, and you'll be able to run into other players anywhere along the way.
<P>
<b>World dungeons</b>, such as Medivh's Tower, the Westfall Deadmines, and the Scarlet Monastery, are grander in scale than micro dungeons and are specifically designed for more epic encounters. All these locations have common areas where players can meet up and fight together. But, deeper in the dungeons are areas set up specifically for more private group (or sometimes guild) adventures. These areas, called "instanced zones," allow you and a group of friends to have a more personal experience, exploring, adventuring, or completing quests in your own private dungeon. You also have the ability to invite others into your instanced zone to join you. This relieves many of the camping, kill-stealing, and farming issues that MMORPGs sometimes face. The monsters in instanced zones are typically more powerful, so groups of players will have to work together to defeat them, but with greater dangers come greater rewards!
