<div class = "postBody">
Race War Realms allow players of the Horde and Alliance to fight each other in addition to the other enemy monsters in the world. Some players
prefer the challenge of facing other real players rather than playing against computer-controlled monsters/characters.
Common reasons for this include: more strategy, excitement, the thrill of defeating a real player and imagining their reaction when defeated.
Race War Realms aren't for everyone so be aware of what you're getting into before joining. You must be prepared to put up with high level
players trying to attack you when you least expect it. You must also be prepared to fail a quest because of enemy interference. There will
be quests that are especially difficult because they take place in enemy territory. You'll have to dodge
and/or defeat enemy players to complete your quest.
<P>
Make sure you learn about the basic rules, which is a list of rules for play on this special Realm type.
<P>
<font color=red>The Horde vs. Alliance race war Realm will establish a straightforward method for the development team to test and balance the races and classes in a PvP environment. PvP concepts will be gradually introduced on this Realm. This means that the initial implementation of PvP is not how our final model will work once World of Warcraft is released.</font>
<P>
<h3 class="title">Offense</h3>
<P>
<B>Large Raids</B><BR>
Raids on enemy territories require coordination and effort to be successful.
<ul>
<B>Level</B><BR>
Character Level is very important. Try to bring as many high level players as you can.
<P>
<B>Bring a Large Number of Players</B><BR>
Numbers are very important when attacking enemy territories. Try to bring as many people as you can.
The enemy can easily call in reinforcements via chat channels because they are closer to
their homelands. Additionally, people attacking enemy territory
often become scared and run, which brings down your numbers.Make alliances with other guilds so that
you can bring more players. If an attack fails, be sure to bring more players next time!
<P>
<B>Create a Chat Channel</B><BR>
Set up a Chat channel to communicate. For best results, only allow the leaders to communicate
and ask the rest of the players to be silent. Otherwise,
the chat channel can turn into anarchy as non-leaders may give poor commands and feedback which can damage the success of the raid.
<P>
<B>Stay Together</B><BR>
Keep together. You're stronger in large numbers. Don't leave people behind.
If your party begins to get separated, be sure to wait and re-group, so your attack will be more successful.
<P>
<B>Pick a Good Path to Follow</B><BR>
Use strategy to pick the best path to the enemy base. Sometimes it's best to stay off the road and normal paths. That way you're less
likely to be spotted before you spring your attack.
<P>
<B>Groups</B><BR>
It is up to you to decide just how involved your attack will be. Experienced raids involve grouping people up by specific classes.
Each group has a specific purpose and responsibility in the attack. Perhaps you want a Priest in
every group. Perhaps you would like to organize groups entirely of specific classes such as Rogues or Mages.
<P>
<B>Surprise</B><BR>
Surprise is very important. You want to attack when the enemy is not expecting it. Try to do as much damage as you can (killing NPCs
and enemy players) before the enemy can react. For these reasons, it's important to be secret about your plans. This can be very difficult
when you bring a lot of players.
<P>
<B>Fall Back/Retreat!</B><BR>
If you are having trouble in an enemy town, fall back.
When you fight in enemy towns you not only have to deal with other players, but also the town guards. If you retreat you'll only have to fight the enemy players.
</ul>
<B>Small Raids</B><BR>
Small raids can be very effective with a high level, organized, and experienced party. These raids can cause confusion with the enemy who
might believe you're attacking in much larger numbers. The key to such attacks is surprise and lots of running away.
Hit-and-run attacks with a small party can create a great amount of chaos.
<P>
<B>Death</B><BR>
Currently, if you die in enemy territory, you respawn at the nearest graveyard whether it is
alliance or horde controlled. Also, be aware that ghosts of each side can see each other.
<P>
<B>Enemy Regions vs. Friendly/Contested</B><BR>
The enemy has the advantage in an enemy (red) region. They can have spies following you around that you're unable to attack.
They are able to strike first and you will be unable to fight them until they have first attacked you.
<P>
<B>Bait</B><BR>
A good trick for killing lower level enemies in their own territory, who would
normally not engage you in combat, is to bring a low level friend as bait.
Make sure the enemy sees your friend and not you, and they will most likely
engage in combat with him, enabling you to attack them.
<P>
<B>Other Tips</B>
<UL>
<LI>Have your Hearthstone ready. You'll likely need your Hearthstone to get away after a battle is over. If your attack fails you will need to sneak away and the Hearthstone is very useful for that task.
<P>
<LI>Bring Healing Potions.
Have friendly players cast as many buffs on you and your party as they can in order to make
your character more powerful.
</UL>
<h3 class="title">Defense</h3>
<P>
<B>Defend!</B><BR>
Defend the alliance! Perhaps there are others that can take up this challenge or perhaps you will
be called on to defend the land. Here are some tips:
<ul>
<B>Find out more information</B><BR>
Find out where the enemy is and how their attack is doing. If it is failing, your services are not needed. If the enemy is winning,
join in the battle and help drive them back. Pay attention to general chat and set up contacts that can inform you of the current
status of a battle.Also, spread the word to alliance defenders where the battle is so they can join in. Guilds are very effective
at spreading information and defending against attacks.
<P>
<B>Numbers</B><BR>
Bring as many players as you can to help out.
<P>
<B>Use the Guards/Friendly NPCs</B><BR>
Whenever possible, lure the enemy into more powerful guards that can help you defend.
</ul>
<h3 class="title">Other Information</h3>
<P>
<B>Attacked while Questing</B><BR>
If you are in a contested or enemy region (zone), there exists a danger of being attacked while you're
trying to complete your quest(s). You should be aware of this risk. If you're trying to level up,
you might wish to pick areas where the enemy is unlikely to attack, thus reducing the risk of
confrontation. Alternatively, be aware that you can level up in instance areas where the enemy
cannot attack you.
<P>
<B>Body Camping</B><BR>
If the enemy begins camping (waiting in the area of your corpse for a long time) your body, you have several options:
<UL>
<LI>The area for reviving is very large so you can pick a spot furthest away from the enemy. Revive and run further away from the enemy.
<LI>Wait. You can wait until they leave. You can simply alt-tab and let them waste their time while you're away from the computer.
<LI>Have someone else lead them away from your body.
<LI>Revive, run away, and then use a Hearthstone to get away.
</UL>
