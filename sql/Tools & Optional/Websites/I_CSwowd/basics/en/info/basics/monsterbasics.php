<B>Monster Level</B><BR>
Each Monster has a level. Select the monster then look at their name which includes their level in two locations on the screen.
Generally, it's a good idea to attack monsters below or near your level. If you fight monsters that are much higher than your level, you should be sure to have a party to help you out.
<P>
If the monster's level is listed as "??" or a skull it indicates that you cannot determine the level
of the monster, but it's a LOT higher in level than you. So in other words, it's not a good idea to
mess with it.
<P>
<B>Elite Monsters</B><BR>
Elite Monsters are MUCH more powerful versions of the regular monsters you find. They are typically
either special quest monsters, or monsters that are found inside special dungeons. They can be identified by a "+"
after their level, an "Elite" tag, or when they smash you into the ground!
It's not best to fight Elite monsters alone unless your character has a much higher level than the monsters.
You will find this out for yourself when they attack/kill you.
<P>
<B>Monster Aggression</B><BR>
There are three main types of status for monsters:
<P>
Aggressive - Red<BR>
Neutral - Yellow<BR>
Passive - Green
<P>
<font color=red>Red Targets</font> - Aggressive is often referred to as "KOS" for kill-on-sight. This means that once you get near
enough to the monster they will attack you. You can identify this status by the red color
of the name box (target window) for the monster.
<P>
Once you become a much higher level, the monster aggro radius becomes smaller,
which allows you to get much closer to them before they will attack. Aggressive monsters will always attack
players that wander too near to them.
<P>
<font color=yellow>Yellow Targets</font> - Passive monsters, such as animals like boars, will not attack you
unless you first attack them. These monsters are usually liked by players because they can travel safely
through them and will usually only be attacked by them when players want to be attacked. Be careful about Area
of Effect Spells (AOE) such as Frost Nova, which could "wake up" one or more of these monsters.
<P>
<font color=00c400>Green Targets</font> -You cannot attack these targets. Examples of these are party members, merchants, and most
NPCs.
<P>
<B>Patrolling vs. Stationary</B><BR>
Some monsters are stationary. Other monsters will patrol around.
The patrolling monsters are the dangerous ones because they typically will surprise you and initiate battle when you're not expecting it.
You should study the monster's patrol patterns, or keep a watch out for any nearby patrolling monsters.
<P>
<B>Monster Aggro Radius</B><BR>
Aggressive Monsters have an area around them to which they will respond if a player were to enter.
Imagine an invisible circle around the monster, Once you enter that circle, the monster will "wake up" and
come attack you.
You can test this by walking very slowly toward a stationary aggressive monster. Once you enter their field of vision, or
aggro radius, the monster will attack you.
This information is very important to keep in mind. A common mistake made by newbie players is to travel around a zone and walk through pockets of monsters, waking them all up.
They then end up dying or leading their party to their death.
<P>
While you play, it's very important to scan around for patrolling monsters.
If a monster is patrolling near you, you need to move out of its way before it spots you. Otherwise, the monster will attack you.
<P>
If a monster is coming towards you while you're fighting something else, but hasn't spotted you, you need to lead your current monster away from the former's path.
Just slowly back up while you're fighting and lead the monster away from the incoming patrolling monster. If an entire party is fighting, you will need to ask them all to move back.
Alert your party to the monster's presence and have everyone back up.
<P>
<B>Solo vs. Social</B><BR>
<B>Solo monsters</B> will not help fellow monsters of the same type if they are attacked. An example would be a Boar that
doesn't care if you attack another boar. That boar will just walk right by you as
you attack his brother. <B>Social monsters</B>, on the other hand, will make "calls for help." When you attack
one, it will call out for help. If any other monsters of the same allied network are around, they will assist each
other when one of their friends is attacked. Examples of these types of monsters are Murlocs and Thieves.
<P>
<B>Monster Hate List/History</B><BR>
Once combat has begun, monsters keep track of damage being done to them and also keep track of the spells being cast.
This includes healing spells, or spells that buff other party members.
If the monster sees a player doing a lot of damage to them, or casting something they don't like, they will then turn and attack that player.
This is fine if you want the monster to attack you; however if you're a caster you generally don't want this to happen.
To counter this, you must have to have another party member do more damage to the monster, or make it angrier
so the monster will attack the other party member instead of yourself.
You will also need to immediately stop doing things that will make the monster angry, such as, casting more spells, or attacking the monster.
Once another party member has done enough damage or made the monster angry enough to redirect it's attention, you can resume your attack on that monster.
You should also be wary of casting area effect spells on a group of monsters.
They can, and will, gang up on the player casting that ability.
You need to make sure there is someone else around you that can attract their attention and pull them off you if you decide to cast the AOE.
<P>
<B>Monster Claiming</B><BR>
The first player or group to damage a monster will "tap" them. When the tapped monster dies, that player
or group will get the experience and loot from the kill. Other players will be able to tell if a monster is tapped because its
portrait will turn gray. Players are free to help kill the tapped monster, but they will receive no experience or loot
from the kill. Even with help, the player or group who initially tapped the monster will gain full experience from the
kill.In situations where a much higher level player helped with the kill, some of the experience reward will be reduced.
<P>
Example 1 : Level 10 player attacks level 10 monster, level 10 player helps - Player A gets full experience<BR>
Example 2 : Level 10 player attacks level 10 monster, level 30 player helps - Player A gets reduced xp
<P>
<B>Rookie Mistakes - Max Number of Monsters</B><BR>
A common first mistake is trying to attack too many monsters at once.
You need to determine what the maximum number of monsters you can fight at once is, and stick to that number.
Otherwise, you will continually attract too many monsters that will overwhelm and kill you! Often it's good to fight one monster at a time.
If you are in a large party and are more powerful than your targets, you might be able to take on two monsters at once.
If your party is good with spells and knows how to use certain spells that can disable monsters (like Sleep),
you might be able to take on even more monsters.
The point is to figure it out what your "max number of monsters" is. Don't get your party killed because you got overconfident and took on too many monsters.
As you play more, you will learn how to judge this. Experienced "Pullers" can also help you out and teach you how to do this correctly (See Parties: Pulling for more information on what a "Puller" is).
<P>
<B>Monster Chasing</B><BR>
You will likely be spending some time running away from monsters as you get accustomed to fighting in World of Warcraft.
Running isn't something to be ashamed of: you should always run if you face a monster that is too powerful, or if you realize you don't have enough Health or Mana
to fight the current monster. Run away! "/v flee"!
<P>
Monsters will only chase you for a specific distance before returning to their patrol area.
So if you want to avoid a battle, keep running until they leave you alone. You can rotate the screen around to see behind you,
as you run, to know when you can stop running.
<P>
<B>Monster Healing</B><BR>
Some monsters can heal themselves, much like how some players can. You will need to stun them, or finish
them off quickly with powerful attacks before they can heal.
Typically, stun abilities are the best to use for this situation.
<P>
<B>Monster Health Regeneration</B><BR>
Monsters will quickly regenerate to full health if you attack them and then run far enough away. This is to prevent cheesy hit-and-run tactics.
<P>
<B>Monster Buffs</B><BR>
Like players, monsters have certain buffs they can cast on themselves to make themselves more powerful.
You may discover this by watching them "buff up", or by reading the combat log and screen for results of the fight.
Whenever possible, cast "Debuff" or "Dispel" on the monsters to remove their buffs. This will make the monsters easier to kill.
<P>
<B>Monster Debuffs</B><BR>
Monsters often have special attacks which will place a "Debuff" on your character. Remove them as soon as possible. There are many
different spells which can do this.
<P>
<B>Spell Casting Monsters</B><BR>
Spell casting monsters, such as a mage, are very dangerous. They can do a lot of damage in a short amount of time. Fortunately, they are typically very
weak and can be quickly killed. Attack them first, and try to stun them if possible to prevent them from casting spells.
<P>
<B>Fleeing</B><BR>
Some monsters will run away when they are near death. You should use abilities that prevent them from moving to halt their escape. Another
possible way to deal with a fleeing monster is to use a ranged direct-damage spell, such as "Fireball", to kill them off as soon as they try to run.
<P>
<B>Sleep or Monster Disabling Abilities</B><BR>
Some players have the ability to disable a monster for a limited amount of time with a spell, like "Sleep".
Casters should only cast abilities, such as "Sleep", on monsters that are not currently being hit. This is because a direct hit will wake them up and waste the effect. Other members of the party
should make sure that they don't wake up the sleeping or disabled monster until the time is right.
<P>
<B>Fighting High Level Monsters</B><BR>
Players will find it more difficult to fight monsters that are a good deal higher in level than they are.
A player's chance to hit and damage ("glancing blows") is reduced, while the monster's chance to hit
and damage ("crushing blows") is increased.
<P>
<B>Monster Spawning</B><BR>
In lower level areas, monsters will not immediately attack players as they spawn.
<P>
<B>Other Information</B><BR>
<UL><LI>Creatures will regenerate health and mana while evading. Monsters evade when they are stuck or when they cannot reach their target.</UL>
