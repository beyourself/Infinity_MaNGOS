<B>Where do I find food/drink?</B><BR>
Monsters drop food and drink. You can also buy them from vendors. The cooking profession can be used to create
food.
<P>
The fishing profession can also be used to get some food.
Fish can be eaten raw but it gives more benefits if it is cooked.
<P>
<B>Does food or drink "go bad" or spoil?</B><BR>
No, it lasts forever or until it is used. Summoned food/water, however, will disappear after a being logged out of
