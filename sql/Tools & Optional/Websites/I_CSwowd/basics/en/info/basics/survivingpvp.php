<b>Quests: Non Standard Spawns</b><br>
Whenever possible, pick quest monsters to kill that are in the least popular locations. Typically, these are the ones that are hidden and harder to find. You can scout around to find them or ask more experienced players.
<p>
<b>Quests: PVP Quests</b><br>
Certain quests may be difficult to complete because the enemy side knows about them or can be alerted to your presence via the LocalDefense and WorldDefense channels. Another example of a quest that is very different in a PvP environment is an escort quest in a contested zone. These quests can be very difficult to complete. Experienced players that have been through all of the quests several times can typically tell you which quests are difficult to complete due to enemy interference.
<p>
<b>Quests: Enemy Territory</b><br>
If you have to quest in enemy territory, try to do it during non-peak hours, such as very late at night or early in the morning. You'll find it much easier than during primetime.
<p>
<b>Quests: Leave Me Alone!</b><br>
Sometimes the Horde and the Alliance can both quest in the same areas without conflict. This happens when players from both sides would rather complete their quests and attack monsters than each other. If you attack the enemy player, it might start a prolonged war, which can slow down your experience or quest progress, so sometimes players from both sides refrain from fighting. There are other types of players that will always attack enemy players on sight. It really depends on which type of player you face. You can try /wave or /hug on the enemy to show you won't attack them.
<p>
<b>Quests: Instances</b><br>
The enemy cannot attack you inside instances. They can only attack you as you enter or after you leave the instance. Instances can be good places to safely level.
