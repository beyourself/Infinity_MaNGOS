<b>Potions</b><br>
Potions can sometimes be the difference between a win or a loss. You might wish to keep an assortment of potions for battle. Health potions are especially useful, as are mana and combo potions. Many potions are on the same timer, so you can't use multiple potions of a different type in quick succession.
<p>
<ul>
<li>Swiftness Potions are great for escaping
<li>Free Action Potions can keep you from being stuck
<li>Invisibility Potions can help you sneak up on the enemy or escape
</ul>
<p>
<b>Use Friendly NPCs</b><br>
Whenever possible, use friendly NPCs to help fight your battles (if you need their help). Lead enemy players to your guards.
<p>
<b>Use Funny Emotes or Dance</b><br>
If you use funny emotes or /dance you can sometimes convince the enemy player to leave you alone or let you live. "/love /beg /hug /cry" are good options! Of course it won't work against heartless killers with no sense of humor =[
<p>
<b>AFK</b><br>
If you have to leave your keyboard, try to place your character in a hidden spot where players won't find you. Rogues can use stealth and Night Elves can use Shadowmeld to hide.
<p>
<b>Fake AFK Trick</b><br>
Type /afk and /sit and then wait for someone looking for an easy kill to come and attack you. Surprise them and kill them!
<p>
<b>Hide</b><br>
You can sometimes escape enemy players by traveling in circles and hiding behind a tree or other object. Remember that your name is above your head and is red to the enemy, so that can give you away unless you hide in a good spot.
<p>
If you are using a stealth ability, the enemy will be able to more easily spot you if they are higher level than you.
<P>
<b>Conclusion</b><br>
Hopefully you'll find some of these tips useful. =]
