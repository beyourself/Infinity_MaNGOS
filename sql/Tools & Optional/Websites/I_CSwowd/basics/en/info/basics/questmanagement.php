<div class = "postBody">
There are many aspects in World of Warcraft that require micro-management skills, and Questing is no
exception. Quest Management is a very important part of playing the game. You already have to manage the items
against the amount of space you have available in your backpacks and banks. When your space is full, you are
faced with the decision to sell items, destroy items, or trade items in order to get more space. These
choices and techniques can also be applied to quests, since the quest log can only store a maximum of
20 quests. You must manage the quests in your logs and decide which quests to focus on completing.
<P>
<B>Focus in a particular region whenever possible</B><BR>
Rather than bouncing around, it's usually best to do all the quests you can in a particular zone. Once you get to quests that are too hard or that you can't find groups to complete, then you must move on to another zone with easier quests or more people to play with.
<P>
<B>You don't have to do every quest</B><BR>
There are hundreds of quests. While it is possible to do them all, it's not very efficient. Once
quests become too low level (green or gray) you should abandon them and focus on quests that will give you
more experience. You should only do a super easy quest if it has a reward you want or because you are
more interested in the quest itself than the experience it provides.
<P>
<B>Ask for quest advice</B><BR>
Other players can tell you which quests are good for experience or which might have a quest reward that will benefit your character. Feel free to talk to guild members, party members, or players in the general channels for advice.
<P>
<B>Quest too hard?</B><BR>
If the quest title is orange or dark red, it's unlikely you will be able to complete it unless you have a group. It's best that you find some other yellow or green quest to do. As you level up, the red quests will turn to yellow and
they will be easier to do.
<P>
<B>"I'm out of easy quests, what do I do?"</B><BR>
There is an easy answer to this question: simply move on to another area. There will be several different areas
around your character's level range you can use to level up. For example, you can play in Westfall (Humans),
Loch Modan (Dwarves), or Darkshore (Night Elves) which are all around the same difficulty level.
If you're having trouble in one of these areas, go to another area with a similar level range.
<P>
<B>Finding quest help</B><BR>
If you are having problems completing a quest or finding where to get a quest, ask for help in
the general chat channel. You can also post about quests in the Forums. Usually the best place
to ask is your guild, especially if there are more experienced players who have completed the quests
several times.
<P>
<B>Getting quest items</B><BR>
You can buy, trade, or receive some quest items as gifts from other players. These items must not be
marked as "quest items" which cannot be traded. For example, one quest requires Buzzard Wings. Once
people learn that these are a part of a quest they may sell them, or give them away to friends who
need them for the quest. You can ask your guild "does anyone have any extra Buzzard Wings?". Then
people will know that you need them. You can also use the mail system. Have friends mail you the
quest items. You can also use the Auction House to look for quest items. This can give you a short
cut in completing quests rather than fighting monsters for the quest items. It's a balance of
time vs. money. You can save money by getting the items yourself but it will take longer.
Once you've identified what items are used in quests, you can save them and give or
sell them to other players.The number of quest items that can be traded is small. In most cases
the only quest items that players can trade are tradeskill items.
<P>
<B>Use your bank</B><BR>
You can store quest items in your bank for later use. Just make sure you pick them up before you go out to continue the quest.
<P>
<B>Keep a few Quest Slots Open</B><BR>
For system performance reasons, the quest log has a limit of 20 quest slots. As you travel you should always try to have 1-2 slots open for new quests. Sometimes you will be placed in situations that prompt you for a new quest and not having room to accept one could hamper your progress. This can happen with escort missions and instances. For example: If you go into an instance and have to get a quest very
quickly but are "full", it is possible that you won't be able to get the quest again until the NPC resets (or until you enter a new instance).So leave some room for emergency situations.
<P>
<B>Abandoning Quests</B><BR>
If you abandon a quest you can still go back and get the quest again. As your character advances beyond the optimal level range for a quest the exclamation point above the quest giver's head will disappear, but you can still return to start the quest at a later date. Just write down which quest it was, the NPC's name and its location just in case you need to refer to your notes.
<P>
Quest rewards, both in experience and items, are designed to be appropriate for the level at which they're offered. If you're going to
abandon a quest try to abandon quests that are gray or that have been green for a long time. Those usually aren't worth your time
unless you are trying to get a continuation quest or are doing it for enjoyment of the storyline.
<P>
Try to abandon a quest that is on the first step. It is very easy to go back and get that quest again.
It can be more confusing if you're trying to get a continued quest again.
<P>
You should only destroy the quest items for abandoned quests if you have no intention of getting that quest again. If you are concerned
about inventory space, you can store quest items in the bank until you're ready to continue the quest. Just remember to pick them up again
before you try to complete it. :)
