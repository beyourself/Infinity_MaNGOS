<B>Chat Auctions</B><BR>
Chat Auctions are an alternative to <a href="auctionhouses.php">Auction Houses</a> for
players who want to trade instantly without waiting. The seller runs an auction in a trading channel, then sells
the item to the highest bidder at the close of the auction. The buyer and seller must then meet up in the world to make the trade.
<P>
<b>Sellers</B>
<ul>
<LI>Use the Trade Channel.<BR>
<LI>Pick a starting bid for your item. <P>
<B>Low Starting/Minimum Bids</B><BR>
If you set it really low you might get a lot of attention (and possibly insults/laughs) by players who know the item is worth much more. This can work to your advantage as it's free publicity when players talk about your item.
<P>
<B>High Starting Bids</B><BR>
If your minimum bid is set too high then you won't get any bids on your item. If you start an item at 1 gold you may find no bidders for that item.
But, if you start the item off at 25 silver the same item might sell for much higher than 2 gold. Your selling strategy determines
the success of your trade.
<P>
<LI>Announce your item with a link to the item: [Iridescent Pearl] min bid 1g.
<P>
<LI>Wait for bids. Once someone makes a bid, acknowledge their bid by repeating it with their name. I have 1g on [Iridescent Pearl] by Jonny. If people are bidding really quickly against each other, wait until it slows down and announce the last bid.
<P>
<LI>If bidding slows down or stops, wait a bit (it's up to you how long) and say [Iridescent Pearl]
going once for 1.5g to Jonny. (or whatever the last bid was). Wait a little bit longer then
say [Iridescent Pearl] going twice for 1.5g to Jonny. Then call the auction as sold. [Iridescent Pearl] sold for 1.5g to Jonny.
<P>
<LI>It's important that you keep repeating the link and the price so people who come into the channel later can see what is being bid on and what the price is at. It's also important that you call the auction. Otherwise the buyer might become bored and will quit.
<P>
<LI>You should now message the winning bidder and tell them that they won and set up a place to meet. Sellers should locate themselves by banks or flying transport locations so that buyers can easily find them.
<P>
<LI>If the deal falls through somehow and you're unable to work out the problems with the winning bidder,
offer the item up for auction again. You can also try selling it to the second highest bidder at their
last bid price. However, they are under no obligation to pay since the auction is over.
<P>
<B>Other tips</B><BR>
<LI>Don't try to sell an item that cannot be traded such as a soulbound item. Read about item basics to learn about the quality of items and bind status. Many quest items cannot be traded. Check to see.
</ul>
<B>Buyers</B>
<ul>
As items scroll by, make a bid on an item you want. Example: "10 s (silver) on [Amazing Sword]." Try to mention what you are bidding on if there are multiple items for trade at the same time.
<P>
<B>Beware of bid wars</B><BR>
The goal of the auction isn't to BEAT another player in some imaginary battle. The goal is to get the item at a reasonable price. Don't bid up an item to crazy numbers just to win against another person bidding on the item.
Pull out if the price gets too high.
<P>
<B>Beware of shady auctions or bids</B><BR>
Look up the bidders and seller using /who to make sure they are not in the same guilds.
</ul>
<B>Auction Etiquette</B>
<ul>
<LI>Try to include a link to the item whenever possible. There are always new people showing up that may have missed the previous link.
<P>
<LI>Once people are bidding on an item, you should sell it at the end of the auction. It's rude to offer to sell an item and then retract the item from bid.
<P>
<LI>Try not to take back a bid offer unless there are special circumstances. If you have to, make sure you inform people that you are sorry that you have to retract your bid.
<P>
<LI>If someone is selling an item andyou think they're asking for too much money, message them privately. They will either thank you, ignore you, or be less polite. It's rude to insult a player's asking price in public. If the item is really overpriced then no one will buy it. If someone is paying too much for an item, let them. Maybe they have a ton of money and don't care what an item is priced at, they just want the item NOW.
<P>
<LI>Don't bid on something if you don't have the money or are not ready to trade. If you're questing, you better be ready to leave and trade. You could offer to pay a tip if the person selling must wait, but don't be surprised if they resell the item.
<P>
<LI>If the seller has to wait or has to travel to deliver an item, they should be tipped over the cost of the item to compensate them
for their travel time.
<P>
<LI>If someone asks for more money for an item or does anything shady then you might want to back out of the deal.
</ul>
