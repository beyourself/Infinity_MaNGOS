<B>Tips on Being Nice!</B><BR>
<img src="../../../shared/wow-com/images/smallcaps/plain/t.gif" align=left>here are a lot of easy things you can do to win friends and perhaps help improve your reputation.
This may help you in the future by getting you in parties or guilds, or getting you good trade deals, free gifts,
and other rewards. These suggestions are not mandatory and aren't always applicable. Use your best judgment
when applying them!
<P>
<UL>
<table border=0 cellpadding=0 cellspacing=0>
<tr><td valign=top><img src="../../../shared/wow-com/images/parchment/plain/light/bulletblack-small.gif"></td><td valign=top><span>Be polite when you make item trades. Thank the person for their purchase or for selling the item to you.
You can also /wave and /bow. Whatever happens, keep your cool and be polite. Take the high road in any item disputes.
<p></span></td>
<td rowspan=3 valign=top><img src="../../../shared/wow-com/images/basics/benice/running.jpg" align="right" title="Girls, c'mon! We didn't mean it!"></td>
</tr>
<tr><td valign=top><img src="../../../shared/wow-com/images/parchment/plain/light/bulletblack-small.gif"></td><td valign=top><span>Help point the way when players have questions or problems.
<p></span></td></tr>
<tr><td valign=top><img src="../../../shared/wow-com/images/parchment/plain/light/bulletblack-small.gif"></td><td valign=top><span>Cast Buffs on other players you meet along the way. They won't be expecting it and it could help
them in battle. Often other players will return the favor by casting whatever buffs they have on you also!
<p></span></td></tr>
<tr><td valign=top><img src="../../../shared/wow-com/images/parchment/plain/light/bulletblack-small.gif"></td><td valign=top colspan=2><span>If someone is about to die fighting a monster, save them! You can heal them or help finish the
monster off. Of course, don't let the other player take advantage of you by continually fighting things they
cannot defeat on their own.
<p></span></td></tr>
<tr><td valign=top><img src="../../../shared/wow-com/images/parchment/plain/light/bulletblack-small.gif"></td><td valign=top colspan=2><span>As you are traveling around, if you see someone who is really damaged, cast healing on them (if you have it).
You'll regain your mana while you travel so it doesn't hurt you any and the other player might really appreciate
the help.
<p></span></td></tr>
<tr><td valign=top><img src="../../../shared/wow-com/images/parchment/plain/light/bulletblack-small.gif"></td><td valign=top colspan=2><span>Give away items. You don't always have to sell items you no longer want. You can help another player
instead.
<p></span></td></tr>
<tr><td valign=top><img src="../../../shared/wow-com/images/parchment/plain/light/bulletblack-small.gif"></td><td valign=top colspan=2><span>If you meet up with another player or group of players at the same quest location, try teaming up to
complete your goal. That way rather than competing against each other you can work together!
<p></span></td></tr>
<tr><td valign=top><img src="../../../shared/wow-com/images/parchment/plain/light/bulletblack-small.gif"></td><td valign=top colspan=2><span>If you notice a person is about to attack a monster, let them have it. Find another monster elsewhere
to attack.
<p></span></td></tr>
<tr><td valign=top><img src="../../../shared/wow-com/images/parchment/plain/light/bulletblack-small.gif"></td><td valign=top colspan=2><span>If multiple parties are in the same location, you can help each other complete especially difficult
quests! For example one party can help clear monsters around a boss while the other party fights the boss.
You can then exchange positions and do it again until everyone has completed their objective.
<p></span></td></tr>
<tr><td valign=top><img src="../../../shared/wow-com/images/parchment/plain/light/bulletblack-small.gif"></td><td valign=top colspan=2><span>If an item drops in a party and you don't really need it, give it to a party member that does. Being
greedy could hurt your reputation!
<p></span></td></tr>
<tr><td valign=top><img src="../../../shared/wow-com/images/parchment/plain/light/bulletblack-small.gif"></td><td valign=top colspan=2><span>If you happen to have extra copies of items that can be used for a quest, you can give them to other
players that are working on that quest. This only works with items not marked as quest items.
</td></tr></table>
</UL>
