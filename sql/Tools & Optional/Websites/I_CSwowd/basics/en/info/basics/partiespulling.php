<div class = "postBody">
The concept of "pulling" has been perfected in other games and carries on to World of Warcraft.
The idea is a player in a party, known as "the puller", scouts ahead, finds the next monster or monsters, and brings them
back to the waiting group. The party fights and kills those monsters and then the process repeats. This is a much safer
method than the whole party wandering around together in dangerous locations where they could wake up too many monsters.
<P>
The existence of pulling in the game doesn't indicate that there is a lot of camping. Typically as a group is advancing in an
area or dungeon they will stop and pull nearby monsters to a safe area. Once those monsters are cleared they will advance on. Players
aren't typically sitting in one location waiting for monsters to respawn.
<P>
<B>The Puller</B><BR>
Pullers are generally tanks such as a Warrior or Paladin. This is because they have the most hit points and armor and can take a real
beating. This is required because they have to lead the monsters back to the party. A casting class could be used as a puller if the
monsters are easy or are not able to hit them with ranged attacks.
<P>
Another reason a squishy mage type class is often not a good puller is due to the hate/aggro balance you try to achieve in party pulls.
<P>
If a Mage pulls with a long distance ability the monster will come running in perhaps easier than a Warrior/Paladin running up and body-pulling or letting it agro on them, but then once it reaches the party its first target will be that soft squishy Mage type who will generally take a beating until the heavy tank class can pull it off the puller.
<P>
Not only does the squishy puller take a beating they are not designed to take, the healers in the group will have to waste more power by healing someone, and then have to switch targets to heal a different person each time a person pulls.
<P>
Not to mention that the special heals and other abilities designed as 'group saves' will need to be used more often and won't be available when they are really needed. For example, if you have pulled and are fighting monsters ok and then something else aggro's into the party like a wandering monster.
<P>
This is a main factor in why a tank often becomes the puller so that aggro/hate originates on the person who is going to 'tank' (poetic isn't it?) the monster while the party beats it to death.
<P>
<B>Camping Spot</B><BR>
First you need to pick a camping spot. This will be the area where the party sits and the puller leads the monsters back to fight.
It's best to pick a spot that is not patrolled by monsters. You also don't want to choose a spot that is in the middle of a monster camp.
A monster camp may respawn (monsters spawn again) while you're already fighting monsters which can shift the
battle to the monsters' favor.
Experienced pullers will know which spots are good to camp. After the camping spot has been determined, instruct the party to
remain here: "/v stay." After the party has stopped, it's now the puller's turn to go fetch the monsters.
<P>
<B>Pulling</B><BR>
It's very important to know about monster aggro radius. This knowledge can be gained through experience. Simply walk up to a monster
inch-by-inch tapping the forward key. Watch the monster for their "excited" movement and sound when they notice you. You will then
know about how close you have to get to a monster to wake them up. You always want to wake up the monsters at the maximum possible
range away from the monster. This will give you time to run back to the party.
<P>
If you are stunned, rooted, or immobilized and doubt that you can return to your party, inform your party so that they can come save you.
<P>
It's also important not to bring back too many monsters and kill the whole party. As you gain more experience at pulling you will learn
what a safe "pull" is.
<P>
After you have woken up the monsters, turn around and run back toward your party. You may have to run past your party's location to drag
the monster to your party members. Make sure you tell your camping party members to stay where they are. After the monsters are killed,
go out and grab some more monsters.
<P>
<B>Ranged Weapons</B><BR>
The puller should have some sort of thrown weapon or ammunition they can use to "wake up" the monster at a distance. This will allow
you to pull monsters much easier than just walking up to them. Even Warriors can use Thrown Weapons of some type.
<P>
<B>Pulling Melee Monsters</B><BR>
Pull them to the front of the party so that ranged party members can fire on them. Otherwise they will get failed messages
if you pull it behind them.
<P>
<B>Pulling Ranged/Magic Monsters</B><BR>
The monster will only be pulled when the puller is out of the monster's range. Since they are ranged, unlike melee monsters, the
puller will need to run past the party to lead the monster into the middle of the party.Sometimes the puller will have to run
a LONG way. Keep looking behind you to make sure the monster is in the right location. Then turn around and fight. Make sure you
do not blindly run into additional monsters while running away.
<P>
<B>Pulling to a Location</B><BR>
If you are traveling to a destination, such as to the end of a mine, you will have to continue to move your camping spot forward as
the monsters are cleared. The puller should tell the group when to move up and where to stop.
<P>
<B>Leading Long Distances</B><BR>
It can be very challenging to lead a monster a very long distance. Once you run a specific distance away from the
monster's original location the monster will stop and return to it. What you have to do is damage them every so often while
leading them back. This will make them angry enough to keep following you.
<P>
<B>Additional Instructions for Pullers</B><BR>
Type "/v inc" to give your party a notice that monsters are coming. You can also just type "inc."
<P>
Watch party members' health and mana bars. If they are low, don't pull. Wait until they are ready before going
for another run. Otherwise they might not be able to handle the next group of monsters.
<P>
<B>Instructions for Camping Party Members</B><BR>
<LI>Before the puller comes back, select the puller (F2-F5) and be ready to hit F for assist to target the right monster.
If you get confused, hit V to show the monster hit point bars. Select the one that is the most damaged.
<br>
<LI>Watch for the puller's notice that monsters are incoming and be ready to fight. While you're waiting sit down
to regenerate mana/health or use food/drink.
<br>
<LI>Heal the tank before they head back to pull another monster. You want to do this instead of healing them when
they come back with monsters in tow (unless they are about to die). If you heal an incoming puller the
monsters will pile on the healer and sometimes kill them.<BR>
<LI>Don't move from the camping spot until the party puller says to move.<BR>
<LI>If any monsters appear near the camping spot while the puller is away, make sure you inform them so they don't ALSO bring back monsters. You can do this by typing "1 here", "2 here", "respawn", or "pop." If you do not inform the puller, they will often bring back monsters and the combination of monsters will kill the party. <BR>
<LI>Remain patient and don't try to follow the puller. Their job is to get the monsters, your job is to SIT THERE. =]<BR>
<LI>Don't move forward until the puller tells you to or until you see them stopped fighting a monster.<BR>
<P>
<B>Failed Pulls</B><BR>
Your party may have crowd-controlling abilities to deal with too many monsters such as Polymorph or Sleep, which can help a group survive a bad pull. If these abilities fail, everyone in the party can run or alternatively, the puller can just decide to suicide and die before the monsters aggro the other members of the group. The party can then bring them back to life and continue their adventure.
<P>
