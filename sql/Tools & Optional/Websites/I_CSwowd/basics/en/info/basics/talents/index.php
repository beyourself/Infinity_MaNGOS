<div class =" postContainerPlain">
<div class =" postBody">
<blockquote>
<img src="../../../../shared/wow-com/images/smallcaps/plain/t.gif" align=left>o learn a talent, mouse over the talent first and decide if it is right for you. Talents are difficult to unlearn, so you should be sure before you commit to one. Also keep in mind that you cannot learn every single talent, so you should make a choice about which talents or what category of talents you want to learn.<p></p>
When you decide on a talent, left-click on it to learn it. There is no confirmation of your pick. Once you click on it, you buy it. If by purchasing the talent you open new avenues of talents, the newly unlocked talents will turn from gray to bright. All classes have talents that allow the player to customize their character. Talents can be applied in the following ways:
<ul>
<li>To improve a class' existing spells or abilities.
<li>To alter the functionality of existing class spells or abilities.
<li>To acquire new spells and abilities.
<li>To improve class skills.
</ul>
The three lines of class talents are geared towards each class' main strengths. For example, mages naturally divide their talents into arcane, frost, and fire, which mimic the three main schools of magic mages learn. Warriors have the talent lines of arms, fury, and protection. Each category of talents is designed like a tree, with new talents branching out from a core set of base talents you can learn at low levels. Within each tree, a character can learn talents that make that character more effective in all the abilities and capabilities that fall under the heading of that talent tree. The frost line of talents, for example, enhances a mage's proficiency with frost spells. The warrior's protection talents improve the warrior's taunt and defensive abilities. No player can ever hope to become a master of all three lines of talents. In fact, with dozens of talents per line, and multiple ranks to each talent, the most a character could hope to fully master is one talent tree, and perhaps half of another one.
<p></p>
Talents can be seen by clicking on the Talents button or pressing the hotkey N. Mouse over each talent to receive a description of what it is and how to use it. Be sure to peruse your class' entire array of talents before choosing them. It helps to have a goal or general theme for your character when purchasing talents, rather than picking them haphazardly. Of course, there is nothing wrong with experimentation, but you may find it wise to research all your class' talents before purchasing them.
<p>&nbsp;</p>
</blockquote>
