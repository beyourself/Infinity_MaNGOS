<div class =" postContainerPlain">
<div class =" postBody">
<blockquote>
<img src="../../../../shared/wow-com/images/smallcaps/plain/w.gif" align=left>very class has three categories of talents that are organized by your class abilities. Mages, for instance, rely primarily on their spells, which are broken up into the arcane, frost, and fire schools. Thus, the mage's talent categories are arcane, frost, and fire. Each category can be accessed using the tabs at the bottom of the Talents window.
<p></p>
Talents are bought on a one-to-one ratio. One talent point spent on a talent gives you one rank in a talent. You do not accrue talent points until you have advanced past the first few levels of gameplay.
<p></p>
The talent system is tiered. There are low-level talents you must buy and master before you can learn high-level talents. However, you can see all talents in a category, and see the paths leading to all talents. In addition, mousing over any talent tells you what the talent does and what requirements you must meet to learn it. Talents that are grayed out cannot be learned, either because you have no talent points to spend or because you do not have enough mastery of other talents. Talents do not have level limits.
</blockquote>
