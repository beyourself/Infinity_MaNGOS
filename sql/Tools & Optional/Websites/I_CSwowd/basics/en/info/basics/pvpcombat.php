There are special combat rules in place on the Horde vs. Alliance Race War Realm.
<P>
<B>Death</B><BR>
Players that die in PvP, either versus another player or versus a PvP-Flagged NPC, must wait 2
minutes before resurrecting at their corpse.This delay does not apply to players that are resurrected
by other players or by using resurrection items.
<P>
<B>Control Spell Limits</B><BR>
Control types of spells such as Charm, Fear, and Stun are subject to diminishing returns. This means that if a second spell of the same type is used against the same player within 15 seconds of the first effect wearing off, the second spell's duration will be reduced by 50%, the third's duration will be reduced by 75%, and the player will be immune to the fourth casting.
<P>
<B>Slowing Spells & Diminishing Returns</B><BR>
A handful of spells were added to the list of spells subject to diminishing returns in PvP. It is our goal to set as few spells as possible to be diminished. We've identified this short list of spells as having durations and cooldowns sufficient to almost permanently slow or immobilize targets but without adequate controls or other limiting factors. When a spell with diminishing returns is used against a target in PvP, the first effect has full duration. On the second use of the same category of spell (e.g. slowing), that spell's duration is reduced by 50%. On the third use, the duration is reduced by 75%. On the fourth attempt, the target becomes immune to the spell. Spells must be used on the same target within 15 seconds of the *end* of the duration in order to be diminished. In other words, if a target hasn't had a slowing spell active on them for more than 15 seconds, the next slowing spell will have full effect. The list of affected spells is:<br>
<ul>
<li>Frost Shock
<li>Entangling Roots
<li>Frost Nova
</ul>
<p>
<b>PvP Officer Lounges</b><br>
Guard directions, signs and signposts have been added to point you toward the PvP Officer Lounges in Stormwind City (Champions' Hall) and Orgrimmar (Hall of Legends).
<p>
<b>Zone Under Attack Messages</b><br>
You can turn the "zone under attack" messages on or off by joining/leaving their channels. By default they are both off; to join either type /join LocalDefense and /join WorldDefense.
<p>
<b>Other Information</b><br>
<ul>
<li>The Arena in Stranglethorn is flagged for Free For All PvP. Players of either faction can kill each other in the Arena.
<li>You cannot buff or heal enemy PvP-enabling NPCs. (NPCs with a shield icon in their portrait.)
<li>Resurrecting a PvP-enabled player corpse will flag you for PvP
<li>Healing and buffing NPCs will not flag you for PvP unless those NPCs are in combat.
<li>Party members participating in PvP have light green names.
<li>Pets and totems do not attack PvP-flagged players if you're not in PvP. (better chance for the spell to succeed).
<li>Goblin guards will attack players who are fighting each other and not dueling - it's bad for business.
<li>Goblin guards may not be healed or buffed.
<li>Skills will not increase from use while dueling or engaged in PvP.
<li>Patrolling scouts that can see through stealth and invisibility have are located in Orgrimmar, Thunder Bluff, Undercity, Stormwind City, Darnassus, and Ironforge.
<li>NPCs will not tap creatures they are fighting.
<li>Spells in PvP have a slight increase in range and area of effect when targets are moving.
<li>Charm and Polymorph mechanics can be reactively countered by spells and abilities (e.g. Druid Shapeshifting will now cancel Polymorph effects).
</ul>
<B>Faction Specific Graveyards</B><BR>
There are faction specific graveyards for many zones. Depending on your faction, you'll
appear at one graveyard or the other. Examples:
