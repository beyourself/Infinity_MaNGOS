<div class = "postBody">
<img src="../../../shared/wow-com/images/smallcaps/plain/o.gif" align="left">pen up the guild screen by hitting the "O" key followed by selecting the guild tab. The guild must already be
created and you must be a member of the guild. You will now see a guild listing with several different options.
<P>
<B>Show Offline Members</B> (check box) - This option is located in the top right. Toggle this to display guild members
that are currently not logged into the game. You might find this useful to focus on players that are in the game and
playing vs. not online.
<P>
<B>Guild Member List</B> - In the main middle windowpane you will see a list of guild members sorted by Name, Rank, Note, and
Last Online.
<ul>
<table cellspacing =" 0" cellpadding =" 0" border =" 0" border =" 0" align =" right"><tr><td></td></tr></table><B>Name</B> - The player's character name.<BR>
<B>Rank</B> - The player's position in the guild. This position is determined by the officers or those guild members
authorized to change rank.<BR>
<B>Note</B> is a special note on that player created by someone authorized to enter notes.<BR>
<B>Last Online</B> - When that player was last online. This is also useful to guild officers to check how active
that player is. Remove inactive players? =]
</ul>
<h1 class =" title">Sorting</h1>
You can sort the list by each of the above four fieldsby clicking on any of the above headers. For example, click on "Rank" to
sort the list by rank.
<P>
If you click on "Show Player Status" you will see an alternate guild listing by Name, Zone, Lvl, Class, and Group.
<ul>
<table cellspacing =" 0" cellpadding =" 0" border =" 0" border =" 0" align =" right"><tr><td></td></tr></table><B>Name</B> - Guild member's character name.<BR>
<B>Zone</B> - The zone or region where the player is currently located. This is useful information to know
where the player is currently located so you can determine their current status, or whether they are able
to team up.<BR>
<B>Lvl</B> - Player's character level.<BR>
<B>Class</B> - The class of that guild member.<BR>
<B>Group</B> - Whether they are currently in a party. If they are not in a party perhaps they are open to teaming up.
</ul>
Select "Show Guild Status" again to toggle back to the previous view.
<P>
<h1 class =" title">Members Totals</h1>
Below the guild member listings you will see a listing of the total number of guildmembers and how many are online. If
"Show Offline Members" is toggled off, the total number of members shown will be of thosecurrently online.
<P>
<h1 class =" title">Control Panel</h1>
Below the guild player listing you will find another area with a bunch of various buttons.
<ul>
<table cellspacing =" 0" cellpadding =" 0" border =" 0" border =" 0" align =" right"><tr><td></td></tr></table><B>Group Invite</B> - Select a player on the list, then hit this button to invite them into your party.<BR>
<B>Guild Control</B> - This will launch a control window pane where you can edit the ranks of the guild and set up permissions for each rank. This will be explained in detail further down the page.<BR>
<B>Add Member</B> - Press this button then enter the name of the player you wish to invite to the guild. Once they accept your invitation they will be added to the guild. If they do not accept you will have to invite again.
<P>
At the very bottom of the guild tab you will see:
<P>
<B>Promote</B> - Select a player on the list then hit "promote" to set their rank to a higher rank in the guild.
A message will be sent to the entire guild that this player's rank has changed, so be careful when using it.<BR>
<B>Demote</B> - Select a player on the list and hit "demote" to demote that player one rank in the guild.<BR>
<B>Remove</B> - Select a player on the list and hit "Remove" to kick them out of the guild.
<P>
<img src="../../../shared/wow-com/images/basics/guilds/icon-trumpet.jpg" align="left"><B>Guild Message of the Day</B> - Select the trumpet to see the guild message of the day. Double-click on the text
for the message of the day to edit it. Your current rank must be authorized in order to modify the message of the day. This
message will be displayed to anyone in the guild that comes online. <BR><img src="../../../shared/wow-com/images/layout/pixel.gif" height="5"><br>
<img src="../../../shared/wow-com/images/basics/guilds/icon-notes-players.jpg" align="left"><B>Player Notes</B> - Select a player; then hit the "Public Note" button to see any public notes on that player.
Those notes will display to everyone in the guild. To edit these notes your rank must have permission under
guild control. <BR><img src="../../../shared/wow-com/images/layout/pixel.gif" height="5"><br>
<img src="../../../shared/wow-com/images/basics/guilds/icon-notes-officers.jpg" align="left"><B>Officer's Notes</B> - Select a player; then hit the "Officer's Note" button to see or change any officer notes on that player. Only ranks authorized to view officers'
notes can see these notes on the player. Officer notes are useful to put any secret information on that player that
you want officers and authorized users to be aware of. For example perhaps a player is a troublemaker or has
received a warning. You can place "This person has been a jerk lately so watch out for them." The other officers can
see this warning and can take appropriate action if they see this player getting out of hand. Be careful as to which
ranks you allow to see this information. Make sure you enter this information under officers' notes rather than public
notes. To edit these notes, click on the text.
</ul>
<h1 class =" title">Guild Control</h1>
Select this button to edit guild ranks and set permissions for each rank.
<P>
Select "Guild Rank" to reach a screen/window where you will find the
default ranks listed; you can stick with those, create your own ranks,
or rename existing ranks.
<P>
<ul>
<B>Add Rank</B> - Select the <img src="../../../shared/wow-com/images/basics/guilds/icon-plus.jpg"> icon and type in the name of the rank you wish to add. It will be added to the bottom of the list. Select the guild rank dropdown menu and select the rank to modify the permissions for that rank. You can create up to 10 ranks. <BR>
<B>Rename Rank</B> - Select the rank you wish to modify via the dropdown menu. Now enter the new name of the rank in the "Rank Label" box to rename it.<BR>
<B>Remove Rank</B> - The <img src="../../../shared/wow-com/images/basics/guilds/icon-minus.jpg"> icon appears if you have more than 5 ranks and if you have
the last rank selected. There is a minimum of 5 ranks; you can't remove ranks beyond that.
</ul>
<h1 class =" title">Guild Rank Permissions</h1>
Below the Rank Label is a set of check boxes for permissions. Toggle each box to set what each rank is allowed to do.
<P>
<ul>
<B>Guildchat Listen</B> - Player is able to listen to conversations in Guild Chat. You would uncheck this
option if you wanted to prevent a rank from being able to read conversation in guild chat.<BR>
<B>Guildchat Speak</B> - Player is allowed to type in the guild chat channel. <BR>
<B>Officerchat Listen</B> - Allow players to listen to conversations in the special officer chat channel.<BR>
<B>Officerchat Speak</B> - Allow players to type messages into the officer chat channel.<BR>
<B>Promote</B> - Players can promote other players in the guild.<BR>
<B>Demote</B> - Players can demote other players in the guild.<BR>
<B>Invite Member</B> - Players can invite other players to the guild.<BR>
<B>Remove Player</B> - Players can remove players from the guild.<BR>
<B>Set Motd</B> - Player is able to change the guild message of the day.<BR>
<B>Edit Public Note</B> - Player is able to edit public notes. If you uncheck this box the player is not
able to edit public notes.<BR>
<B>Edit Own Public Note</B> - Player is only able to edit their own public notes.<BR>
<B>View Officer Note</B> - Player is able to view special notes on each player written by officers or other authorized members of the guild.<BR>
<B>Edit Officer Note</B> - Player is able to modify hidden officer notes under each player.<BR>
</ul>
