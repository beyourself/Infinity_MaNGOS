<h1 class =" title">Common Questions</h1>
<ul>
<B>How can I Identify an Instance Area?</B><BR>
You can recognize an instance area by the swirling blue portals of energy. If
you pass through these doors you will see a loading screen.
<P>
<table cellspacing =" 0" cellpadding =" 0" border =" 0" border =" 0" align ="right"><tr><td align ="center"><a href ="../../../shared/wow-com/images/basics/instancing/ss10.jpg"><img src ="../../../shared/wow-com/images/basics/instancing/ss10-thumb.jpg" border =" 0"></a><br><span><a href ="../../../shared/wow-com/images/basics/instancing/ss10.jpg">Loading Screen</a></span></td></tr></table><B>Are only world dungeons instanced?</B><BR>
There are other instances besides dungeons such as the Stormwind Stockades. We may continue to add instance areas as needed.
<P>
<B>What if someone is in copy A and wants to be in copy B?</B><BR>
They must leave their first group and then exit the instance. The group in copy B must then invite that person outside the
instance. The person can then "zone in" (enter) the instance and be in copy B.
After a player joins a group, the new group can summon the player (Warlock and 2 players required) into the instance.
<P>
<B>What happens if everyone in the instance dies?</B><BR>
That copy of the instance is left intact for several minutes. Players should return to the instance to make sure it doesn't
reset. If you wait too long, the instance is reset.
<P>
<B>How do I tell if the instance is reset/fresh?</B><BR>
You will have to use your own experience/knowledge of the monster spawns to determine if the area is a new one or one that has
already been played in. You can typically tell this by noticing how many monsters are there. Often, not all monsters respawn
after being killed.
<P>
<B>How do I get a new copy of an instance when I'm done playing in my current one?</B><BR>
Leave the current instance. Wait a period of time (approximately 30 minutes+) and then enter the instance again. Another option is to
have another player exit the party, enter an instance, and then invite other players into the party.
<P>
<B>What happens if I die? How do I get my body back?</B><BR>
When a player enters the instance as a ghost, they return to their body on the other side. They do not have to go through the instance
looking for their body. The best way to handle death in an instance is to be resurrected to get back to the same spot.
<P>
<B>Do the monsters respawn?</B><BR>
It depends on the dungeon. In some cases only some of the monsters respawn.The bosses don't respawn because killing them
without their minions' help would be too easy.
</ul>
