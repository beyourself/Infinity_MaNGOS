<P>
<a href ="../../../shared/wow-com/images/basics/instancing/ss01.jpg"><img src ="../../../shared/wow-com/images/basics/instancing/ss01-thumb.jpg" border="0" align="right"></a><img src="../../../shared/wow-com/images/smallcaps/plain/i.gif" align="left">nstance zones allow you and a group of friends to have a more personal experience exploring, adventuring, or completing quests
in your own private dungeon. You also have the ability to invite others into your instance zone to join you. This relieves many
of the camping, kill-stealing, and farming issues that MMORPGs sometimes face. The monsters in instance zones are typically more
powerful, so groups of players will have to work together to defeat them, but with greater dangers come greater rewards!
<P>
An instance is a special copy of the dungeon for you and your party. The only players in this instance will be yourself and members
in your party. No one else can enter this copy of the dungeon. This allows you your own personal dungeon without other outside
interference. The dungeon is fully spawned and it is balanced and designed specifically for you and your party.
