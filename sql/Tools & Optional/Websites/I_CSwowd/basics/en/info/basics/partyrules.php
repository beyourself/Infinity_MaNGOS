<img src="../../../shared/wow-com/images/characters/ingame/tailor.jpg" align="right">Party etiquette is very important in a massively multiplayer game. Keep in mind that your reputation will follow you throughout
your playing career. If you offend fellow party members, your reputation can be affected. Those party members
can tell their friends or guild mates about your behavior and this may affect your dealings with them in the future. They could possibly
refuse to invite you to group with them, they might not allow you to join their guild, or possibly bar you from receiving
services from their guild mates. This is a worst-case scenario; however it can happen, and it all depends on your behavior.
So, it is always in your best interest to be on your best behavior. Making friends is more beneficial than creating enemies.
<P>
Here are some tips:
<P>
<B>Stay Until the Job is Done</B><BR>
Don't leave a party after completing your portion of the quest. Stick around until everyone in the party has completed the quest.
For example: each party member must collect 5 items. You collect 5 but there are still members in the party that haven't collected 5 yet.
It is bad manners to suddenly leave the party once you have collected your share without helping out the remaining party members, or receiving
their permission to leave. If you leave like that, people often think that you just used them to complete the quest, and abandoned
them once you got what you wanted.
<P>
<B>Loot</B><BR>
If you receive an item that you cannot use, but there is another party member that can use it, you should give it to that party member. For
example: you loot a really nice Staff, but you are a Warrior. Certainly you could sell that Staff for money, but if there is a Mage
in that group using Staves, you should give them the Staff. This also includes trade skill item components and recipes. Consult your party or
group leader for the current rules of the group. This situation has a potential to blow up if you loot an item that you cannot use, and refuse
to give it to someone who could have really used it.
<P>
<B>Don't Loot During Battle!</B><BR>
You should usually avoid trying to loot a monster during battle (unless you're all about to die). You need to focus on the battle and make
sure everything is safe before looting. If your party is fighting, while they see you standing around and not
helping, you could offend them.
Fight first, loot later.
<P>
This is especially true for treasure chests. You shouldn't try looting a chest while the rest of your party is fighting. That is
rude. The only time you might want to bypass this rule is when you are traveling with very good friends, or if other players outside your party might be close to
looting it. Let the group leader determine who is going to loot. A good way is to type "/random 1 100" to generate a number
from 1-100 for each player. Whoever rolls the highest number can loot the chest, get the item, etc.
<P>
<B>Healing</B><BR>
If you have healing and a party member needs it, heal them. If you were unable to heal them, make sure you say sorry if you were the primary
healer in the group. You can't always prevent people from dying, but you can at least let them know that you tried your best.
<P>
<B>Returning to the Party</B><BR>
When you return to your party's location after going somewhere else, make sure you don't get followed by a "train", or bring monsters with you that may
attack them. This offends players greatly. If you need assistance getting to your party's location, ask them for help to clear the way.
<P>
<B>Buff up your Party</B><BR>
If you have Buff spells that are beneficial to your party, pass them out. Ask party members to inform you when the buffs have worn off.
If players know you have these buffs but you don't use them, they might not respect your ability to play your class. On the other hand,
if you buff well they will say "Hey, that person was a great player."
<P>
<B>Don't Wake Up More Monsters</B><BR>
While you are fighting, scan around you for patrolling monsters. If one comes near, make sure you don't set it off so that it
joins into your current battle.
<P>
<B>Professions</B><BR>
If there are multiple people with the same Profession, share the items or resource
nodes between you. Don't be greedy.
<P>
<B>Go the Extra Mile</B><BR>
Whenever possible, go the extra mile to do things for your party. Play your best. Do whatever you can to help party members.
If you impress them, they may look back on you with good memories. This can build good relations for the future.
<P>
