<A href="../../../shared/wow-com/images/basics/smallpets/ss01a.jpg">parrots</A> (several types),
<A href="../../../shared/wow-com/images/basics/smallpets/ss01a.jpg">dragon whelplings</A> (green/crimson/black),
<A href="../../../shared/wow-com/images/basics/smallpets/ss01a.jpg">snakes</A>,
<A href="../../../shared/wow-com/images/basics/smallpets/ss01a.jpg">spiders</A>,
