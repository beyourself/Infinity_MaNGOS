What happens when player characters die? They become ghosts! This page explains the death penalty system in World of Warcraft.
World of Warcraft is a work in progress. When a player dies, he or she will be prompted with a dialogue box to "Release Spirit." Pressing this button will instantly transport the player to the nearest graveyard. The player will then be transformed into a ghost. On death, all equipped items will lose 10% durability.
<p>
<b>As a ghost, you have three options:</b>
<p>
<ol>
<li>You can return to your corpse and recover it. There is no further penalty if you choose this option.
<p>
<a href="../../../shared/wow-com/images/basics/death/ss03.jpg"><img src="../../../shared/wow-com/images/basics/death/ss03-thumb.jpg" width="202" height="143" align="right" border="0" title="My bad, guys. My bad."></a>
<li>You can have a fellow player (Paladin, Shaman, Priest) resurrect you, or
use a Warlock's Soulstone, which will bring you back to life next to
that player.
There are also items in the game that allow players of any class to resurrect you. You will
not lose any experience when resurrected by another player, but you will have "resurrection sickness" for a few minutes.
Players with resurrection sickness will have a temporary reduction in character attributes. Also, you can be resurrected by
other players even after you have released your spirit back to the graveyard.
<p>
<li>You can talk to a spirit healer NPC (currently located at graveyards) to immediately resurrect yourself at the spirit healer's location. If you choose this option all items (equipped and inventory) will take 25% durability loss and the character will gain resurrection sickness for a duration that scale according to level. The length of resurrection sickness suffered from using the Spirit Healer is set to a maximum of 10 minutes.
</ol>
<p>
<b>Being a Ghost</b>
<p>
Players have special characteristics while in ghost form:
<p>
<ul>
<LI>Players appear in a black & white world while dead. <p>
<li>Players are ethereal in appearance.<p>
<li>In ghost form, players are unable to interact with most things in the world.<p>
<li>Ghosts cannot be seen by, nor see living players and most creatures, other than those creatures that are in the immediate area
surrounding their corpses.<p>
<li>Ghosts can see other ghosts.<p>
<li>Ghosts can run faster, which makes it easier to return to where they died. <p>
<li>A few special creatures may be able to see ghosts (these will be apparent to players), and they can attack players in ghost form.<p>
<li>If players do die in ghost form, they will be transported back to the most recent graveyard that they were at (not the graveyard
nearest to where their ghosts just died, if different). <p>
<li>"Public chat" is not available to ghosts, but "private chat" such as /tells, /guild, and /party chat are still allowed.<p>
<LI>Players can't use ghost form as a means to travel to far off locations.Having a spirit healer resurrect you in a graveyard
other than the one you appeared at when you died will always teleport you back to that initial graveyard.<p>
<LI>Spirit healers are ghosts and can only be seen by players in ghost form.<p>
<LI>Player corpses turn into race-specific skeletons as they decay.<p>
</ul>
