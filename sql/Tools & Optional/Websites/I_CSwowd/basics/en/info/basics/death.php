<b>Finding Your Corpse</b>
<br>
When you are a ghost, it is easy to find your corpse. An arrow is placed on your minimap, directing you to your corpse.
Once you get near the corpse, a special icon also appears on the minimap to show you the corpse's exact location.
<p>
<b>Reviving as a Ghost</b>
<br>
Once you approach your corpse, you will see any monsters that are near it. You do not have to reincorporate directly on
the spot where your corpse is. As you come closer to your corpse, a dialogue box will pop up, asking if you are ready
to be revived. You'll then be able to pick a safe location near your corpse to come back to life.
Players that revive will have 50% health and mana.
<p>
<b>Dungeons</b>
<br>
If you die in an instanced portion of a dungeon, crossing the instance line into that dungeon as a ghost counts as reclaiming your corpse. You don't have to go all the way back in to find it, and it doesn't matter if your particular instance no longer exists. You cannot explore the dungeon in ghost form. Also, your ghost may not enter an instanced dungeon in which you do not have an active corpse.
<p>
<b>Other Information</b>
<br>
<UL>
<LI>Players do not need to manually bind at the graveyard. The game selects the nearest graveyard automatically.
<LI>Bodies will start to decompose after a player is resurrected or revived.
</UL>
