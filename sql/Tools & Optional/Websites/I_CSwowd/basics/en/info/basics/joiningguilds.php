<div class = "postBody">
Guilds offer many benefits including free items, opportunities for groups, access
to Professions masters, quest items, and readily available Professions ingredients through gathering guild members. You may discover that a guild greatly enhances your gameplay experience. You can meet friends, share adventures, and find people to protect you if you fight in faction versus faction combat.
Typically, players in good guilds can go places and do things that players in poor guilds or no guild can't. This is especially the case at maximum character level (60), where the dungeons become very challenging.
<P>
Keep in mind that guilds are run by players and not Blizzard. The quality of the guild and
the guild experience depends entirely on the players in that guild. Guilds can be a grab bag where
you never know what you'll get. Every guild is different.
<P>
Finding a guild can be very easy. People often sit in town asking anyone to join their guild because guilds require a minimum number of members to create. However, finding a quality guild with quality members and leadership can be a difficult task. Don't be afraid to shop around. You can join one guild, try it out, then leave and join another guild until you find one you like.
<P>
<B>Guild Size</B><BR>
Guild size can be a big factor in choosing a guild. Guilds that invite too many people, that
don't pick quality members, and invite new people all the time are generally less organized and less powerful. However, it can be much easier to find people to group with in a larger guild.
Smaller guilds are more personal. One complaint players have about large guilds is that you often run into guild members you don't know ("I don't know all these people."). With a smaller guild there is much more opportunity to get to know each member. For that reason, some players prefer a small guild.
Ultimately, the "Large vs. Small" debate depends on the guild and the members that join. There are large guilds that still remain very good. Leadership and recruitment policies are the main factors in determining how a guild turns out.
<P>
<B>Grouping</B><BR>
Grouping can be a good way to find a guild. Team up with people and play with them for a while.
Make friends with them and play together for several days. If they get to like you, they will usually want you in their guild so that you'll continue to play with them. After helping them out, inquire about their guild. Perhaps they may help you join.
<P>
<B>Reputation</B><BR>
Do your own research into a specific guild. As guilds become powerful, well known, and popular, rumors about them begin to appear. Often when a guild member does something to offend a player, that offended player starts spreading stories about the entire guild.Jealous players make untrue generalizations about the guild and its members. Make sure you find out the real story on your own with whatever evidence you can find such as screenshots or web reports. Sometimes a guild may be good and is just the victim of a bad reputation. Guilds can be very jealous or hostile toward each other and thus make up rumors to hurt the reputation of the other guild. Or, perhaps, the reputation about that guild is true. Maybe the guild is full of troublemakers. Find out yourself.
<P>
<B>Visit Guild Web Sites</B><BR>
Visit the World of Warcraft.com Guild Recruitment forums. They contain links to various guild sites. You can visit their web sites, see what activities they engage in, and check out their rules. Quality guilds often have very good web sites. This can be a good indicator that this might be a good guild to join. You can also ask guild members if they have a web site.
<P>
<B>Interview Guild Members</B><BR>
If you find a guild you like, interview some of their guild members. Ask them how things are in their
guild and if they enjoy it. If you're interested in joining, ask them who you should talk to. Be very
careful when messaging guild members because their impression of you is very important. If you offend
the guild member, they may spread the word about your offensive request (via guild chat), and that
could harm your chances of joining. Be on your best behavior. Asking to join one of the more powerful
guild can actually be similar to a job interview in real life.
<P>
<B>Positive Guild Behavior</B><BR>
Look for guilds with:
<ul>
<B>Good Grouping</B><BR>
One of the most important things you need is the ability to find groups to help you complete your quests. That's why most people join guilds.
<P>
<B>Have good leadership</B><BR>
A guild needs clear leadership and strong rules. Members need someone who can resolve conflicts and give direction to members.
<P>
<B>Generous</B><BR>
Look for guilds whose members are willing to share items and help you get things. You shouldn't expect free handouts for no effort. A quality guild will offer items and discounts
to their members.
<P>
<B>Spam</B><BR>
Guild chat is a very important part of a guild. Guild members typically use this method to communicate. Find a guild that has good guild chat rules and enforces them. Otherwise, you might have to put up with a lot of garbage text while you play. There are advanced chat options that allow you to turn off guild chat.
<P>
<B>Drama</B><BR>
Item fights and guild members fighting with each other are common problems in guilds. If the guild has good leaders and quality members, these problems can be reduced or dealt with when they arise. Sometimes players quit a guild because of "too much drama." Games are supposed to be fun. If time is spent fighting, that's not very fun. Don't be afraid to leave a guild. There will always be others to join and you can always create your own.
<P>
<B>Mature Leaders</B><BR>
When players get power such as the ability to remove guild members, demote them, and tell them what to do, they sometimes abuse that power. Find a guild whose leaders are responsible with the power they have.
</ul>
<B>Conclusion</B><BR>
Do some research, shop around, and keep trying until you find a good guild. Don't give up. There are good guilds out there. You just have to find them. Remember, you can always create your own guild if you want and make one that follows your own requirements.
