<div class = "postBody">
Completing quests is the easiest way to level up, get money, receive trade skill recipes/components, earn equipment, and more.
It is best to spend the majority of your time focusing on quests if you want to level up and become more powerful.
Be sure to grab every quest you can find, and feel free to jump on to your next quest as soon as you've
finished one. You can play however you want, but remember, it's all about having fun!
<P>
Hit "L" in order to bring up your quest log. From there you can, see the quests you have, and read about
what they require you to do, and the rewards you will receive. Quests are divided up by regions for your convenience.
<P>
-A yellow "!" above an NPC's head means they have a quest for you now.<BR>
-A gray "!" above an NPC's head means that they will have a quest for you once you reach a higher level.
So go level up, then come back.<BR>
-A yellow "?" above an NPC's head means they are expecting you to return to them with completed items for a
quest, or news about a quest's completion.<BR>
-A blue "?" above an NPC's head indicates a quest is repeatable.
<P>
<B>Quest Colors</B><BR>
Quests are listed by color to indicate how the quest stacks up vs. your current level. Easier quests will mean you get less experience.
<ul>
<FONT color=gray><B>Gray quests</B></FONT> - Trivial. You probably shouldn't bother taking it.<BR>
<FONT color=00c400><B>Green quests</B></FONT> - Easy<BR>
<FONT color=yellow><B>Yellow quests</B></FONT> - Normal<BR>
<FONT color=orange><B>Orange quests</B></FONT> - Hard<BR>
<FONT color=red><B>Red quests</B></FONT> - Very Hard<BR>
"Dungeon" - Takes you into dungeons.<BR>
"Elite" - you'll probably need a group in order to complete this quest.<BR>
"Raid" - you will need a large raid group to beat this quest.
</ul>
Currently, you get full experience for everything but gray quests. Gray means that you'll get less experience for every
level that the quest has been gray. The amount of experience you get for completing a quest will drop as you get to higher
levels. This prevents people from going back and doing a bunch of super easy quests with high level characters.
<P>
<B>Quest Tracking</B><BR>
You can now have quest completion criteria for the quests you are currently on display on the game screen as well as in the quest log. If you shift-click on a quest in the quest log, it will display the completion criteria on the main game screen underneath the mini-map.
<P>
<B>Class-Specific Quests</B><BR>
Some quests are only available to certain classes. Try to do them because they will be
valuable for your character. Some class upgrades require you to complete class-specific quests.
<P>
<B>Trade Skill Quests</B><BR>
There are some quests available to players that have certain trade skills, such
as, Blacksmith quests. These quests are only available to those players with
the specific Profession. Players must complete quests to gain Artisan levels of Fishing, Cooking, and First Aid.
<P>
<B>Quest Items</B><BR>
Quest rewards are labeled as "Bind on Acquire".Quest rewards can still be equipped or sold to a merchant, but you
can't sell or give them to other players.This was implemented to deter these items from flooding the
game economy, which was an issue prior to this rule.
<P>
<B>Party Quests</B><BR>
You can tell how many other players in your group have the same quest you do. When you are in a group, it will display the number of other players in the group who have the quest in parenthesis to the left of the quest name. If you mouse over the quest name, it will display the names of the other players in the party who have the quest in the bottom right hand corner of the screen.
<P>
<B>Quest Sharing</B><BR>
You can share your quests with your entire party. In order to utilize this option, you need to open your quest log, select a quest, and press the "Share Quest" button. All members of your party will now be presented with the quest like you were the quest giver NPC.<BR><BR>
Some restrictions: <BR>
<UL>
<LI> You can only share with party members.
<LI> Party members must be nearby when you share.
<LI> Party members must meet the pre-requisites of the quest itself. For example, if you are on step 2 of a quest and your friend is still on step 1, you could not give him a "short-cut" to step 2.
<LI> Quests generated from items cannot be shared.
</UL>
<P>
<B>Party Quest Share Information</B><BR>
If you mouse over a quest that multiple party members have, it will display a mouseover tooltip indicating what nearby party member are on that quest. Also, to the left of the quest name in your quest log it will list a number in parenthesis indicating how many other players in your party have that quest.
<P>
<B>Quest Text Speed</B><BR>
You can disable the slow scrolling of text when you are receiving a new quest.
<P>
<B>Minimap</B><BR>
If you select a quest in your log, often the quest NPC you need to talk to will now show up on your minimap if you are near them.
<P>
<b>Quest Experience to Gold Conversion at Level 60</b><br>
Previously, quest experience was wasted if one completed a quest at level 60. In this patch, any quests done at maximum level will have their experience reward converted to a healthy amount of gold, thus adding additional incentive to completing those quests in your log once you hit 60.
<p>
<B>Other Information</B><BR>
<ul>
<ul>
<li>You will receive credit for killing a monster even if you die during battle.
<li>You're not expected to do every quest. There are a LOT of quests out there, so don't feel bad if you miss out on a couple. After you reach a certain level, it is best to move on to the next area and focus on more powerful quests for greater experience and rewards. You're certainly not prohibited from doing every quest you find, so if you so desire, you can go ahead and complete every quest in a region for the fun of it and to learn more about the World of Warcraft story.
<li>Keep in mind that there is a limit of 20 quests that you can currently hold in your quest log. Once you have reached that limit you must abandon a quest to add another new quest to your quest log. This limit is required for technical reasons and for balance reasons.
<li>You can abandon a quest and go back and get it again later if you want. When you abandon a quest, you only abandon the step that you are on. In order to pick up a multi-part quest, you will need to go to the NPC who gave you the exact part of the quest you abandoned.
<li>If you do not like the quest reward, or cannot use it, don't worry. You still get good experience for completing a quest and you can sell your quest reward. You can even use the reward money from quests you have completed to buy something you do want.
<li>Most quest items only drop if you have the corresponding quest in your quest log. If you do not have the quest, you will not find the quest items. Some quest items that are not marked as "quest items" can be traded. 
<li>Parties: Once a player has collected all of their required quest items, quest items that drop from this point forward will be open to looting by party members that are still collecting the quest items. This means that now that you've got all your items, your friends can now loot them if any more drop, even when it is your turn to loot. This can create a situation where two party members (on round robin) can loot the same body. The person who has finished collecting the quest items will loot money and items, while another player in the party can loot the quest item from the same corpse.
<li>You will get bounty (kill) credit if you die and do not release your spirit. For example, if you are on the Leaders of the Fang Quest and die while fighting a Fang Leader, you will still get credit for the kill if your party kills the leader before you release your spirit.
<li>The completion of a quest may result in a reputation adjustment of a faction or a group of factions.
</ul>
</ul>
</ul>
