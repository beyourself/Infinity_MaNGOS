<div class = "postBody">
Crowd Control can be described as the process of efficiently dealing with multiple monsters attacking you or your party
at the same time. This is the case whether the attack is expected or unexpected. Essentially this
means disabling extra monsters and fighting just one monster. Battles can be much safer because you're
fighting a smaller number of monsters. For example, you can pull two monsters, disable one, and only fight
one monster at a time. Also, you could pull four monsters and disable two.
<P>
Some packs of monsters are too numerous to use crowd control, but generally it is an important skill to utilize
when fighting. Groups that are good at crowd control can typically accomplish much more in a shorter amount
of time than those that have no crowd control ability.
<P>
<B>How to Control a Crowd</B><BR>
First, identify party members with specific spells or abilities that can disable monsters. There are many
of these immobilization abilities.We will identify <U>a few of them</U> for sample purposes.
<ul>
<B>Mage</B> - <B>Polymorph</B> - The Mage polymorphs a monster while the party fights the other monsters.<BR>
<B>Rogue</B> - <B>Sap</B> - Rogue disables a humanoid monster. You must sneak up and Sap the monster before it
is attacked.<BR>
<B>Druid</B> - <B>Entangling Roots</B> - Druid entangles one monster while the party fights additional monsters.<BR>
<B>Warlock</B> - <B>Fear</B> - Sends a monster running away. This is sometimes not the best to use because they can bring back help.<BR>
<B>Warlock</B> - <B>Banish</B> - Disables a monster for a short amount of time.<BR>
<B>Priest</B> - <B>Psychic Scream</b> - Similar to Fear with restrictions.<BR>
<B>Priest</B> - <B>Shackle Undead</B> - Priest holds an undead in place while party fights other monsters.<BR>
<B>Item</B> - Engineers Make/Sell: Target Dummies.<BR>
</ul>
<B>Put the Abilities into Action</B><BR>
Inform everyone in your party that you'll be using your crowd control abilities. Then tell them what to do
and when. You may have to be the person who pulls for the party depending on which ability is used.
The procedure is for you to cast your spell, or use your ability, on the monster you don't wish to fight.
This may bring an additional monster(s) near the disabled enemy.
The other monster will typically come running when you disable its partner, if both monsters are aggressive.
<P>
Make sure your party members do not attack the disabled monster, which will "wake them up". This can be difficult
to do if your party members are not experienced players. Thus it may require some
training. Pay attention to which player "woke up the monster", point them out, and chastise them.
Remind party members that 'area of effect' spells will wake up sleeping monsters, so they should
be used with caution, if at all.
<P>
Remind party members not to cast a DoT (damage-over-time) on a monster. Otherwise you won't be able to sleep
them.
<P>
When fighting higher level monsters you may have a much more difficult time disabling the monster due to spell resists.
<P>
<B>Drag the Awake Monster</B><BR>
Once one of the monsters is disabled, drag the other attacking monster away from its sleeping counterpart.
That way, it will be clear to everyone in the party which monster they should be attacking.
It is also then clear to players that the other monster is disabled, and it will be easier to notice when the
immobilized monster has woken up. To drag the attacking monster, have the person who has attracted its
attention back away from the other disabled enemy. The monster will then follow the player away from
its immobilized counterpart. You might have to explain this concept to players who have never seen
this method used before.
<P>
<B>Protect the Player doing Crowd Control</B><BR>
When a player disables a monster, they typically generate a lot of hate or aggression from the monster
being disabled. Once the monster "wakes up", it typically runs after the person that disabled
it. Tanks need to be aware of this and should taunt the offending monster off the person once it "wakes up".
The player being attacked also needs to inform the party that a monster is on them, as is
standard procedure for non-tank characters.
<P>
<B>Keys to Good Crowd Control</B>
<UL>
<LI>Having and properly using abilities that disable monsters
<LI>Disabling the correct or best monster
<LI>Having a party that does not attack disabled monsters
<LI>Having tanks in the party that can taunt the monster once it wakes up
<LI> Communication with party members
</UL>
<B>Other Uses for Crowd Control</B>
<UL>
<LI><B>Traveling</B>:Crowd control abilities are also useful for traveling.
Simply disable the monster to allow you to travel without fighting.
<LI><B>Escape</B>: If you run into unexpected monsters, disable them. This can allow you to escape a battle that might not be possible without a crowd control ability.
<LI><B>Duels</B>: Disable the other player while you heal or regenerate mana.
</UL>
