<div class =" postBody"><img src="../../../../shared/wow-com/images/smallcaps/plain/a.gif" align="left"> faction is a group of people that share same interests. Many of the factions in Azeroth are not involved in the on-going tensions between the Horde and Alliance and, thus, are generally open to members of both sides. These "non-aligned" factions can provide you with information, equipment, and experience. Other factions are actively involved in the disputes between Alliance and Horde and are thus open only to members of one of those two sides. These factions are often attached to the various <a href="../battlegrounds/index.php">battlegrounds</a>.
<br><br>
You change your status within a faction by gaining (or losing) reputation. Additionnal information on reputation is found <a href="../reputation.php">here</a>.
<br>
