<a name="battlegrounds"></a>
<img src="../../../../shared/wow-com/images/smallcaps/plain/b.gif" align="left">attlegrounds factions are not only attached to either the Horde or the Alliance; they are also attached to their specific battleground. In order to gain reputation with a particular battleground faction, you will have to complete quests inside that faction's preferred battleground and turn in tokens from that battleground. For instance, the Frostwolf Clan will only accept tokens from members of the Horde who have gone into Alterac Valley.
<p>
<h3 class="blood">Alterac Valley Factions</h3>
After Thrall's triumphant uniting of the clans, the <b>Frostwolves</b>, now led by the Orc Shaman Drek'Thar, chose to remain in the valley they had for so long called their home. In recent times, however, the relative peace of the Frostwolves has been challenged by the arrival of the Dwarven Stormpike Expedition.
<p>
The <b>Stormpikes</b> have set up residence in the valley to search for natural resources and ancient relics. Despite their intentions, the Dwarven presence has sparked heated conflict with the Frostwolf Orcs to the south, who have vowed to drive the interlopers from their lands.
<br><li>More information on rewards <a href="../battlegrounds/rewards-alterac.php">here</a>.
<p>
<h3 class="blood">Arathi Basin Factions</h3>
Arathi Basin, located in Arathi Highlands, is a fast and exciting Battleground. The Basin itself is rich with resources and coveted by both the Horde and the Alliance. The <b>Forsaken Defilers</b> and the <b>League of Arathor</b> have arrived at Arathi Basin to wage war over these natural resources and claim them on behalf of their respective sides.
<br><li>More information on rewards <a href="../battlegrounds/rewards-arathi.php">here</a>.
<p>
<h3 class="blood">Warsong Gulch Factions</h3>
Warsong Gulch is the battleground of the <b>Warsong Outriders</b> and the <b>Silverwing Sentinels</b>. The two factions wage war against each other from their respective holds of Warsong Lumber Mill and Silverwing Hold. The goal of their war is to lay claim to Warsong Gulch by capturing their enemy's flag and returing it to their own base. By aiding them in their struggle, adventurers can get various rewards, as well as honor and the esteem of one of the two factions.
<br><li>More information on rewards <a href="../battlegrounds/rewards-warsong.php">here</a>.
