<B>How many people must gather for a Battleground before I can enter?</B><BR>
For Warsong Gulch, eight players from both factions must be present in the queue at the same time. Since Alterac Valley is designed for full-scale epic battles, at least thirty players are required from both sides in order for an instance to begin.
<P>
<B>Will joining the queue alone using the "First Available" option get me into Battlegrounds more quickly than entering in a group with the "Join as Group" option?</B><BR>
Yes. Think of it as being seated at a restaurant. If you show up at a restaurant with ten people, the staff must wait until there's room to seat you. This may take some time, depending on the crowd. If you request a table for one, however, you'll be seated much more quickly, as there's more room for just one person than ten people.
<P>
The same holds true for Battlegrounds. For example, if 24 players are waiting to get into Warsong Gulch and they form four raid groups of six players each, not one instance will start because none of them can fit. Unless you absolutely have to be with the people in your group, join solo. You'll get in faster.
<P>
<B>How can my entire party or group of friends join a Battleground together?</B><BR>
You can join the Battleground as a party or raid by forming the group and having the party or raid leader enter the queue by clicking the "Join as Group" button.
<P>
<B>If I join a group that has already been placed in a queue, will I be placed in their queue along with them?</B><BR>
No. The group leader will have to re-enter the queue in order for everyone in the party to be placed in the queue.
<P>
<B>Do I need to be in front of the Battleground instance while waiting in the queue?</B><BR>
No. Roam around and play! You can even run instances while in a queue -- you'll be ported straight into the Battleground when the time comes.
<P>
<B>Are there plans to implement alternate methods for joining Battlegrounds?</B><BR>
The next patch will introduce a new way to enter the Battleground queues. Battlemasters! They are located in some of the cities and right-clicking on them places your character in a Battleground queue just as if you had touched that Battleground's entrance portal.
<P>
<B>What happens if I'm dead or in the middle of flying between two locations when my time to enter comes up?</B><BR>
Being dead or locked into a flight path will not hinder your ability to enter Battlegrounds once you're in a queue. Simply accept the invitation to join and prepare to be ported.
<P>
<B>What if I'm in an instance when the Battleground time comes up?</B><BR>
If you are in an instance when it is your turn to enter the Battleground, you will still have the option to either port to the Battleground or remain in the instance.
<P>
<B>Why does typing /afk kick me out of the instance?</B><BR>
Battlegrounds are a fast-moving PvP environment where it's important for everyone involved to remain active. Being idle or away from the keyboard can certainly hurt the rest of the players on your team by hindering their ability to complete important objectives.
<P>
<B>Do the diminishing returns rules for contribution on honor kills still apply in Battlegrounds?</B><BR>
Yes. Defeating the same opponent repeatedly within a short amount of time will result in less contribution, until eventually none is received.
<P>
<B>I want to tell my friends to join the same instance as me. How do I find this information to ensure we all join the same one?</B><BR>
When you attempt to enter the Battlegrounds portal, an interface box appears listing each available instance with a number.
<P>
<B>When honor is tallied each week, are rankings from the Horde and Alliance independent of each other?</B><BR>
Yes!
<P>
<B>How do insignias work?</B><BR>
Your character carries a team insignia on it that can be looted by enemy players when you die. When a player
removes your insignia from your body, you can no longer resurrect at your corpse. In this situation, you must
resurrect at a friendly graveyard.
<P>
<B>Is there a durability loss for dying to anything in Battlegrounds?</B><BR>
There is no durability loss for dying to a player or NPC in a Battleground, only to other forms of death such
as falling damage. However, regular combat durability loss from normal use of your equipment is applied in the
Battlegrounds.
