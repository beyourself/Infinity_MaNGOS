<B>Battlemasters</B><BR>
Located in all of the major cities, right-clicking on a battlemaster will allow your character to enter a battleground queue just like you normally would if you touched that battleground's entrance portal. The functionality is exactly the same, so when it's your character's time to enter the chosen battleground, you will be teleported directly in. Some of the Battlemasters are listed below:
<ul>
<li>Glordrum Steelbeard stands in the Hall of Arms in Ironforge, serving as the Alliance battlemaster for Alterac Valley.
<li>Aethalas of the Silverwing Sentinels stands ready in Darnassus, next to the warrior trainers, to send Alliance characters into Warsong Gulch.
<li>Brakgul Deathbringer is in the Hall of the Brave, located in Orgrimmar's Valley of Honor. He serves as the Horde's battlemaster for Warsong Gulch.
<li>Kartra Bloodsnarl will send you to bring glory to the Horde in Alterac Valley. Guards in their respective cities can give you directions on how to find them.
</ul>
Characters that use the Battlemaster to enter a Battleground will port back to that Battlemaster when they leave the Battleground for any reason.
<P>
<B>Battleground Holidays</B><BR>
Battleground "holidays" exist for Warsong Gulch, Alterac Valley and Arathi Basin. Holidays occur during most weekends, starting on Thursday night at midnight and continuing until Tuesday morning. During a holiday, emmissaries from that Battleground will be found in the major cities, and honor/faction rewards for performing objectives in that battleground are increased.
<P>
<B>While in a Battleground</B><BR>
<UL>
<LI>The score screen displays information on the status of the Battleground. You can bring up the score screen while in a Battleground by pressing shift+spacebar. See our score screen guide pages for details on the information listed on the Alterac Valley and Warsong Gulch score screens.
<LI>Players that go AFK (automatically or by manually inputting the /afk command) while in a Battleground will be instantly teleported to the Battleground entrance.
<LI>You can open the world map to see the locations of all the players on your side who are in the Battleground.
<LI>If you are disconnected while in a Battleground, you have a short time to safely log back in and resume adventuring in that Battleground.
<LI>Logging into a Battleground that's closed will teleport you to the entrance (dead characters will be resurrected as well).
</UL>
<B>Battlegrounds shutdown in the following circumstances:</B>
<UL>
<LI>One of the Battleground's victory conditions is met. When this occurs, a score screen will appear and the
Battleground will close in two minutes.
<LI>One faction's total amount of players in the Battleground slips below the minimum allowed for that Battleground.
A countdown will then ensue and the faction that has the minimum number of players or more when the countdown ends
will be declared the winner by default. The minimum number of players per faction is 10 in Alterac Valley, and five
in Warsong Gulch.
<LI>If both factions slip below the minimum number of players allowed for that Battleground, the map will close after
a countdown with no winner.
</UL>
<B>When a battleground is closed, the following happens:</B>
<UL>
<LI>All players are prevented from moving or casting.
<LI>All players are healed.
<LI>The score screen is displayed with a Leave Battleground option.
<LI>Players are able to view the score screen or leave the Battleground.
</UL>
