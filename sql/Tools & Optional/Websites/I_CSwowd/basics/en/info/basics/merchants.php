<P>
<img src="../../../shared/wow-com/images/characters/ingame/goblin-banker.jpg" align="right"><img src="../../../shared/wow-com/images/smallcaps/plain/t.gif" align="left">he world is full of merchants waiting to sell and buy items. Each town has merchants that will sell armor,
weapons, food, drinks, trade skill components, and more. To see what an NPC has to sell, right-click on it.
Merchants also list what type of items they will sell below their name. Be sure to check every merchant you
find to see what special items they might be selling.
<br><img src="../../../shared/wow-com/images/layout/pixel.gif" height="20"><br>
<h1 class =" title">Limited Items</h2>
Some NPCs will sell limited supplies of special/rare items every so often. These items have a number over the item indicating how many of that item that merchant has. Once that number is bought, the item is gone, and who knows when the merchant will get more! Find out what merchants sell limited items and check them every so often.
<br><img src="../../../shared/wow-com/images/layout/pixel.gif" height="20"><br>
<h1 class =" title">Buying</h1>
Merchants charge a lot of money for their items. You could even say they are overpriced. You might find better deals if you buy from other players. But if you really want something and the merchant is the only one that sells it, buy it!
<br><img src="../../../shared/wow-com/images/layout/pixel.gif" height="20"><br>
<h1 class =" title">Pages</h1>
Sometimes merchants have more items than are currently shown. Use the arrow buttons at the bottom to browse additional pages of items.
<br><img src="../../../shared/wow-com/images/layout/pixel.gif" height="20"><br>
<h1 class =" title">Selling</h1>
<a href="../../../shared/wow-com/images/basics/merchants/ss01.jpg"><img src="../../../shared/wow-com/images/basics/merchants/ss01-thumb.jpg" border="0" align="right"></a>The merchants will pay a very low price for items. Whenever possible, sell your items to other players instead for more money. But you'll
have to figure out which items you can sell to players and which items you'll have to sell to merchants.
<br><img src ="../../../shared/wow-com/images/layout/pixel.gif" height =" 20"><br>
<h1 class =" title">Traveling Merchants</h1>
Some merchants make their rounds around the regions in the world. If you see a traveling salesman, stop them and see what they have to sell. It might be very rare!
<br><img src="../../../shared/wow-com/images/layout/pixel.gif" height="20"><br>
<h1 class =" title">Recipes/Training</h1>
<a href="../../../shared/wow-com/images/basics/merchants/ss02.jpg"><img src="../../../shared/wow-com/images/basics/merchants/ss02-thumb.jpg" border="0" align ="right"></a>Some merchants sell rare recipes or trade skill training. Be on the lookout for them.
<br><img src="../../../shared/wow-com/images/layout/pixel.gif" height="20"><br>
<h1 class =" title">Buyback</h1>
If you accidentally sell an item you had not intended, you can now buy
it back for the same price at which it was sold.
