<B>I bought an item, how do I get it?</B><BR>
The item will be delivered to you in the mail. Go check your mailbox.
<P>
<B>How do I put an item up for Auction?</B><BR>
Click on the Auctioneer, then click on the "Auction" tab. Drag the item you wish to auction into the
