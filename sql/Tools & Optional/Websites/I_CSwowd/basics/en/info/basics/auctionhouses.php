<div class = "postBody">
<img src="../../../shared/wow-com/images/smallcaps/plain/a.gif" align="left">uctioneer NPCs can be found in in Orgrimmar, Undercity, and Thunder Bluff for Horde players. Alliance players will find Auction Houses in Ironforge, Stormwind City, and Darnassus. These Auction Houses are linked together for each faction. Horde and Alliance players cannot buy or sell items to each other unless they use the neutral Auction Houses listed below. Ask a guard for directions.
<P>
The three non-faction based Auction Houses are located in Gadgetzan, Booty Bay, and Everlook.
<P>
Sellers are able to place items up for auction, set a starting price, set the auction duration, and
include a buyout price by speaking with an Auctioneer.
<P>
<table border=0 cellpadding=0 cellspacing=0 align=right><tr><td><a href="../../../shared/wow-com/images/basics/auctionhouses/ss01.jpg"><img src="../../../shared/wow-com/images/basics/auctionhouses/ss01-thumb.jpg" border=0></a></td><td><img src="../../../shared/wow-com/images/layout/pixel.gif" width=39></td></tr></table>
All auctions will be initially charged a deposit fee. The deposit fee is refunded upon the successful
sale of the item.All successful sales are charged an auction consignment fee which is extracted
directly from the final sale price.All fees are higher in Gadgetzan.
<P>
Buyers are able to search through items up for auction, place bids, and purchase items that include
buyout prices by speaking with the Auctioneer.Players are not able to place bids on items being
sold by a character on their own account.
<P>
All items won at an Auction House will be delivered through the <a href="mail.php">in-game mail system</a>.
<P>
<B>Browsing Auction Items</B><BR>
When you click on the Auctioneer you will see a blank list. You need to first search for what types of items you want to find. There are many possible custom search options including:
<ul>
<B>Name</B> - Name of a specific item you're looking for.<BR>
<B>Level Range</B> - Set the minimum and maximum level of the item. For example, if you're level 30,
try looking for items up to level 30.<BR><a href="../../../shared/wow-com/images/basics/auctionhouses/ss02.jpg"><img src="../../../shared/wow-com/images/basics/auctionhouses/ss02-thumb.jpg" align="right" border="0"></a>
<B>Useable Items</B> - Click this boxif you only want to see items that you can currently use. That way you
won't see Warrior items if you're a Mage.<BR>
<B>Filters</B><BR>
There are many filters and sub filters you apply to your searches. For example, if you click on Weapon and
press the Search button you will receive a list of Weapons for sale. You can dig deeper and select even more
specific items using the filters. If you don't select any filter it will show you a random selection of
items.
<ul>
<B>Weapon</B><BR>
<B>Armor</B><BR>
<B>Container</B> - Bags<BR>
<B>Consumable</b> - Things like Potions or Armor Kits<BR>
<B>Trade Goods</B> - Things like Gems and Metals like Bronze<BR>
<B>Projectile</B> - Ammo for Guns or Bows<BR>
<B>Quiver</B> - Bags to store Ammo<BR>
<B>Recipe</B> - Professions Recipes<BR>
<B>Reagent</B> - Things used for Professions Recipes<BR>
<B>Miscellaneous</B> - Other things people sell.
</ul>
</ul>
<B>Bidding on an Item (Buying)</B> <BR>
<ul>
Select the item you wish to bid on in the Browse window. The Quality/Name of the item will be listed with
Level (Lvl), Closes In (when the auction ends), High Bidder (current highest bidder), and Current Bid.
<P>
<B>Buyout Price</B><BR>
If the item has a Buyout price it will be listed below the bid price.
This is not the current bid, but instead how much you must pay to buy the item and end the auction now. If this price seems too
high, don't worry. The price won't reach that high unless you want to end the auction now or someone else decides
to "Buyout" the item.
<P>
<B>Auction Length</B><BR>
Check out how long the auction is running. If it's running an extremely long time, you may wish to avoid
bidding on that item. Otherwise you'll have to wait that long to get the item.
<P>
<B>Outbid!</B><BR>
If you are outbid on an auction you will receive a yellow text message. You will also receive your
bid money back in your mailbox. You will need to bid again if you want that item.
<P>
<B>Bid Tab</B><BR>
Select the Bid Tab to see the current status of the items you're bidding on. You will also receive yellow pop
up messages in the middle of the screen letting you know if you were outbid or if you have
won an auction.
</ul>
<B>Creating An Auction (Selling)</B>
<ul>
<B>Place Item in Auction Box</B><BR>
Click on the "Auctions" Tab. In the top left of that menu under "Auction Item" there is a box to place your item.
Drag an item you wish to sell from your backpack into this box.
<P>
<B>Set Starting Price</B><BR>
The auction house will suggest a starting price. You can adjust this by setting it higher or lower. In some
cases the suggested price will be much too low for the true value of the item, or it will
be set too high. As you buy and sell more items, you can determine a good starting price. If you don't know,
just pick the default suggested price. A low price will attract bidders who think they can get a deal
on the item.
<P><a href="../../../shared/wow-com/images/basics/auctionhouses/ss03.jpg"> <img src="../../../shared/wow-com/images/basics/auctionhouses/ss03-thumb.jpg" align="right" border="0"></a>
<B>Set Auction Duration</B><BR>
It's important to realize that the longer you set the Auction Duration, the more the deposit will be. If
you set an extremely long duration, the deposit price can be way more than the actual value of the item. If
your item sells, you will receive your deposit back. If no one buys the item, you'll lose your deposit. Generally
it's good to set a very short auction time (several hours or less) to reduce the risk. However, if you
don't set the auction duration long enough, people won't have a chance to see your item in time.
<P>
<B>Buyout Price (Optional)</B><BR>
If you want, you can set a buyout price. This is the price at which you will instantly sell an item to a person.
If you're selling a bag and think that 85 silver is a good price for it, you can set the buyout price
at 85 silver. However, be warned that if you set the buyout price too high, it might discourage people from
bidding because they think your buyout price is way too high. It's up to you if you want to use this
option or not. Some people really appreciate being able to instantly purchase an item.
<P>
<B>Auction Successful</B><BR>
If someone buys the item you are selling you'll receive a text message popup when the auction ends. Go to your
mailbox and you'll find a mail from the auction house which contains the money. It will be lower than the
sale price because of the auction house fee was taken out of the winnings.
<P>
<B>Auction Expired</B><BR>
If your item doesn't sell by the time the auction duration is up, your
auction will expire and you will lose your deposit money. The item you
put up for auction will be returned to you from the auction house through the mail. You have up to 29 days to
get it from your mailbox. Try putting it up for auction again at a reduced price or at another time. If you
think it won't be sold, sell it to a vendor instead or try the trade chat channel.
</ul>
<B>Auction House Tips</B>
<ul>
<B>Auction Length</B><BR>
The longer you set the auction to run, the more you'll have to pay for the deposit. You should try to set your auction for
as short of a time as possible. Otherwise the deposit can be VERY expensive. You could also lose the deposit if
the item doesn't sell.
<P>
<B>What to Sell?</B><BR>
If you think it's doubtful that another player will buy the item, don't put it up for auction. If you
do not sell the item you will lose your deposit. You can search the current items to see what types of
items are being sold and how much they are going for.
</ul>
<b>Additional Information</b><br>
<ul>
<li>Shift-click item info linking can be used in the auction interface.
<li>Minimum bid increment is set to 5% of the current sale price.
<li>Auction notifications are sent as system messages that can be filtered in the chat interface
<li>Each time a bid is made on an item, a small amount of time is added to the remaining auction duration.
<li>Recipes you already know will not show up in the auction listing if "Usable Items" is checked.
</ul>
<B>Common Questions</B>
<ul>
<B>Where do I find the Auction House?</B><BR>
NPCs with a bag sign icon run the
"auction house." Ask other players where to find them if you need help. They are typically located very
near the bank.
<P>
<B>If I put my item up for bid and it doesn't sell do I lose my deposit?</B><BR>
Yes. You will lose your deposit if the item doesn't sell.
