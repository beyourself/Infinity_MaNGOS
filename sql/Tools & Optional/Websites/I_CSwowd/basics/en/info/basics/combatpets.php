<img src="../../../shared/wow-com/images/characters/ingame/succubus.jpg" align="right" title="My number is 867-5309"><h1 class =" title">Common Pet Questions</h1>
<ul>
<B>Can I give items to my pet?</B><BR>
No.
<P>
<b>Can I name my pet?</b>
Hunters can name their pets. Warlocks cannot.
<P>
<B>Do pets take up extra experience?</B><BR>
No.
<P>
<b>What classes get pets?</b>
Warlocks and Hunters can get permanent pets. Priests can control humanoid monsters as a pet for a limited amount of time.
There are other ways to get pets. For example,Engineers can construct pets. There are items that players can find that
create a pet for a limited amount of time. Players can also buy non-combative pets that are purely for show.
<P>
</ul>
