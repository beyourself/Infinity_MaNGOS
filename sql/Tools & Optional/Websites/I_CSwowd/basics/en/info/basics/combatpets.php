<h1 class =" title">Pet Control</h1>
You need to manage the hate of the monster between you and your pet. If the monster is attacking your pet is about to kill it then you
need to heal your pet or get the monster to attack you instead. You can transfer the monster over to you by casting spells on the
monster that really make it angry. You will have to figure out which spells do that.
<P>
If the monster is too focused on you, you need to stop attacking it and use whatever pet abilities your pet has to make the monster angry
enough to fight it instead. Learning how to properly manage monster hate can take some time. The goal is to keep both you and your
pet alive until the end of combat.
<P>
If you're in a party and your pet is attracting too much attention, have it stop attacking until the monster focuses on someone else in the party. Then wait
a bit longer and have your pet rejoin the fight again. That way your pet can continue to attack and live while the monster is attacking someone
else.
<P>
When you give your pet commands it will cancel any spells they arecurrently casting.
<P>
