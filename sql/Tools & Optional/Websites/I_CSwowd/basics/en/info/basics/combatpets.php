<h1 class =" title">Pets</h1>
If you are a class that uses pets you should learn everything you can about them, including how to best control them.
<P>
<h1 class =" title">Summon Pet</h1>
Once you are allowed to have a pet you need to learn how to summon it. Typically, you need to first get training to receive your pet.
Once you do that, a new summon pet button will appear in your spells menu. Hunters have to convert a pet from the wild with their special ability.
Drag the summon pet button on to one of your action bar pages, and summon your pet. You will now see a new mini action bar that is used to control your pet. This is your pet action bar.
<P>
<h1 class =" title">Pet Action Bar</h1>
Pet users get a special, smaller action bar they can use to control their pet. You can drag abilities onto this action bar. Your pet bar disappears when your pet dies.
<P>
<h1 class =" title">Pet Training</h1>
You can gain new abilities for your pet by visiting a pet trainer for that specific pet.
If you don't know where to find one, ask more experienced players of that class. The trainers are around, but they may be difficult to find. Try visiting a new town.
<P>
<h1 class =" title">Pet Location</h1>
Your pet shows up on the mini-map. The mini-map is very useful for keeping track of how far away your pet is as you travel around the world.
<P>
<h1 class =" title">Pet Death</h1>
If your pet dies you may have to wait through a cooldown period before summoning it again.
<P>
If you get too far away from your pet, or if the pet gets stuck, it will eventually unsummon. You will
have to summon your pet again. Learn to lead your pet around so it doesn't fall behind. Another option
is to dismiss your pet, and then summon it again once you've reached your destination.
<P>
<img src="../../../shared/wow-com/images/characters/ingame/gnome-crying.jpg" align="right" title="Oh noes!!!!!!!111eleventyone"><h1 class =" title">Pet Healing</h1>
Your pet will heal after combat is over. Also, you can heal it yourself.
<P>
<h1 class =" title">Pet Buffs</h1>
You can buff up your pet! Give it a shot.
<P>
<h1 class =" title">Abandon Pet</h1>
Right-click on your pet's name bar to see additional pet options. You can dismiss a pet
if you no longer need it or wish to switch pets.
<P>
