Many quests can be finished more quickly and safely with the help of a good group. Groups allow you to go places that
you would otherwise not be able to go by yourself.<br />
<br />
<h3 class="title">Group finding Options</h3>
<P>
<B>Looking for Group Channel</B>:
Use the Looking for Group channel to list the quests you're looking for help on. Other people with the same
quests might want to team up and work together to complete the quests.
<P>
<OL>
<LI>Join the LookingForGroup chat channel
<LI>Open up your quest menu with "<B>L</B>".
<LI>Now look in your quest log for the quests you want help on.
<LI>Hit <B>enter</B> to open up yourchat enter/typing box.
<LI>Now type "<B>/4 Looking for Help on </B>" (with space).
<LI>Now select the quests you want help on and hold down the <B>shift</B> button and <B>left-click</B> on
the titles of the quests you want to post.
<LI>When you are done add any other text you wish to add and hit enter.
<LI>This will produce the following (varies depending on your quest): Looking for Group: Looking for a group for Scrounging, Stranglethorn Fever, Agmond's Fate, and An Ambassador of Evil. Please send a message if interested in teaming up!
</OL>
Quite often other people are also looking for help. They're in the same situation you are. If you
don't get a response, try again later. Do not spam any channels with your requests.<br />
<br />
<B>Who</B><BR>
Type <B>/who</B> or hit <B>O</B> and then click on the <B>Who tab</B>. At the top of the window you will find a check
box for "I would like to join a group". Place a check in this box. Now whenever people search for
players they will see "looking" under the party header next to your name. They may invite you to
join a group. You will likely find that asking in the general channel will get better results but
it doesn't hurt to try.><BR />
<BR />
<B>Ask For Groups in Popular Areas</B><BR>
Sometimes it's good to start asking for a group in areas where players hang out. Main cities
such as Stormwind are a good place to ask.<BR />
<BR />
<B>Guilds</B><BR>
Guilds are a great source of help. You should consider joining a guild because you might have an easier time
getting help and finding groups. This of course depends on the quality of the guild you join. If
it doesn't work out, leave the guild and find another one!<BR />
<BR />
<B>Meet Up On Site</B><BR>
Sometimes after traveling to a quest location you might find players already there. Ask them if they
want to team up. For collection quests (collect x skins) people might not want to team, but for other
quests, people will often like help. Give it a try. If you have two parties, see if you can merge
the parties into one group!<BR />
<BR />
<B>Finding More Party Members</B><BR>
Once you have several members in your party you can continue to ask for members in the general
channel (as long as it is not spammed) or you can use <B>the who system</b> to find out what players are
not grouped in the same zone. You can send a private message to those players and ask them if
they want to team up. Be aware that some players consider this rude and may not respond.
<P>
These tips should help you have an easier time finding groups. Party on!
