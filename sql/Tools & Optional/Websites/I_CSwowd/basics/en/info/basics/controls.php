<div class = "postBody">
<B>Basic Options:</B> Hit (ESC) to bring up the options menu, where you can adjust video and game settings,
and re-map your key-binding. You can see all of the keys in the game within the key-binding section.
<P>
<B>Movement:</B> Use the W, A, S, D keys to move around the world. Or, hold down both mouse buttons to walk
forward (moving the mouse at this point turns your character). The arrow keys will also move your character.
<P>
<B>Camera:</B> Hold down the left mouse button to look around the world. Hold down the right mouse button to
turn your character using the mouse. You can zoom the camera in and out using the mouse wheel or using the
Home and End keys.
<P>
<B>Interacting:</B> Left-click selects objects and NPCs in the game, while Right-click activates an object or talks to an NPC.
The mouse cursor is context-sensitive; if you move your mouse over an object that you can interact with, an icon will appear on your mouse cursor indicating the type of action that will occur if you Right-click (sword = attack, cog = manipulate, speech balloon = talk, etc.)
<P>
<B>Attacking:</B> To attack a creature, move within range (move to where you can see the attack cursor when
you mouse over the creature - a grayed out cursor means you are out of range) and right-click on the creature.
You will enter attack mode. Move your character next to the creature and you will automatically begin to attack.
In addition to right-clicking on a creature, you may also select the "attack" icon from the Action Bar.
<P>
<B>Action Bar:</B> The action bar at the bottom of the screen contains your spells, special abilities, or frequently used items like Food and Water (which give back health and mana respectively). You can activate these by either left-clicking on them, or by
typing the number corresponding to the slot of the spell or ability you wish to use. Mousing over the icons will also show you a tooltip containing the name of the spell or ability. You can also consult your spell or ability books (Spellbook & Ability button on the Action Bar) to get a full description of each ability or spell your character may have. You can re-arrange your Action Bar by clicking and dragging on the icons. You can also add new spells/abilities to this bar as you receive them by dragging them from your Spell or Ability book. You can also place items in the bar for easy activation.
<p>
Consumable items on the action bar will now always show the number remaining, even if there are none in your inventory.
<P>
<B>Grouping, Trading, and Other Character Interaction:</B> To invite someone to a group, select them by left-clicking on them, and then right-click on their portrait in the upper edge of the screen to bring up a menu of possible interactions with that character. One of these options is grouping. There are several other available interactions with characters, including inspecting them, challenging them to a duel, etc.
<P>
<B>Sound:</B> Control-S toggles sound effects. Control-M toggles music.
<P>
<B>Character Information:</B>
Click on the button in the Action Bar that contains the portrait of your character. This will bring up your Character Info panel which includes the Talent,
Reputation, and Skill panels.
<P>
<B>Leveling:</B> As you level in the game, you will gain talent points which you can immediately apply to your character by opening up the Character Information page and selecting the Talents tab. The Talents tab contains all your currently available talents and their costs in talent points.
<P>
<B>Gaining New Spells & Abilities:</B> New abilities and skills are not gained automatically. You must visit a trainer specific to your class to purchase them. There are other ways to gain spells and abilities, such as tomes found on monsters.
<P>
<B>Casting spells on yourself</B><BR>
If you want to target a spell on yourself, hold down the <B>alt</B> key and hit the hot key or button for the spell you want to
use. Just hit alt-3 (if that is the spell) to quickly cast a spell on yourself.
<P>
