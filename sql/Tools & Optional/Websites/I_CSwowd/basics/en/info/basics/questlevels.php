<b>Finding New Quests</b><BR>
If you're having a hard time finding quests appropriate for your level, there are several ways to locate some. Ask other
players for leads on quests that you have not yet done. The best place to ask about quests is in a group; talk to party
members around your level. Other players can sometimes share quests that you do not have, as long as you qualify for
them; if you don't, ask them what they did previously to acquire the quest, so that you can get started at the beginning
of that particular quest line. You can also ask people in your guild or people in the general channel. There are also
several web sites that you can visit that list quests for you, often broken down by level or region.
<a href="http://www.bradygames.com/bookstore/sales_detail.asp?st=49842&amp;redir=1" target="_blank">The Brady Strategy
Guide</a> has large lists of possible quests as well.
<P>
<B>Other Options</B><BR>
Check out the <a href="regionlevels.php">Region Levels page</a> and <a href="worlddungeons.php">World Dungeons page</a>, which both list available areas and their level range. You
can then head to new areas you haven't visited to look for additional quests. Most commonly, the quest givers will be
located in your faction's major outpost in each region, but occasionally you can find quests in remote areas, so look
around!
<P>
<B>Solo Players</B><BR>
If you're a solo player, do all of the quests you can in a region, then travel to another region of around the same
level and start the quests there. Some of them will be much easier because the quests will be lower level than you,
but these quests are often part of an ongoing story that will increase in difficulty; sometimes, they even lead
to quests in other regions. Continue to rotate between the areas until you've done everything that you can do.
If a quest is gray or a low-level green, you may wish to skip it, but be aware that these could lead to higher-level
quests later on. Additionally, check out the rewards for those quests, as you might want to finish the quest for the
quest reward. You should keep in mind that any quest marked as "elite," even if it is gray or green, may be too
difficult for a solo player to handle alone. If a quest givers quest is now gray to you, the NPC will no longer
show a "!" above its head, but the talk-bubble on the mouse-over will still appear. If you fill up your quest
log (20 limit), abandon the quests that are least important. You might want to write down who gave you the quest
and where you got it prior to abandoning it. That way you can go back to that NPC and get the quest again if you
decide to finish it later on.
<P>
Here are some area progressions you can try out. They are not exact and the recommendations on this page
should only be used as a loose guide for your journeys through Azeroth. You will find that some quests will
ask you to cross into regions that are not listed. This page doesn't list raid areas past level 60...those are
for you to find on your own.
