<img src="../../../shared/wow-com/images/smallcaps/plain/y.gif" align="left">ou don't have to adventure alone in World of Warcraft. In fact, the game can be more fun when you join with other players. Quests that are too difficult alone can be surprisingly easy to accomplish when you group with other players. If you group with other players frequently enough, they might even become regular adventuring companions and friends.
<p>
<b>Creating a Group</b><br>
A group, or party, of adventurers can have a maximum of five players. A group is created as soon as one other player joins you. You can invite a player to join your group in three ways.
<ul>
<li>You can select the player, right-click on the player's portrait, and select the invite command from the resulting dropdown menu.
<li>You can type the /invite command followed by the player's name at your chat prompt.
<li>You can add the player to your friends list, then select that player from your friends list in the Social window and click the Group Invite button.
</ul>
Players you can see next to you can be invited by any of these three means. However, players that you cannot see, usually because they are far away or in a different zone, must be invited through the /invite command or through the Group Invite button on the friends list.
<p>
A player already in another group cannot join your group. If you extend an invitation to such a player, you will get a message back saying the other person is already in a group.
<p>
<b>Joining a Group</b><br>
If you are adventuring by yourself and are invited into a group, you can accept or decline the invitation. However, if you are already in a group, then you will be unable to join a new group until you leave your current group.
<p>
<B>Switch to Party Chat</B><BR>
Type "/p text" to switch to party chat. This allows you to communicate only with members of your party.
<P>
<B>Group Leader Options</B><BR>
Type "/kick playername" to remove a player from the party. You can also
right-click on their name and select "uninvite".
<P>
<B>Leaving a Party</B><BR>
Right-click on your player portrait in the top left corner. Select the option to leave the party.
<P>
<B>Super Groups</B><BR>
Players will also be able to combine parties to form huge groups for raids.
<P>
<B>Party Member Options</B><BR>
Select a party member's portrait and right-click on it to see what options are available to you. Some options are only available to the group leader,
and only when the party members are next to each other, such as trading and inspecting.
<P>
<B>Locating Party Members</B><BR>
The minimap shows little green dots or arrows showing where to find specific party members. Place your mouse over the dots or arrows to see who is who.
<P>
You can also check which area party members are located by mousing over their information box on the left side of the screen.
<P>
<B>Loot Options</B><BR>
When the monster's body sparkles you are allowed to loot the corpse.
<P>
There are several loot options/flags:
<ul>
<B>Round Robin</B> - Everyone takes turns in looting.
It is your turn to loot again when everyone has taken their turn in that "round."
This setting is recommended for most situations.
If a player bends down to loot a monster under Round Robin looting, then if they leave items on the corpse that they
haven't looted, anyone in the party can loot them.
Keep in mind that more than one person will be able to loot a corpse if the person currently looting already has enough of the quest items that that specific monster dropped.
<P>
<B>Free-For-All</B> - This setting allows anyone in the party to loot any corpse. You might find this option useful if no one cares who gets what, or
how often any one person gets to loot.
<P>
<B>Master Looter</B> - This setting allows the group leader to loot everything and allows the leader to decide if other members in the party are to get items, and what items they should get.
This becomes useful when very powerful items are dropping and people can't be trusted to pass them out fairly. Any time an item is distributed using the Master Looter system and the item is quality epic or above there will be a confirmation dialogue to confirm that the item is going to the correct person.
<P>
