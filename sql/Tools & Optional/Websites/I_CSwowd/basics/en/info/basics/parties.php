<b>Marking Targets</b><br>
Parties and Raids can mark targets for their party/raid in the gamefield. A party leader or raid leader/assistant can right-click the portrait of their target to select a symbol to associate with that target. All players in the party/raid will see the selected symbol over the head of the target as well as on the portrait of that target.
<p>
<b>Target of Target</b><br>
Players can turn on the "Target of Target" in the Raid and Party section of the interface options. When this is turned on, anytime a player is in a party or raid, it will show the portrait and health of whatever your current target has targeted. You can click on that portrait to target that thing directly or if you have the spell glove up you can cast spells on the target of target without targeting it directly.
<p>
<b>Resetting Instances</b><br>
The party leader can reset a dungeon instance from the interface. If the party has recently been inside an instance but is now outside of the instance, the party leader will have a right-click option from their own portrait (where you select the loot UI preference) that is "Reset all Instances". If this is selected all non raid instances will be reset for the party.
<ul>
<li>You only get the reset instance option if you are a party leader.
<li>The UI option appears if you are a party leader, you have one or more instances open, and you are not currently in an instance.
<li>The UI option is a right-click option off of your own portrait (like party loot options).
<li>You get a warning dialogue when you select this option asking if you really want to reset all instances.
<li>If you select reset all instances, all non-raid instances will be reset to their starting condition.
</ul>
<B>Combat Targeting</B><BR>
The party should always be attacking the same monster.
Generally, the party members should always be attacking the Puller's target.
Often the most powerful or deadly monsters are targeted first.
<P>
<ul>
<B>Use "V"</B> - It's important that everyone isn't attacking a different target.
During battle hit "V" to show the monster's hit points.
Always try to attack the target that is closest to death.
You can also use this for targeting purposes.
Click on the name of the monster you wish to attack.
<P>
<B>Assist - F2-F5...</B> Can be used to select party members. Use the F-Key that matches the corresponding player and then hit "F" to target the monster that player is attacking.
You can also go into the options menu and select the option "Attack on Assist".
Attack on assist makes characters that assist with "F" automatically try to enter attack mode against their new target.
This option might be good for a warrior, but might not be useful for a priest.
Communicate with other party members to determine who the party should be assisting.
<P>
<B>Tab and Shift-Tab</B> - Tab chooses and targets the nearest monsters and Shift-Tab cycles through the list of monsters from farthest to closest.
Use these buttons during battle if you need to target a nearby monster or cycle through the list of monsters.
</ul>
