<B>Group Loot</B> (default) - When an item equal to or above the threshold (details on threshold below) is on a monster that is killed, everyone gets a pop-up box on their screen with the item and pass or roll options and a timer. Anyone who rolls, displays a number between 1 and 100, the highest roller of all those who choose to roll automatically gets the item. Anyone who waits until the timer expires automatically passes. Items below the threshold are taken care of by normal round robin rules. If more than one player ties a loot roll, a random player will receive the loot. An interface option, "Detailed Loot Information" is defaulted to 'on.' If you turn it off you will only see the roll and Need/Greed option of the player who won the item rather than information for every player.
<P>
<B>Need Before Greed</B> - Same as group loot, except players who cannot use the item automatically pass.
<P>
<B>Threshold</B> - This just determines the level of items that are affected by the methods above. If a party wants to set the threshold to blue items and are in group loot mode, then green items that are found will be taken care of by the normal round robin rules.
</ul>
<P>
Tip: When a party member receives a loot item, give it to the party member that can use it best.
Party members can click on the [lootname] to see the stats of items looted off of monsters.
<p>
The Group Loot/Need before Greed dialogue will now show a golden dragon border around Bind on Pickup items.
<P>
<B>Party Experience</B><BR>
Experience is shared with other party members.
For a party member to get experience, they must be within a certain distance from a kill.
It's best to have players around the same level in a party, but players that have vastly higher or lower levels will still get some experience.
<P>
