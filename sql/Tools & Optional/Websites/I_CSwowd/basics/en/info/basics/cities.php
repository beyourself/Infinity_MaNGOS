<td valign = "top" width = "50%"><span>
<b><i>Banking</i></b> - Banks provide places to store the hard-earned relics and treasures that you gain throughout your adventures in World of Warcraft. Each city features a banking center that links to many of the other banks located around the world, so you'll never be too far from your valuables.
</td>
<td width = "20"><img src = "../../../shared/wow-com/images/layout/pixel.gif" width = "20" height = "20"></td>
<td valign = "top" width = "28"><img src = "../../../shared/wow-com/images/layout/bullet2.gif"></td>
<td valign = "top" width = "50%"><span>
<b><i>Taverns</i></b> - Discuss and celebrate your conquests with your tavernmates! Or, relax with friends over a mug of ale and some good gossip. Taverns are great places to find folks looking for work, as well. Sometimes bounties are posted outside of taverns for those brave enough to undertake such deeds.
</td>
</tr>
