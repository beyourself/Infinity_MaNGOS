<img src="../../../shared/wow-com/images/smallcaps/plain/i.gif" align="left">n addition to small towns, forts, and outposts there are 6 major cities in the world of Azeroth. Each of the major cities represents the capital and a major hub for the races that inhabit them. The cities are all radically different from each other in themes and appearances. More than just a collection of buildings with NPCs standing around, the cities will serve as a place for adventuring, shopping, training, socializing, and much more.
<p>
</span>
<p>
<b>City Highlights</b>
<p>
<center>
<table cellspacing = "0" cellpadding = "0" border = "0" width = "90%">
