<b><i>Adventure areas</i></b> - Despite all the utilitarian purposes of cities, there are still many treacherous places within their darker, more confined districts. Exploring these areas, you could very likely come across long-lost family catacombs, ancient sewers, or perhaps a secret society bent on rekindling arcane, malevolent powers.
</td>
<td width = "20"><img src = "../../../shared/wow-com/images/layout/pixel.gif" width = "20" height = "20"></td>
<td valign = "top" width = "28"><img src = "../../../shared/wow-com/images/layout/bullet2.gif"></td>
<td valign = "top" width = "50%"><span>
<b><i>Lots of shopping</i></b> - Cities abound with merchants and shops that will cater to your every need. Weaponsmiths, armorers, alchemists, and master trainers-these are just a few of the many tradesmen you'll encounter within each city's fortified walls.
</td>
</tr>
