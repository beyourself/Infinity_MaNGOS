<b><i>Player-vs.-player combat areas</i></b> - Players thirsty for a bit of friendly competition can try out their swords and spells on each other in the combat arenas located in the cities. What better way to hone one's skills than against a fellow player?
</td>
<td width = "20"><img src = "../../../shared/wow-com/images/layout/pixel.gif" width = "20" height = "20"></td>
