<td valign = "top" width = "50%"><span>
<b><i>Many quests to be found</i></b> - Some quests can be completed in the city, while others will send you outside into the world. Minor quests can involve assisting local shop owners or working to stabilize the lands around the city, and more grand-scheme quests can involve political treachery or even helping to quell an uprising. Much of your time could be spent unfolding the twisted tales that run through each race's capital city.
</td>
<td width = "20"><img src = "../../../shared/wow-com/images/layout/pixel.gif" width = "20" height = "20"></td>
<td valign = "top" width = "28"><img src = "../../../shared/wow-com/images/layout/bullet2.gif"></td>
<td valign = "top" width = "50%"><span>
<b><i>Transportation hubs</i></b> - Thanks to their easy accessibility, cities also serve as central transportation hubs. Once you have located the appropriate merchant in each city and at various outposts in the region, you will be able to travel to and from cities on the backs of winged beasts such as Gryphons and Windriders, for a small fee of course. Intercontinental travel, such as aboard a Zeppelin, is also available in and around some of the major cities.
</td>
</tr>
