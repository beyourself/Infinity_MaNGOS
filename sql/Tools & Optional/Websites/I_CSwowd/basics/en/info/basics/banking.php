<P>
<span>
<img src="../../../shared/wow-com/images/smallcaps/plain/e.gif" align="left">very Hero needs a place to hide his or her treasures! Visit a bank in World of Warcraft to store items for later use. You can store quest items, weapons, armor, trade-skill components, products for later sale, and more! The six major cities each have a full-service bank for your use, and additional banks can be found at a few special locations around the world. Most minor towns and outposts don't have banks.
<p>
</span>
<table border=0 cellpadding=0 cellspacing=0 align=right>
<tr>
<td align=center>&nbsp;<a href="../../../shared/wow-com/images/basics/banking/ss01.jpg"><img src="../../../shared/wow-com/images/basics/banking/ss01-thumb.jpg" border=0></a>&nbsp;</td>
</tr>
<tr>
<td align=center><span><small><a href="../../../shared/wow-com/images/basics/banking/ss01.jpg">A well-to-do Dwarf makes a withdrawal<br>
from the Bank of Ironforge</a><p></small></span>
</td>
</tr>
</table>
<span>
<b>Finding Banks</b>
<br>
It might be challenging to find the banks, but ask people to help you find them. Banks usually have a sign posted just outside for easy identification. This sign depicts a stack of coins. Inside the banks are bankers, who have the &lt;Banker&gt; tag under their names.
<p>
<b>Tips</b>
<ul>
</span>
<table border=0 cellpadding=0 cellspacing=0>
<tr><td valign=top><img src="../../../shared/wow-com/images/layout/bullet2.gif"></td><td><span>Store Quest Items in the bank when you move to a new region. You can go back to the bank and pick them up when you return to the previous region.<p></span></td></tr>
<tr><td valign="top"><img src="../../../shared/wow-com/images/layout/bullet2.gif" ></td><td><span>Put Professions components you have collected in the bank to save them for your friends.</span></td></tr></table>
</ul>
<span>
<P>
<b>Need More Space?</b>
<br>
You can also buy extra storage space at each bank, which provides you with slots for storing additional bags of items. The larger the bag that you put in each bag slot, the more space you'll have! For example, you might start with a 6-slot bag in one of your bag slots, but later on you might find a 10-slot bag to put there, which would essentially give you four more bank slots. The price for additional bag slots goes up with each new slot, and the final slots are very expensive!
<P>
