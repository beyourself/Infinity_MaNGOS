<p>
<B>Dungeon Player Caps</B><BR>
All dungeons have a cap on group size to limit the number of players that may enter the dungeon. It was necessary to add this cap as raiding these dungeons trivialized much of the content.<BR><BR>
With that in mind, the dungeons below now have the following caps: <BR>
<UL>
<LI>Onyxia/Molten Core - 40 members
<LI>Blackrock Spire - 15 members
<LI>Diremaul - 5 members
<LI>All other dungeons in the game will be capped at 10 members.
</UL>
<b>Create your Party or Raid Group</b><br>
<ul>
<b>Get a Healer</b><br>
It's difficult to play in dungeons without a healer. Preferably, you want to bring at least one Priest for healing and resurrection in a single role. Priests find themselves constantly private messaged for groups because their healing abilities are in high demand. If a priest is not available, you can use Druids and Shaman, or even Paladins. While a Paladin can heal, you typically don't want to rely on that for healing for your entire group.<br>
<p>
<b>Get a Tank</b><br>
You will have greater success with a heavily armored class like a Warrior or Paladin to soak up damage and to lead the party through the dungeon. You can play without a Warrior, but you'll find it's much better to have a good Warrior whenever possible.<br>
<p>
<b>Puller</b><br>
Find someone to pull for the party. A Warrior can do this with its ranged weapon, and a Hunter is also a great pick for this role. The Hunter can even place a giant target on a monster for the party to see, which helps coordinate attacks. And the Hunter can lay down traps to immoblize or hinder pulled monsters.Many raid groups rely on a Hunter to pull and a Warrior to tank.<br>
<a href="partiespulling.php">[Learn more]</a>
<p>
<b>Odd Groups</b><br>
You can create non-standard groups for dungeons. They can work depending on the skill of the players, the level difference, equipment, and the balance of the group. However, non-standard parties are recommended only for expert players.<br>
<p>
<b>Pick a Group Leader</b><br>
<a href="partytips.php">[Learn more]</a>
<p>
<b>Take an Expert</b><br>
If possible, bring someone who knows the dungeon and its secrets so that they can tell newcomers what to do. If you want to explore the mysteries of the dungeon yourself, don't bring that expert.<br>
</ul>
<P>
<b>Get the Quests</b><br>
Make sure you and your party have all of the required quests before entering the dungeon. List all of the quests you have in party chat.<br>
<p>
<ul>
<b>1.</b>Hit enter to open up the text box while in party chat.<br>
<b>2.</b>Open your Quest log and select a quest for that dungeon.<br>
<b>3.</b>While holding down the shift key, left-click on the quest title.<br>
<b>4.</b>Repeat step 3 for each quest.<br>
<b>5.</b>Finally, hit enter to send the list of quests to party chat. Note: you can only list a limited number of quests in a single line. You'll need to use another chat line to list additional quests.<br>
</ul>
<p>
Party members will then see what quests you have and can see if they are missing any. Make sure you tell your party members that this is why you are listing your quests. Share whatever quests you can with the group. Experienced players will already know what you're doing.<br>
<P>
<b>Time</b><br>
Make sure you are prepared for the major time commitment necessary to complete the dungeon. Ask about how long the dungeon will take. Some are fairly quick to beat, while others will take lots of time. It's very bad for the group if you leave in the middle of the instance, so do not enter unless you're willing to go for the entire ride. Let your group know about any time concerns you have prior to entering the dungeon.<br>
<p>
<b>Establish Loot Rules</b><br>
<p>
Make sure the party is aware of the looting rules at the beginning. There are several options such as /random 100, need before greed, and so on. Establish the rules before the items stop dropping.<br>
<p>
Typically, the highest-quality equipment and weapons can be found within instances. This makes hunting in them very appealing. However, it also attracts people that are there simply for the loot. Make sure that before you enter the instance, everyone has agreed to how loot the loot gets divided. The group may be hoping to find a particular item for a particular character, or they may be prepared to sell everything that drops and split the cash. Usually items that can be used by multiple members of the party are "rolled for." It is considered proper etiquette to pass on items you cannot use; however, there may be items that the Group Leader decides that everyone should roll for. The Group Leader has control over the Loot Policy setting. Make sure you know what this setting is and what you may or may not do with regards to loot. Most arguments between players stem from Loot issues. Don't let Loot spoil your fun.<br>
<p>
<b>DO NOT LOOT BIND ON PICKUP ITEMS</b><br>
<p>
Dungeons have many items, typically blue and above, that are bind on pickup. This means that once the player picks up the item, it's bound to their character. They can no longer trade it and can only sell the item to the vendor or destroy it. There is a popup box that asks for confirmation before looting a bind on pickup item. Players should always pause at that popup and make sure they are supposed to loot the item first. If you pick up an item by mistake, you can cause irreversible damage to your reputation as other players might not believe it was an error. This can also cause additional repercussions such as difficulty finding groups, or demotion and ejection from your guild.<br>
<p>
A good trick to use is to count down before looting a bind on pick-up item. "Looting X in 10, 9, 8, etc." Then wait a little longer once you get to 0 before looting it. That way you can say you gave people a chance to complain prior to looting.<br>
<p>
<b>Single Run vs. Multiple Visit</b><br>
<p>
Some dungeons such as Deadmines are designed for a straight through one-time shot. Other dungeons, such as Scarlet Monastery, are meant to be completed in multiple trips. For example, in Scarlet Monastery you can do The Armory one night, then do the Cathedral another night, and so on.<br>
<p>
<b>Additional Tips</b><br>
<p>
Make sure you have read <a href="partyroles.php"><b>party roles</b></a>, the <a href="partiespulling.php"><b>art of pulling</b></a>, and <a href="partycrowdcontrol.php"><b>party crowd control</b></a>.
<p>
Know what your job will be. Ask if you are unsure.<br>
<p>
<b>Level Difference and Aggro</b><br>
Lower-level players will have a much larger aggro radius with monsters. This means they will attract monsters (anger them) from much farther away. Therefore, it's important to bring players that are around the same level and within the proper level range for the dungeon. If you do decide to bring in a lower-level player in your party, have them hang behind the group so they don't pull extra monsters.<br>
<p>
<b>Running Away</b><br>
Normally you can run away from a monster until they stop chasing you. In instances, they will continue to chase you to the dungeon exit. So typically, running is not an option in dungeons.<br>
<p>
<b>Stopping Running Monsters</b><br>
It's very important to stop monsters from running away in dungeons. Normally, if a monster goes and grabs some friends, you can just run away. However, in dungeons you don't have that luxury. A "runner" that gets away in a dungeon typically will wipe out an entire group when it alerts its buddies and brings them to the party.. There are many different abilities that can stop a monster from running away, such as Rogue slowing poisons or the Warlocks' Curse of Recklessness. Do not use Fear or similar abilities in the dungeon as the monsters may bring back friends.<br>
<p>
<b>Assisting</b><br>
It's very important that everyone in the group is attacking the same monster. What advanced players do is pick an MA (Main Assist). The main assist determines which monsters the party should be attacking. Typically, this is also the player that pulls for the group. You should create a macro for this action. Type /macro and enter "/assist playername." Then place that button on your bar and hit it continually during combat.<br>
<p>
<b>Wipe-out</b><br>
A group wipes out when all party members die and have no way to resurrect themselves. They have to run back to the dungeon from the graveyard and start from the beginning of the dungeon (instance) again. Depending on the dungeon and the length of time since you first entered, monsters may have respawned and you might have to fight some of them again. If you're lucky, the monsters you killed previously will all still be dead, and you can run right back to where you last were.<br>
