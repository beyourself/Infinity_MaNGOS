<b>Suicide Bad Pull</b><br>
If the puller makes a bad pull, have that puller just die and not lead the monsters back to the group. The puller should die in a location where their corpse is easy to select so that they can be resurrected.This is an old standard trick used in previous MMORPGs.This will *not* work if the party is already in combat mode. The monsters will search for any player on their hate list. The best way to make sure the party survives a bad pull is to keep a resurrection-capable player out of line-of-sight of the mobs when they are being pulled. Make sure that this player is not doing anything that could put them on the monsters' hate lists, like renewing the party's buffs or healing someone.<br>
<p>
In some circumstances, a Hunter can use "Feign Death" to escape a bad pull.<br>
<a href="partiespulling.php">[Learn more]</a>
<p>
<b>Hide the Resurrecting Player</b><br>
If you have enough players (such as in a raid), you can have one player that is capable of resurrecting hang back and remain out of combat (no crossed swords over level). If everyone dies, this player can come in and resurrect the party. If you've had a bad pull and the puller cannot "suicide," then this is another option.<br>
<p>
<b>Hearth, Repair, and Summon</b><br>
Warlocks enable party members to leave and return to the dungeon with greater ease. Let's say a player needs to repair, get food/water, or needs to turn in a quest or pick up a quest item. That player can use their hearthstone to port back to base. The party can then summon that player back once the player has completed their task. The player must first return to the dungeon and enter the instance before they can be summoned.<br>
<p>
<b>Pet Classes</b><br>
Hunters and Warlocks should have their pets on Passive the majority of the time in an instance. Consider binding "Pet Attack" to its own key.<br>
