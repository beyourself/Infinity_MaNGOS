<div class = "postBody">
Some spells improve friendly units or weaken enemy troops. Other spells do direct damage to enemies or heal allies.
Most spells cost a certain amount of mana to cast. In the place of mana some classes have their own ability requirements
such as Warriors that use Rage. Some Abilities/Spells do not require mana but are instead based on a timer. Many people use
the word "spells" to cover both spells and abilities.
<P>
<B>Effect</B><BR>
This means how much the spell alters the target.
If it is a damage spell, then the effect is how much damage the spell does.
If the spell is a healing spell, then the effect is how much health is healed by the spell.If the spell is a blessing or curse, then the effect is how long the spell affects the target, in seconds.
<P>
<B>Cast Time</B><BR>
The Cast Time is a measurement of the amount of time it takes from the start of casting a spell to the moment
the spell is activated or finished casting. The longer the Cast Time, the longer it takes to cast a spell.
You will find this time listed in your spell or abilities menu. When you are casting an ability, a Progress
Indicator will pop up on the screen, indicating how close you are to casting the Ability. If you are attacked by
a monster during this time, the progress bar will drop and delay the casting of your spell.
You can continue to try and cast the spell or stop casting and attack the monster if you believe the
delays will make it impossible to complete the casting of the spell. If you move or are interrupted by a monster's special ability (such as stun), the casting process will be completely canceled and you will have to try casting it again.
<P>
<B>Channeling/Interruption</B><BR>
Many spells must be maintained or "channeled" to achieve their full effect. This applies mainly to mass-damage
area-effect spells (Blizzard is an example of such a spell) although there are a few other spells that
also require channeling. You will know a spell requires channeling when a progress bar appears with
the word "Channeling" in it. If you are interrupted by moving, attacking, being attacked, or if you try
casting something else, the spell will not be completed and you will
have to try casting it again. <P>
Spell casting is not interrupted when leaving combat.
<P>
<B>Instant Cast</B><BR>
Some spells can be cast instantly. This means that they cannot be interrupted by monsters, and they do not have a Casting Time. These spells can be identified in the spell book or by noting the lack of a progress bar when they are cast.
<P>
<B>Casting on the Run</B><BR>
Some abilities can be cast while you're running. However, most require you to be standing still.
The warning message that appears when you are unable to cast a spell will let you know if
you must stand still, or what other error is causing you to be unable to cast the spell.
<P>
<B>Canceling Spell Casting</B><BR>
You can also cancel a spell by hitting the "ESC" key.
Once you begin casting a spell, do not move; otherwise you will interrupt the spell. This is also a
good way to stop casting if you change your mind about casting a particular spell. For instance, let's say
you're a healer that is about to heal someone, but someone else heals your target before you do, or
the person uses a potion. You should then start moving and interrupt the casting of your own Heal spell.
This way, you will not waste Mana healing a target that no longer requires healing.
<P>
<B>Cooldown</B><BR>
Cooldown is the amount of time it takes before you can cast the same spell again. This is alsoknown as a Casting Delay. Cooldown is fixed and is unaffected by increases to Mana capacity and/or Mana Regeneration. Some spells do not have a Mana cost, and are instead limited by their Cooldown
timer. Once you have used an ability, place your mouse over the ability to see how much longer you must
wait in order to use that ability again.
<P>
<B>Range</B><BR>
The spell book lists the range of each spell. To cast a spell on a monster, you will need to get close enough to be inside of the maximum range allotted by the spell.
Typically you will want to cast your spell as far away from the monster as possible, which will give you more time to cast a second spell or do something else
(like having another party member/pet taunt it) before the monster reaches you. Here is a test which will allow you to determine the maximum range: walk towards the monster and stop
every few feet to click on a ranged spell.
You will receive an error message each time, up until you are actually close enough to cast the spell.
This is how you will know what the maximum range is. As you use your abilities more, you may find that experience allows you to judge distances and judge what the maximum range is, without doing this test.
<P>
<B>Area of Effect Spells (AOE)</B><BR>
AOE spells affect multiple enemies at the same time. These abilities are useful if you encounter a pack of monsters, especially when another party member can Taunt them to him or herself,
and stop the pack from swarming on the caster, or if the pack is all nearly dead. Be very careful when using
these spells; they can "wake up" monsters that are neutral, and make them aggressive towards you, or generate enough "monster hate"
to cause multiple monsters to attack you at once. (This will cause the caster to die almost instantly.)
It's helpful to group with a Tank (A Tank is a character that can absorb massive amounts of damage without dying) to Taunt the pack and take the attention away from the caster.
AOE spells have a special targeting display. Place it exactly over where you want to cast it.
<P>
<B>Leash Spells</B><BR>
A Leash Spell is an ability where your character lassos the target with a stream of spell effect.
Life Drain is an example of a Leash spell. You must be within range to cast and if your target moves too far away the spell will cancel (it will be out of leash range) before it reaches its full effect.
<P>
<B>Line-of-Sight/Casting Direction</B><BR>
Most abilities require that you be facing the target, and that you be able to see the target.
If you receive a line-of-sight error message, move your character until it is facing the target and the target is in your character's line-of-sight.
<P>
<B>Diminishing Interrupt Time</B><BR>
Spellcasting has diminishing interrupt time. Each successful hit after the first will set the spell being cast back less each time to a minimum of .2 seconds. First hit 1 second, second hit .8 seconds, third hit .6 seconds, fourth hit .4 seconds, fifth and all successive hits .2 seconds.
<P>
<B>Mana Regeneration</B><BR>
Mana does not regenerate for the five seconds after you cast a spell.
<P>
<B>Buffs</B><BR>
A "Buff" is a beneficial and persistent spell effect. An example of a Buff is the Priest's Renew. Players can cast Buffs on themselves,
their pets, other player's pets, and other players.
<P>
<B>De-Buff</B><BR>
A De-buff is a hostile spell that is cast on the enemy, or cast by monsters on yourself. Spells like these do damage over time, or modify your character in a negative way. You will need to remove these abilities with a "Remove Curse", "Dispel" or
any abilities that provide a similar effect. Short of that, you will simply have to wait until the De-buff's timer is up, and it goes away by itself, which is much more common.
<P>
<B>De-Buff Limit</B><BR>
The debuff limit is 16. The game will display all 16 debuffs.
<P>
<B>Buff Timers</B><BR>
There is an option to display the time remaining on your buffs all the time. The option is found in the "Interface" options window under "Buff Durations."
<P>
<B>Spell/Ability Reagents</B><BR>
Some abilities require that you have a special item(s) in your inventory. These are known as Reagents. The error message will tell you what item is required.
You must then find this item and place it anywhere in your backpack or other bags. If you do not have the required reagents, you will be unable to use the ability.
<P>
<B>Resistances/Monster Level</B><BR>
Monsters have a chance to resist your spells. If they resist a spell you will see "resist" displayed. Lower level monsters will resist your spells less often than higher level
monsters do. If you attack a very high level monster you will find that they will resist spell effects almost every time. If you attack a low
level monster you will find the monster rarely resists your spells. If you are having problems with resists, try attacking lower level monsters,
getting some help, or using other abilities that are less likely to be resisted.
<P>
<B>Autocast Spell</B><BR>
Some spells and abilities do not need to be manually activated by the player.
They are called Autocast spells and you can set them to be automatically cast by the player when needed.
Some pets also have Autocast spells. Spells like these will have a border around the spell icon that flashes, letting you know when it is active.
<P>
<B>Getting New Spells or Abilities</B><BR>
Most abilities can be found by visiting trainers. Other abilities can be found in books, which are items dropped by monsters, found through quests, or sold by other players.
Use your mouse to right-click on a book to learn a new ability. A new button that depicts this ability will now appear in your spells or abilities menu.
<P>
<B>Tracking</B><BR>
Tracking of all types works until canceled, but you can only have one type of tracking working at a time (Herb Finding, Mineral Finding, Sense Undead, etc)
<P>
<B>Spell Stacking</B><BR>
Spell effects that stack multiple times on a character will display a number in the bottom corner indicating how many times the effect has stacked. For example, if a player has had the Sunder Armor ability used on them three times, the Sunder Armor effect icon will display a 3 in the corner.
<P>
<B>Additional Information</B><BR>
<ul><li>Mana regeneration is disrupted when a spell has completed casting rather than at the start of <br>casting. It will resume normally five seconds after the last spell cast.
<li>Elixirs will not stack with player spells that improve the same attribute.
<li>Water walking won't work on lava or slime.
<li>Casting Dispel Magic on a friend under the influence of charm will dispel the charm
<li>Abilities that activate off critical hits work off critical hits from abilities as well (Blood Craze, Enrage).
</ul>
