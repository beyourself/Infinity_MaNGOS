<img src="../../../shared/wow-com/images/smallcaps/plain/b.gif">efore entering battle, each party member should be aware of the tools that their class provides for controlling a battle and the role that they will be expected to play. This will help prevent confusion in battle and will help the group function more effectively. Most groups contain a tank, such as a warrior or paladin, a damage dealer such as a rogue or mage, and a healer, such as a priest, druid or paladin. Some classes are capabable of performing different roles depending on a particular group's needs. Druids, shamans and paladins are good examples of this, as they are able to heal, tank or do damage depending on the situation, albeit not as well as some of the more specialised classes. Detailed below are some of the different types of roles which there are in parties.</div>
