<img src="../../../shared/wow-com/images/smallcaps/plain/n.gif">o, we don't mean "thanks", we mean tanks! A tank is a character designed to take a lot of damage from monsters while still fighting on! Just like..uh..a.. tank?
If any character is fighting with a monster with their melee attack and the monster is attacking them, they can be said to be "tanking". The class best suited to tanking is the warrior, although several other classes are able to tank to a certain degree, for example druids, paladins and shamans.<br />
<br />
<B>Purpose:</B> The purpose of a tank in a party is to absorb and deflect damage from other weaker party
members, such as casters. A tank is much more capable of taking physical damage than other party members.<br />
<br />
<B>Tank Jobs</B>
<ul>
<B>Taunting</B><BR>
Tanks typically have an ability that will taunt the monster off another player.
Whenever a weaker party member, such as a mage or priest is attacked, they should let the tank know.
The tank should immediately find the monster attacking the party member and use their taunt ability until the monster starts attacking them instead. That way
the tank continues to take all the damage and the casters do their job of healing the tank or blasting the monster.
<P>
<B>Pulling</b><BR>
Another usual role for the tank is to pull monsters for the group. The tank typically has a lot of hit points and can withstand more damage. Tanks should have some ability or weapon that allows them to pull with a ranged attack.
[<a href="partiespulling.php">Learn more about pulling</a>].
<P>
<B>Leading</B><BR>
The tank is usually responsible for leading the group around to their destinations.
<P>
<B>First in, last out</B><BR>
The tank should be the first person into battle under most circumstances. If the party is forced to run, the tank should stick around taking more damage, possibly even dying in order to buy time for the other party members to escape. The other party members can then resurrect the dead tank.
</ul>
<B>Primary Tanks vs. Secondary Tanks</B><BR>
If there are multiple tanks in a group a primary tank should be chosen. The secondary tanks are responsible for helping out the main tank but typically don't have to worry about taunting, pulling, or leading. They just help do additional damage and occasionally tank extra mobs.<br />
<br />
<B>Hybrids</B><br />
A hybrid is any class that is capable of filling (at least partially) roles primarily held by other classes. A paladin is a hybrid because it can tank in lieu of a Warrior, and heal in lieu of a Priest. A Shaman is a hybrid because it can deal damage in lieu of a Rogue/Hunter/Mage, but also heals in lieu of a Priest. A Rogue is not a hybrid class, as they cannot heal or tank but can only deal damage and crowd-control.<br />
<br />
<a name="healers"></a>
<B>Healers</B><br />
A healer is any class with the ability to heal other characters. Healers include Shamans, Druids,
and the best healer: the Priest. A group is always better off with a healer. Parties without a healer are
possible, but are typically more challenging and, some say, less fun. Healers are very popular in parties.Their purpose is to heal party members and keep them alive.
Healers are not for everyone. There are good healers and bad healers. Good healers do a good job of healing
everyone and almost always heal at the right time; bad healers don't. Always try to learn and accept tips on
becoming a better healer. Good healers are in high demand, if you're a good healer word spreads and people remember you. You may find yourself finding more groups and more respect!<br />
<br />
<B>How to be a Good Healer</B><BR>
<LI>Always pay attention to party members' hit points. You don't want to be in a situation where party members die and you could have saved them if you were paying attention.<BR>
<LI>Learn control methods to heal people faster. One way to do it is with hot keys such as Function keys which select party members and the number keys associated with the healing spell.<BR>
<LI>Give a verbal confirmation that you're healing someone so they don't freak out or think you're not "on it." Example: "healing j" <BR>
<LI>Let your party members know they can always ask for healing or remind you at any time. They can even use the voice macro: "<B>/v heal</B>"l.<BR>
<LI>If a monster is attacking you, preventing you from healing, let the tank know so he can save you.<br />
<br />
<B>Mana Spell Management</B><BR>
As the healer levels up, the healer receives multiple healing spells. It's important that the healer figures out what is the best heal to use for the situation. There are factors to consider such as cast time (don't want people to die while casting), and healing per mana cost. It's wise to keep several healing spells on the action bar and use them based on the situation. <br />
<br />
<B>Tricks of the Masters</B><BR>
<LI>Get a stock of Mana Potions to use for emergency situations.<BR>
<LI>Always have a supply of drinks. Drinks regenerate mana. <br />
<br />
<B>Casters</B><br />
A caster is a spell caster. Casters include: Mages, Priests and Warlocks.
To a lesser extent, casters also include Druids (in certain forms) and Shamans. Their purpose is to quickly destroy enemies from a distance using powerful spells. Because casters are weak to melee attacks they should find other tank classes to tank for them whenever possible. <br />
<br />
<B>Buffs</B><BR>
If you have any buff spells available which can help other party members, make sure they are always active on them.
At the very least make sure the tanks in the party have them.<br />
<br />
<B>Other Tips</B><BR>
<LI>Let tanks know when you're being attacked so that they can taunt the monsters off you.<BR>
<LI>When a monster is attacking you, don't run. Stand still and let the tank know so that they can taunt the monster off you. If you run it is more difficult for the tank to taunt the monster.<BR>
<LI>Carry drinks so that you can regenerate your mana during or after combat<BR>
<LI>If the monster is trying to get away, use your direct damage spell (nuke) to finish it off. This will prevent the monster from bringing back help (you hope).
