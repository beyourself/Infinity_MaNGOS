<b>Daily Honor Calculation</b><br>
Each day, a few hours after midnight, the previous day’s Honor is calculated for all characters. All your kills and achieved objectives are added together into your Honor for the day, completely independent of other characters.<br>
<br>
<b>Weekly Standing and Rank Calculation</b></br>
During the Wednesday maintenance your weekly Standing is determined and Rank adjustments are calculated. Your Standing is your position on the scoreboard for having the most Honor among your faction on your realm this single week. I.e. the character on your side on your realm with most Honor gained during the week has Standing 1. The Rank adjustments are a bit more complicated.<br>
<br>
<b>Rank</b><br>
There are 14 Ranks. Behind the Ranks is a hidden number scale, each Rank representing a range of points. <a href="../../../shared/wow-com/images/basics/honorguide/fandral.jpg"><img src="../../../shared/wow-com/images/basics/honorguide/fandral-thumb.jpg" border="0" align="left"></a>These points are not your Honor points for the week, but something we’ll call Rank Points for the sake of this guide. The exact points and scale will not be revealed here, but it’s important to keep in mind that there are numbers behind the Ranks to understand how the Ranks work.<br>
There are three steps in calculating and adjusting your Rank:<br><br><br><br>
<ol type="1">
<li>Excluding those with too few kills from the pool.
<ol type="a">
<li>Those with less than 25 Honorable Kills will not be included in the calculations this week, and their points for the week will be 0.
<li>A cut-off is calculated based on the number of Honorable Kills made by all characters of your faction this week. Those having less Honorable Kills than this cut-off (but at least 25 Honorable Kills) will not be included in the calculations this week, but their points for the week will be enough to at least attain or keep Rank 1.
</ol>
<li>Calculating your points for the last week. Factors involved are:
<ol type="a">
<li>The total Honor of all characters of your faction on the realm who made the cut-off: this determines the size of the pool of points to be given out to your faction this week. A more active faction will have a bigger pool to share, but also more characters to split it between.
<li>Your Honor for the week: this determines how many of the points from the pool will be assigned to your character.
<li>Your level: characters lower than level 60 will not get their full share of points for the week. The penalty is especially significant at levels below 50, and for someone of level 30 or below to attain any Rank above 1 is hard indeed.
<li>Thus, for maximum points for a week your faction needs to collect a lot of Honor, you need to be one of the characters collecting the most Honor, and you need to be at or very close to level 60.
</ol>
<li>Adjusting your Rank Points and thus your Rank.
<ol type="a">
<li>This is done by comparing your points for the week with your Rank Points. If the points for the week are higher than your current Rank Points, your Rank Points are adjusted upwards by a set percentage of the difference. If your points for the week are lower than your Rank Points, your Rank Points are adjusted downwards by a set percentage of the difference. The set percentage is higher for upward adjustments than downward adjustments.
</ol>
</ol>
<b>Example: (note that you will never actually see any of these points in-game, only the Rank)</b><br><<a href="../../../shared/wow-com/images/basics/honorguide/sylvanas.jpg"><img src="../../../shared/wow-com/images/basics/honorguide/sylvanas-thumb.jpg" border="0" align="right"></a>
During your first week with the Honor System your activity awards you with 14000 points for the week (not Honor, but points from the faction pool). As this is your first week and you had no points from before you will get a set percentage of these points added to the Rank Points. Let’s pretend this is 2000 points, which then determines your Rank for the coming week.<br>
During the second week you get 25000 points (again, not Honor, but those points from the faction pool). You will now gain the set percentage of the difference between 25000 and those 2000 points, bringing the points determining your rank up to let’s say 5000 points. This probably means you will have a higher Rank than last week.<br>
In the third week you’re not as active, earning only 3000 points (from the pool), which is lower than your Rank Points. This means your Rank Points will be adjusted down by a set percentage, but a lower one than the percentage used when you gain points. Let’s say you go down to 4500 points, which would probably not lose you a Rank.<br>
