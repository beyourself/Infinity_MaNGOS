<small class="smallBold">Short honor point scoring reference</small>
<br>
<br>
<br>
There are three sources to Honor during the week:
<ul>
<li><a href="honor-guide.php#a">Honorable Kills</a>
<li><a href="honor-guide.php#b">Killing racial leaders</a>
<li><a href="honor-guide.php#c">Achieving Battlegrounds objectives</a>
</ul>
<b><a name="a"></a>Honorable Kills</b><br>
Killing a player character within your XP range, i.e. a character who is color-coded (green to red), not grey. There are several factors influencing the Honor value of such a kill:
<ul>
<li><b>Your opponent’s level:</b> killing a level 60 character gives more Honor than killing a level 50 character. Killing a “grey” player gives no Honor.
<li><b>Your own level:</b> a level 60 character killing another level 60 character gains more Honor than a level 40 character killing another level 40 character.
<li><b>Subsequent kills:</b> killing a character a second, third or fourth time within 24 hours after the first kill will give reduced points for these kills (75%, 50% and 25%, respectively). The fifth and subsequent kills give no Honor at all. Note: the Honor notification in-game when killing an opponent does not take this diminishing return into account.
<li><a href="../../../shared/wow-com/images/basics/honorguide/cairn.jpg"><img src="../../../shared/wow-com/images/basics/honorguide/cairn-thumb.jpg" align="right" border="0" title="Cairn SMASH!"></a><b>Group or raid:</b> killing while in a group or raid will share the Honor among team members in the same way as XP, with the exception of the raid penalty on XP not being applied to Honor. Lower level members will get a smaller portion than the higher level ones. Note: kills made in a group or raid still count towards your subsequent kills penalty (see above).Members of the group or raid group will not receive honor points if they are too far away from the group member who got the kill, much like earning XP.
<li><b>Your, your team's, or your raid’s contribution to the kill:</b> each solo character, group or raid participating in the kill gets their share of the Honor. The share is roughly equal to the percentage of damage inflicted to kill the target. Notes:
<ul>
<li>If an opponent does not receive any damage for one minute the record of who contributed to killing him is wiped clean. Thus, even if you come upon a lone enemy someone else might have fought him (and lost) recently and you might not get the full share of Honor for killing him.
<li>Monsters and NPCs take their part too. If you jump a character already injured by a monster or NPC (and maybe still in combat) your reward will be smaller.
</ul>
<li><b>Your opponent’s rank:</b> Honor Ranks make the target worth more Honor.
<li><b>Being present at the time of the kill:</b> you need to be alive and close by at the time of the kill to get your share of the Honor.
</ul>
Note: The opponent being healed during combat does not mean he’s worth more points, only that it takes more damage to kill him.<br>
<br>
<b><a name="b"></a>Killing racial leaders</b><br>
Killing one of the NPCs considered leaders of their race. There are only a few of these, all on a two hour spawn timer, and the bonus for their defeat is divided between participating solo characters, groups and raids based on damage contribution. The racial leaders are:
<a href="../../../shared/wow-com/images/basics/honorguide/bolvar.jpg"><img src="../../../shared/wow-com/images/basics/honorguide/bolvar-thumb.jpg" border="0" title="*stares blankly*" align="left" hspace="15"></a><ul>
<li>Thrall
<li>Cairne Bloodhoof
<li>Lady Sylvanas Windrunner
<li>Highlord Bolvar Fordragon
<li>King Magni Bronzebeard
<li>Arch Druid Fandrag Staghelm
</ul>
<p>
<br>
<br>
<b><a name="c"></a>Achieving Battlegrounds objectives</b><br>
The Battlegrounds contain several opportunities to gain Honor in addition to killing your opponents, one of them naturally being winning the Battleground. E.g. in Catch-the-Flag Battlegrounds each returned flag gives your side a share of Honor. All members of the Battlegrounds team (Alliance / Horde) will receive the same amount of Honor for a flag capture.<br>
Tips:
<ul>
<li>Battlegrounds objectives are intended to be a substantial part of your Honor for a week. Don’t ignore them if you want to attain a high Rank.
<li><a href="../../../shared/wow-com/images/basics/honorguide/thrall.jpg"><img src="../../../shared/wow-com/images/basics/honorguide/thrall-thumb.jpg" border="0" title="Hi Mom!" align="right"></a>As kills in raids give fewer points to you personally but still count toward your subsequent kill ratio, try get some kills alone or in a small group earlier in the day / evening, then join a raid to collect more kills to meet the cut-off.
<li>If your number of Honorable Kills is a bit low for the week, join a raid or go play a Battleground to collect some more before the weekly calculation.
</ul>
<b>Dishonorable Kills</b><br>
Killing an NPC labeled as a “civilian” is considered a Dishonorable Kill. The civilian label appears on NPCs such as vendors and quest givers when your level substantially exceeds theirs. Beware: penalties for dishonorable kills are applied immediately and may cost you Ranks outside of the weekly Rank calculations.
