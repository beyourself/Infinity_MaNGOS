<p>
<a href = "../../../../shared/wow-com/images/basics/battlegrounds/queue/ss03.jpg" class="right"><img src = "../../../../shared/wow-com/images/basics/battlegrounds/queue/ss03-thumb.jpg" border = "0"></a><img src = "../../../../shared/wow-com/images/smallcaps/plain/t.gif" align = "left">he following is a step-by-step process regarding the Battlegrounds queue system. The Battlegrounds are instanced, and have a limit as to how many players can enter, though this number is unique for each Battleground. Therefore, we have implemented a system to allow players to play in whichever Battleground they choose, or join their friends in a specific Battleground instance.
<P>
<OL>
<LI> You have two options for joining a Battleground: visiting a Battlemaster or traveling to the Battleground's main entrance.<p>
<b>Talk to a Battlemaster</b><br />
Located in some of the cities, right-clicking on a battlemaster will allow your character to enter a battleground queue just like you normally would if you touched that battleground's entrance portal. The functionality is exactly the same, so when it's your character's time to enter the chosen battleground, you will be teleported directly in. Glordrum Steelbeard stands in the Hall of Arms in Ironforge, serving as the Alliance battlemaster for Alterac Valley. Aethalas of the Silverwing Sentinels stands ready in Darnassus, next to the warrior trainers, to send Alliance characters into Warsong Gulch. Brakgul Deathbringer is in the Hall of the Brave, located in Orgrimmar's Valley of Honor. He serves as the Horde's battlemaster for Warsong Gulch, while Kartra Bloodsnarl will send you to bring glory to the Horde in Alterac Valley. Guards in their respective cities can give you directions on how to find them.
<P>
<b>Visit the Battleground</b><br />
You can join the Battleground queue by running into a swirling red portal. Alliance can only go into the Alliance portal, and Horde only into the Horde portal. Each faction will have their own entrance, protected by NPCs.
<LI>Once you run into the red portal, a dialogue box appears with the following:
<P>
<a href = "../../../../shared/wow-com/images/basics/battlegrounds/queue/ss01.jpg" class="right"><img src = "../../../../shared/wow-com/images/basics/battlegrounds/queue/ss01-thumb.jpg" border = "0"></a><B>First Available</B> - Go into any instance as soon as a spot becomes available. <BR>
<B>Alterac Valley 1</B> - Choose the instance you want to join. Could result in longer wait time <BR>
<B>Alterac Valley 2</B><BR>
<B>Etc</B>
<P>
The numbers indicate the instance, so that players inside the instance can let their friends know which instance to enter.
<P>
<LI>After you select an option, you are in the queue, and a battle standard icon will appear in the lower left area of the minimap, letting you know your queue status. By right clicking this icon, you can either change the instance you want to join, or leave the queue completely.
<P>
<LI>While you are in a queue, you are free to go wherever and do whatever you like. You do not have to be in a certain area. Once you are ready to join, you will be teleported into the Battleground from any location in the world.
<P>
<a href = "../../../../shared/wow-com/images/basics/battlegrounds/queue/ss02.jpg" class="right">><img src = "../../../../shared/wow-com/images/basics/battlegrounds/queue/ss02-thumb.jpg" border = "0"></a><LI>If it is your turn to join the Battleground, a popup dialogue box will appear asking whether you are ready to join, or if you want to hide the dialogue box. If you click Hide, you will have 1 minute to choose whether to join the battle, or leave completely. Again, these options will be available through the battle standard icon.
<P>
</OL>
<b>Getting In A Queue</b>
<ul>
<li><b>Join as Group</b> - Party or Raid leaders can select "Join as Group" to put there whole group into the same queue as the leader, overwriting each member's previous Battleground queue status.
<ul>
<li>Group members may choose another queue afterwards.
</ul>
<li><b>Join Battle</b> - Places the individual in the Battleground queue, regardless of party or raid status.
<li><b>First Available</b> - You'll be placed into a Battleground wherever there's an open spot, whether it's to start a new instance or fill up one that hasn't reached its capacity. Joining the queue using the "First Available" option will get you into the Battlegrounds more quickly than grouping and using the "Join as Group" option.
<li><b>Selecting a specific instance</b> (ex: Warsong Gulch 2) - If an Alliance Group and a Horde Group want to be be placed against each other, this is the best way to do it. However, if multiple groups choose the same instance, the instance is given out on a first-come-first-served basis, so there's no guarentee that groups can be matched up against each other.
</ul>
<p>
<b>While Waiting In The Queue</b>
<ul>
<li>Upon entering a queue, an icon attaches itself to the minimap.
<li>Right-clicking on the icon will give the player an option to change their queue selection or to leave the queue.
<li>Don't forget -- you can explore and play the game as you normally would while you wait! Roam around and play!
</ul>
<p>
<b>Getting in a Battleground</b>
<ul>
<li>Battleground instances are created when there are enough Horde and Alliance players in the queue to match the laucnh requirements
<li>Once an instance is created, the first set of queued players will receive a dialogue box noting that the instance is ready. The instance will not launch until the minimum player limit of the Battleground is met.
<li>If a player accepts, they'll be instantly ported into the instance and resurrected if needed.
<li>If a player hides the dialogue, the option to accept or decline can still be found through the icon attached to the minimap.
<li>If a player declines, through action or inaction, they will be removed from the queue.
</ul>
