                <b>Repair Robot</b><br>
                An engineering recipe is available in Blackrock Depths for a repair robot. This robot acts as a repair vendor for 10 minutes and will repair and purchase items for normal cost. This will allow dungeon groups and raids to get items repaired and sold without returning to town.
