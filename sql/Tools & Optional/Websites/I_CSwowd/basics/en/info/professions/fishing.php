Once you have made your cast, a fishing bobber will appear. Move your mouse over the fishing bobber, which should reveal the standard loot/interaction icon.
<P>

You must now wait until the fishing bobber moves and you see a splash. Fishing requires patience.  It's not for everybody.
A timer will appear and begin counting down after you cast. The timer indicates the length of your current cast. After the
timer is up, you'll have to cast again.
Right-click on the bobber as soon as possible after the bobber moves.
The game portion involves timing it right so that your right-click will result in the fish being caught and not getting away.
<P>

If you are successful, you will receive a loot box and you'll be able to catch the fish. If you are not successful a message will inform you that the fish got away.
<P>

The splash/hook may occur at different times along the timing bar.
Sometimes the splash/hook appears right before the time is up, requiring you to click quickly before the fishing operation is canceled.
As you fish more, you will likely become better at clicking at the right time.
<P>

Sometimes the bobber doesn't bob at all during the fishing period. Just cast again.
<P>

<B>"No fish are hooked"</B><BR>
This means you clicked on the fishing bobber at the wrong time. Try again.
<P>
<B>"Your fish got away!"</B><BR>
This means you are fishing in an area that is difficult for your skill level. If you receive this message a lot
or all of the time, you need to go to a lower level fishing area. If you only receive this message part of the time,
you're in the right area to gain skill-ups. You can reduce this from happening by getting a better fishing pole, applying
powerful bait, getting a fishing enchant or item, or by raising your skill (which these items do).
